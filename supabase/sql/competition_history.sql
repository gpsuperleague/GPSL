-- ---------------------------------------------------------------------------
-- Transfer season label (for club history records)
-- ---------------------------------------------------------------------------

CREATE OR REPLACE FUNCTION public.transfer_history_season_label(p_transfer_time timestamptz)
RETURNS text
LANGUAGE sql
STABLE
SECURITY DEFINER
SET search_path = public
AS $$
  SELECT coalesce(
    (
      SELECT s.label
      FROM public.competition_seasons s
      WHERE p_transfer_time IS NOT NULL
        AND s.started_at IS NOT NULL
        AND p_transfer_time >= s.started_at
        AND (s.ended_at IS NULL OR p_transfer_time < s.ended_at)
      ORDER BY s.started_at DESC
      LIMIT 1
    ),
    (
      SELECT s.label
      FROM public.competition_seasons s
      WHERE s.is_current
      ORDER BY s.id DESC
      LIMIT 1
    ),
    to_char(p_transfer_time AT TIME ZONE 'UTC', 'YYYY')
  );
$$;

-- ---------------------------------------------------------------------------
-- Club history bundle RPC
-- ---------------------------------------------------------------------------

CREATE OR REPLACE FUNCTION public.competition_club_history_bundle(p_club_short_name text)
RETURNS jsonb
LANGUAGE plpgsql
STABLE
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_club text := upper(btrim(p_club_short_name));
  v_honours jsonb;
  v_seasons jsonb;
  v_awards jsonb;
  v_records jsonb;
BEGIN
  IF v_club IS NULL OR v_club = '' THEN
    RETURN '{}'::jsonb;
  END IF;

  SELECT coalesce(jsonb_agg(row_to_json(h) ORDER BY h.honoured_at DESC), '[]'::jsonb)
  INTO v_honours
  FROM public.competition_club_honours_public h
  WHERE h.club_short_name = v_club;

  SELECT coalesce(jsonb_agg(row_to_json(s) ORDER BY s.season_label DESC), '[]'::jsonb)
  INTO v_seasons
  FROM public.competition_club_season_history_public s
  WHERE s.club_short_name = v_club;

  SELECT coalesce(jsonb_agg(row_to_json(a) ORDER BY a.season_label DESC), '[]'::jsonb)
  INTO v_awards
  FROM public.competition_season_awards_public a
  WHERE a.club_short_name = v_club
    AND a.award_type = 'ballon_dor';

  WITH career AS (
    SELECT *
    FROM public.competition_player_career_public
    WHERE club_short_name = v_club
  ),
  totals AS (
    SELECT
      player_id,
      max(player_name) AS player_name,
      sum(goals)::int AS total_goals,
      sum(assists)::int AS total_assists,
      sum(potm_awards)::int AS total_potm,
      sum(appearances)::int AS total_apps
    FROM career
    GROUP BY player_id
  ),
  season_totals AS (
    SELECT
      player_id,
      max(player_name) AS player_name,
      season_label,
      sum(goals)::int AS goals,
      sum(assists)::int AS assists,
      sum(potm_awards)::int AS potm_awards
    FROM career
    GROUP BY player_id, season_label
  )
  SELECT jsonb_build_object(
    'all_time_top_scorer',
      (SELECT row_to_json(t) FROM totals t ORDER BY total_goals DESC, total_assists DESC LIMIT 1),
    'all_time_top_assists',
      (SELECT row_to_json(t) FROM totals t ORDER BY total_assists DESC, total_goals DESC LIMIT 1),
    'all_time_top_potm',
      (SELECT row_to_json(t) FROM totals t ORDER BY total_potm DESC, total_goals DESC LIMIT 1),
    'season_top_goals',
      (SELECT row_to_json(s) FROM season_totals s ORDER BY goals DESC, assists DESC LIMIT 1),
    'season_top_assists',
      (SELECT row_to_json(s) FROM season_totals s ORDER BY assists DESC, goals DESC LIMIT 1),
    'season_top_potm',
      (SELECT row_to_json(s) FROM season_totals s ORDER BY potm_awards DESC, goals DESC LIMIT 1),
    'record_signing',
      (
        SELECT row_to_json(x)
        FROM (
          SELECT
            h.player_id::text AS player_id,
            p."Name" AS player_name,
            h.fee::numeric AS fee,
            coalesce(h.agent_fee, 0)::numeric AS agent_fee,
            (coalesce(h.fee, 0) + coalesce(h.agent_fee, 0))::numeric AS total_cost,
            public.transfer_history_season_label(h.transfer_time) AS season_label,
            h.seller_club_id,
            h.buyer_club_id,
            h.foreign_buyer_name,
            h.transfer_sale_note,
            h.transfer_time
          FROM public."Transfer_History" h
          LEFT JOIN public."Players" p ON p."Konami_ID"::text = h.player_id::text
          WHERE upper(btrim(h.buyer_club_id)) = v_club
            AND coalesce(h.fee, 0) > 0
          ORDER BY
            (coalesce(h.fee, 0) + coalesce(h.agent_fee, 0)) DESC,
            h.fee DESC,
            h.transfer_time DESC
          LIMIT 1
        ) x
      ),
    'record_sale',
      (
        SELECT row_to_json(x)
        FROM (
          SELECT
            h.player_id::text AS player_id,
            p."Name" AS player_name,
            h.fee::numeric AS fee,
            public.transfer_history_season_label(h.transfer_time) AS season_label,
            h.seller_club_id,
            h.buyer_club_id,
            h.foreign_buyer_name,
            h.transfer_sale_note,
            h.transfer_time
          FROM public."Transfer_History" h
          LEFT JOIN public."Players" p ON p."Konami_ID"::text = h.player_id::text
          WHERE upper(btrim(h.seller_club_id)) = v_club
            AND coalesce(h.fee, 0) > 0
          ORDER BY h.fee DESC, h.transfer_time DESC
          LIMIT 1
        ) x
      )
  )
  INTO v_records;

  RETURN jsonb_build_object(
    'club_short_name', v_club,
    'honours', coalesce(v_honours, '[]'::jsonb),
    'seasons', coalesce(v_seasons, '[]'::jsonb),
    'ballon_winners', coalesce(v_awards, '[]'::jsonb),
    'records', coalesce(v_records, '{}'::jsonb)
  );
END;
$function$;

CREATE OR REPLACE FUNCTION public.competition_player_career_bundle(p_player_id text)
RETURNS jsonb
LANGUAGE plpgsql
STABLE
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_pid text := btrim(p_player_id);
  v_player jsonb;
  v_stints jsonb;
  v_awards jsonb;
  v_totals jsonb;
BEGIN
  SELECT to_jsonb(p)
  INTO v_player
  FROM (
    SELECT
      p."Konami_ID" AS player_id,
      p."Name" AS player_name,
      p."Position" AS position,
      p."Rating" AS rating,
      p."Nation" AS nation,
      p."Contracted_Team" AS current_club
    FROM public."Players" p
    WHERE p."Konami_ID"::text = v_pid
    LIMIT 1
  ) p;

  SELECT coalesce(jsonb_agg(row_to_json(c) ORDER BY c.season_label DESC), '[]'::jsonb)
  INTO v_stints
  FROM public.competition_player_career_public c
  WHERE c.player_id = v_pid;

  SELECT coalesce(jsonb_agg(row_to_json(a) ORDER BY a.season_label DESC), '[]'::jsonb)
  INTO v_awards
  FROM public.competition_season_awards_public a
  WHERE a.player_id = v_pid;

  SELECT jsonb_build_object(
    'appearances', coalesce(sum(appearances), 0),
    'goals', coalesce(sum(goals), 0),
    'assists', coalesce(sum(assists), 0),
    'potm_awards', coalesce(sum(potm_awards), 0),
    'clean_sheets', coalesce(sum(clean_sheets), 0),
    'avg_rating', round(avg(avg_rating) FILTER (WHERE avg_rating IS NOT NULL), 2)
  )
  INTO v_totals
  FROM public.competition_player_career_public c
  WHERE c.player_id = v_pid;

  RETURN jsonb_build_object(
    'player', coalesce(v_player, '{}'::jsonb),
    'stints', coalesce(v_stints, '[]'::jsonb),
    'awards', coalesce(v_awards, '[]'::jsonb),
    'totals', coalesce(v_totals, '{}'::jsonb)
  );
END;
$function$;


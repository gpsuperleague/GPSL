CREATE OR REPLACE VIEW public.competition_club_honours_public
WITH (security_invoker = false)
AS
SELECT
  a.club_short_name,
  c."Club" AS club_name,
  a.season_label,
  a.season_id,
  'league_champion'::text AS honour_type,
  CASE a.division
    WHEN 'superleague' THEN 'SuperLeague champions'
    WHEN 'championship_a' THEN 'Championship A champions'
    WHEN 'championship_b' THEN 'Championship B champions'
    ELSE a.division
  END AS honour_label,
  a.division,
  NULL::text AS cup_code,
  a.created_at AS honoured_at
FROM public.competition_club_season_archive a
JOIN public."Clubs" c ON c."ShortName" = a.club_short_name
WHERE a.final_position = 1

UNION ALL

SELECT
  w.winner_club_short_name,
  c."Club",
  w.season_label,
  w.season_id,
  'cup_winner',
  CASE w.cup_code
    WHEN 'super8' THEN 'Super8 winners'
    WHEN 'plate' THEN 'Plate winners'
    WHEN 'shield' THEN 'Shield winners'
    WHEN 'bowl' THEN 'Bowl winners'
    WHEN 'league_cup' THEN 'League Cup winners'
    ELSE w.cup_code
  END,
  NULL,
  w.cup_code,
  w.archived_at
FROM public.competition_cup_season_winner w
JOIN public."Clubs" c ON c."ShortName" = w.winner_club_short_name;

CREATE OR REPLACE VIEW public.competition_player_career_public
WITH (security_invoker = false)
AS
SELECT
  a.season_id,
  a.season_label,
  a.player_id,
  p."Name" AS player_name,
  a.club_short_name,
  c."Club" AS club_name,
  a.division,
  a.player_position,
  a.stat_role,
  a.appearances,
  a.starts,
  a.goals,
  a.assists,
  a.avg_rating,
  a.potm_awards,
  a.clean_sheets,
  a.ballon_points,
  false AS is_live,
  a.archived_at AS as_of
FROM public.competition_player_season_archive a
JOIN public."Players" p ON p."Konami_ID"::text = a.player_id
JOIN public."Clubs" c ON c."ShortName" = a.club_short_name

UNION ALL

SELECT
  m.season_id,
  s.label,
  m.player_id,
  p."Name",
  m.club_short_name,
  c."Club",
  ccs.division,
  p."Position",
  public.competition_player_stat_role(p."Position"),
  count(*) FILTER (WHERE m.appeared)::int,
  count(*) FILTER (WHERE m.started)::int,
  coalesce(sum(m.goals), 0)::int,
  coalesce(sum(m.assists), 0)::int,
  round(avg(m.rating) FILTER (WHERE m.rating IS NOT NULL), 2),
  count(*) FILTER (WHERE m.is_player_of_match)::int,
  public.competition_player_clean_sheets(m.season_id, m.player_id, m.club_short_name, true),
  public.competition_player_ballon_points(
    count(*) FILTER (WHERE m.appeared)::int,
    coalesce(sum(m.goals), 0)::int,
    coalesce(sum(m.assists), 0)::int,
    round(avg(m.rating) FILTER (WHERE m.rating IS NOT NULL), 2),
    count(*) FILTER (WHERE m.is_player_of_match)::int,
    public.competition_player_clean_sheets(m.season_id, m.player_id, m.club_short_name, true),
    public.competition_player_stat_role(p."Position")
  ),
  true,
  now()
FROM public.competition_match_player_stats m
JOIN public.competition_fixtures f ON f.id = m.fixture_id
JOIN public.competition_seasons s ON s.id = m.season_id
JOIN public."Players" p ON p."Konami_ID"::text = m.player_id
JOIN public."Clubs" c ON c."ShortName" = m.club_short_name
LEFT JOIN public.competition_club_seasons ccs
  ON ccs.season_id = m.season_id AND ccs.club_short_name = m.club_short_name
WHERE s.is_current = true
  AND s.status = 'active'
  AND f.status = 'played'
  AND NOT EXISTS (
    SELECT 1
    FROM public.competition_player_season_archive ar
    WHERE ar.season_id = m.season_id
      AND ar.player_id = m.player_id
      AND ar.club_short_name = m.club_short_name
  )
GROUP BY
  m.season_id, s.label, m.player_id, p."Name", m.club_short_name,
  c."Club", ccs.division, p."Position";


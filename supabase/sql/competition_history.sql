CREATE OR REPLACE FUNCTION public.competition_player_stat_role(p_position text)
RETURNS text
LANGUAGE sql
IMMUTABLE
AS $$
  SELECT CASE upper(btrim(coalesce(p_position, '')))
    WHEN 'GK' THEN 'goalkeeper'
    WHEN 'CB' THEN 'defender'
    WHEN 'LB' THEN 'defender'
    WHEN 'RB' THEN 'defender'
    WHEN 'LWB' THEN 'defender'
    WHEN 'RWB' THEN 'defender'
    WHEN 'DMF' THEN 'defender'
    WHEN 'CMF' THEN 'midfielder'
    WHEN 'LMF' THEN 'midfielder'
    WHEN 'RMF' THEN 'midfielder'
    WHEN 'AMF' THEN 'midfielder'
    WHEN 'WG' THEN 'midfielder'
    WHEN 'SS' THEN 'forward'
    WHEN 'CF' THEN 'forward'
    ELSE 'outfield'
  END;
$$;

CREATE OR REPLACE FUNCTION public.competition_player_conceded_in_fixture(
  p_fixture_id bigint,
  p_club_short_name text
)
RETURNS int
LANGUAGE sql
STABLE
SECURITY DEFINER
SET search_path = public
AS $$
  SELECT CASE
    WHEN f.home_club_short_name = p_club_short_name THEN coalesce(f.away_goals, 0)
    ELSE coalesce(f.home_goals, 0)
  END::int
  FROM public.competition_fixtures f
  WHERE f.id = p_fixture_id;
$$;

CREATE OR REPLACE FUNCTION public.competition_player_clean_sheets(
  p_season_id bigint,
  p_player_id text,
  p_club_short_name text DEFAULT NULL,
  p_include_cups boolean DEFAULT true
)
RETURNS int
LANGUAGE sql
STABLE
SECURITY DEFINER
SET search_path = public
AS $$
  SELECT count(*)::int
  FROM public.competition_match_player_stats m
  JOIN public.competition_fixtures f ON f.id = m.fixture_id
  JOIN public."Players" p ON p."Konami_ID"::text = m.player_id
  WHERE m.season_id = p_season_id
    AND m.player_id = btrim(p_player_id)
    AND m.started = true
    AND f.status = 'played'
    AND (
      p_include_cups
      OR f.competition_type = 'league'
    )
    AND (
      p_club_short_name IS NULL
      OR m.club_short_name = btrim(p_club_short_name)
    )
    AND public.competition_player_stat_role(p."Position") IN ('goalkeeper', 'defender')
    AND public.competition_player_conceded_in_fixture(f.id, m.club_short_name) = 0;
$$;

-- Ballon d'Or points (league + cups; role-weighted)
CREATE OR REPLACE FUNCTION public.competition_player_ballon_points(
  p_appearances int,
  p_goals int,
  p_assists int,
  p_avg_rating numeric,
  p_potm int,
  p_clean_sheets int,
  p_stat_role text
)
RETURNS numeric
LANGUAGE plpgsql
IMMUTABLE
AS $function$
DECLARE
  v_apps numeric := greatest(coalesce(p_appearances, 0), 0);
  v_goals numeric := greatest(coalesce(p_goals, 0), 0);
  v_assists numeric := greatest(coalesce(p_assists, 0), 0);
  v_rating numeric := coalesce(p_avg_rating, 0);
  v_potm numeric := greatest(coalesce(p_potm, 0), 0);
  v_cs numeric := greatest(coalesce(p_clean_sheets, 0), 0);
  v_role text := coalesce(p_stat_role, 'outfield');
BEGIN
  IF v_role = 'goalkeeper' THEN
    RETURN round(
      v_cs * 18 + v_potm * 15 + v_rating * 12 + v_apps * 1 + v_assists * 2,
      2
    );
  ELSIF v_role = 'defender' THEN
    RETURN round(
      v_cs * 12 + v_goals * 6 + v_assists * 4 + v_potm * 14 + v_rating * 10 + v_apps * 0.5,
      2
    );
  ELSIF v_role = 'midfielder' THEN
    RETURN round(
      v_goals * 8 + v_assists * 6 + v_potm * 16 + v_rating * 10 + v_apps * 0.4 + v_cs * 2,
      2
    );
  ELSIF v_role = 'forward' THEN
    RETURN round(
      v_goals * 10 + v_assists * 5 + v_potm * 18 + v_rating * 9 + v_apps * 0.3,
      2
    );
  END IF;

  RETURN round(
    v_goals * 8 + v_assists * 5 + v_potm * 14 + v_rating * 9 + v_apps * 0.3,
    2
  );
END;
$function$;

-- Aggregate live stats for one player/club/season from match rows
CREATE OR REPLACE FUNCTION public.competition_aggregate_player_season_row(
  p_season_id bigint,
  p_player_id text,
  p_club_short_name text
)
RETURNS TABLE (
  appearances int,
  starts int,
  goals int,
  assists int,
  avg_rating numeric,
  potm_awards int,
  clean_sheets int,
  player_position text,
  stat_role text,
  ballon_points numeric
)
LANGUAGE plpgsql
STABLE
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_apps int;
  v_starts int;
  v_goals int;
  v_assists int;
  v_avg numeric;
  v_potm int;
  v_cs int;
  v_pos text;
  v_role text;
BEGIN
  SELECT
    count(*) FILTER (WHERE m.appeared)::int,
    count(*) FILTER (WHERE m.started)::int,
    coalesce(sum(m.goals), 0)::int,
    coalesce(sum(m.assists), 0)::int,
    round(avg(m.rating) FILTER (WHERE m.rating IS NOT NULL), 2),
    count(*) FILTER (WHERE m.is_player_of_match)::int
  INTO v_apps, v_starts, v_goals, v_assists, v_avg, v_potm
  FROM public.competition_match_player_stats m
  JOIN public.competition_fixtures f ON f.id = m.fixture_id
  WHERE m.season_id = p_season_id
    AND m.player_id = btrim(p_player_id)
    AND m.club_short_name = btrim(p_club_short_name)
    AND f.status = 'played';

  SELECT p."Position"
  INTO v_pos
  FROM public."Players" p
  WHERE p."Konami_ID"::text = btrim(p_player_id)
  LIMIT 1;

  v_role := public.competition_player_stat_role(v_pos);
  v_cs := public.competition_player_clean_sheets(
    p_season_id, p_player_id, p_club_short_name, true
  );

  RETURN QUERY
  SELECT
    coalesce(v_apps, 0),
    coalesce(v_starts, 0),
    coalesce(v_goals, 0),
    coalesce(v_assists, 0),
    v_avg,
    coalesce(v_potm, 0),
    coalesce(v_cs, 0),
    v_pos,
    v_role,
    public.competition_player_ballon_points(
      coalesce(v_apps, 0), coalesce(v_goals, 0), coalesce(v_assists, 0),
      v_avg, coalesce(v_potm, 0), coalesce(v_cs, 0), v_role
    );
END;
$function$;

-- ---------------------------------------------------------------------------
-- Archive season (admin or SQL Editor)
-- ---------------------------------------------------------------------------

CREATE OR REPLACE FUNCTION public.competition_admin_archive_season(p_season_id bigint DEFAULT NULL)
RETURNS jsonb
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_season public.competition_seasons%rowtype;
  v_row record;
  v_cup record;
  v_player record;
  v_agg record;
  v_max_round int;
  v_winner text;
  v_runner text;
  v_fix_id bigint;
  v_ballon record;
  v_boot record;
  v_play record;
  v_potm record;
  v_glove record;
  v_clubs int := 0;
  v_players int := 0;
  v_cups int := 0;
  v_finance int := 0;
BEGIN
  IF auth.uid() IS NOT NULL AND NOT public.is_gpsl_admin() THEN
    RAISE EXCEPTION 'Admin only';
  END IF;

  IF p_season_id IS NULL THEN
    SELECT * INTO v_season
    FROM public.competition_seasons
    WHERE is_current = true
    ORDER BY id DESC
    LIMIT 1;
  ELSE
    SELECT * INTO v_season FROM public.competition_seasons WHERE id = p_season_id;
  END IF;

  IF NOT FOUND THEN
    RAISE EXCEPTION 'Season not found';
  END IF;

  -- League standings → club archive
  FOR v_row IN
    SELECT *
    FROM public.competition_standings_public s
    WHERE s.season_id = v_season.id
    ORDER BY s.division, s.table_position
  LOOP
    INSERT INTO public.competition_club_season_archive (
      club_short_name, season_label, division, final_position,
      season_id, mp, won, drawn, lost, gf, ga, gd, pts
    )
    VALUES (
      v_row.club_short_name,
      v_season.label,
      v_row.division,
      v_row.table_position::smallint,
      v_season.id,
      v_row.mp::smallint,
      v_row.w::smallint,
      v_row.d::smallint,
      v_row.l::smallint,
      v_row.gf::smallint,
      v_row.ga::smallint,
      v_row.gd::smallint,
      v_row.pts::smallint
    )
    ON CONFLICT (club_short_name, season_label)
    DO UPDATE SET
      division = excluded.division,
      final_position = excluded.final_position,
      season_id = excluded.season_id,
      mp = excluded.mp,
      won = excluded.won,
      drawn = excluded.drawn,
      lost = excluded.lost,
      gf = excluded.gf,
      ga = excluded.ga,
      gd = excluded.gd,
      pts = excluded.pts;

    v_clubs := v_clubs + 1;
  END LOOP;

  -- Cup winners (final round)
  FOR v_cup IN
    SELECT unnest(ARRAY['super8','plate','shield','bowl','league_cup']) AS cup_code
  LOOP
    SELECT max(n.round_no) INTO v_max_round
    FROM public.competition_cup_bracket_nodes n
    WHERE n.season_id = v_season.id AND n.cup_code = v_cup.cup_code;

    IF v_max_round IS NULL THEN
      CONTINUE;
    END IF;

    SELECT n.winner_club_short_name, n.fixture_id
    INTO v_winner, v_fix_id
    FROM public.competition_cup_bracket_nodes n
    WHERE n.season_id = v_season.id
      AND n.cup_code = v_cup.cup_code
      AND n.round_no = v_max_round
      AND n.winner_club_short_name IS NOT NULL
    ORDER BY n.match_no
    LIMIT 1;

    IF v_winner IS NULL THEN
      CONTINUE;
    END IF;

    SELECT CASE
      WHEN f.home_club_short_name = v_winner THEN f.away_club_short_name
      ELSE f.home_club_short_name
    END
    INTO v_runner
    FROM public.competition_fixtures f
    WHERE f.id = v_fix_id;

    INSERT INTO public.competition_cup_season_winner (
      season_id, season_label, cup_code,
      winner_club_short_name, runner_up_club_short_name, final_fixture_id
    )
    VALUES (
      v_season.id, v_season.label, v_cup.cup_code,
      v_winner, v_runner, v_fix_id
    )
    ON CONFLICT (season_id, cup_code)
    DO UPDATE SET
      winner_club_short_name = excluded.winner_club_short_name,
      runner_up_club_short_name = excluded.runner_up_club_short_name,
      final_fixture_id = excluded.final_fixture_id,
      archived_at = now();

    v_cups := v_cups + 1;
  END LOOP;

  -- Player season rows
  FOR v_player IN
    SELECT DISTINCT m.player_id, m.club_short_name, ccs.division
    FROM public.competition_match_player_stats m
    JOIN public.competition_fixtures f ON f.id = m.fixture_id
    LEFT JOIN public.competition_club_seasons ccs
      ON ccs.season_id = m.season_id AND ccs.club_short_name = m.club_short_name
    WHERE m.season_id = v_season.id
      AND f.status = 'played'
      AND m.appeared = true
  LOOP
    SELECT * INTO v_agg
    FROM public.competition_aggregate_player_season_row(
      v_season.id, v_player.player_id, v_player.club_short_name
    );

    INSERT INTO public.competition_player_season_archive (
      season_id, season_label, player_id, club_short_name, division,
      player_position, stat_role,
      appearances, starts, goals, assists, avg_rating, potm_awards,
      clean_sheets, ballon_points
    )
    VALUES (
      v_season.id, v_season.label, v_player.player_id, v_player.club_short_name,
      v_player.division, v_agg.player_position, v_agg.stat_role,
      v_agg.appearances, v_agg.starts, v_agg.goals, v_agg.assists,
      v_agg.avg_rating, v_agg.potm_awards, v_agg.clean_sheets, v_agg.ballon_points
    )
    ON CONFLICT (season_id, player_id, club_short_name)
    DO UPDATE SET
      division = excluded.division,
      player_position = excluded.player_position,
      stat_role = excluded.stat_role,
      appearances = excluded.appearances,
      starts = excluded.starts,
      goals = excluded.goals,
      assists = excluded.assists,
      avg_rating = excluded.avg_rating,
      potm_awards = excluded.potm_awards,
      clean_sheets = excluded.clean_sheets,
      ballon_points = excluded.ballon_points,
      archived_at = now();

    v_players := v_players + 1;
  END LOOP;

  -- Season awards (min 5 league apps for Ballon)
  DELETE FROM public.competition_season_award WHERE season_id = v_season.id;

  SELECT a.*
  INTO v_ballon
  FROM public.competition_player_season_archive a
  WHERE a.season_id = v_season.id
    AND a.appearances >= 5
  ORDER BY a.ballon_points DESC, a.goals DESC, a.assists DESC
  LIMIT 1;

  IF FOUND AND v_ballon.ballon_points > 0 THEN
    INSERT INTO public.competition_season_award (
      season_id, season_label, award_type, player_id, club_short_name,
      stat_value, detail
    )
    VALUES (
      v_season.id, v_season.label, 'ballon_dor',
      v_ballon.player_id, v_ballon.club_short_name,
      v_ballon.ballon_points,
      jsonb_build_object(
        'goals', v_ballon.goals,
        'assists', v_ballon.assists,
        'potm', v_ballon.potm_awards,
        'clean_sheets', v_ballon.clean_sheets,
        'avg_rating', v_ballon.avg_rating,
        'stat_role', v_ballon.stat_role
      )
    );
  END IF;

  SELECT a.*
  INTO v_boot
  FROM public.competition_player_season_archive a
  WHERE a.season_id = v_season.id AND a.goals > 0
  ORDER BY a.goals DESC, a.assists DESC
  LIMIT 1;

  IF FOUND THEN
    INSERT INTO public.competition_season_award (
      season_id, season_label, award_type, player_id, club_short_name, stat_value
    )
    VALUES (
      v_season.id, v_season.label, 'golden_boot',
      v_boot.player_id, v_boot.club_short_name, v_boot.goals
    );
  END IF;

  SELECT a.*
  INTO v_play
  FROM public.competition_player_season_archive a
  WHERE a.season_id = v_season.id AND a.assists > 0
  ORDER BY a.assists DESC, a.goals DESC
  LIMIT 1;

  IF FOUND THEN
    INSERT INTO public.competition_season_award (
      season_id, season_label, award_type, player_id, club_short_name, stat_value
    )
    VALUES (
      v_season.id, v_season.label, 'golden_playmaker',
      v_play.player_id, v_play.club_short_name, v_play.assists
    );
  END IF;

  SELECT a.*
  INTO v_potm
  FROM public.competition_player_season_archive a
  WHERE a.season_id = v_season.id AND a.potm_awards > 0
  ORDER BY a.potm_awards DESC, a.goals DESC
  LIMIT 1;

  IF FOUND THEN
    INSERT INTO public.competition_season_award (
      season_id, season_label, award_type, player_id, club_short_name, stat_value
    )
    VALUES (
      v_season.id, v_season.label, 'season_potm',
      v_potm.player_id, v_potm.club_short_name, v_potm.potm_awards
    );
  END IF;

  SELECT a.*
  INTO v_glove
  FROM public.competition_player_season_archive a
  WHERE a.season_id = v_season.id
    AND a.stat_role = 'goalkeeper'
    AND a.clean_sheets > 0
  ORDER BY a.clean_sheets DESC, a.avg_rating DESC NULLS LAST
  LIMIT 1;

  IF FOUND THEN
    INSERT INTO public.competition_season_award (
      season_id, season_label, award_type, player_id, club_short_name, stat_value
    )
    VALUES (
      v_season.id, v_season.label, 'golden_glove',
      v_glove.player_id, v_glove.club_short_name, v_glove.clean_sheets
    );
  END IF;

  v_finance := public.competition_archive_club_finances_for_season(v_season.id);

  RETURN jsonb_build_object(
    'ok', true,
    'season_id', v_season.id,
    'season_label', v_season.label,
    'clubs_archived', v_clubs,
    'players_archived', v_players,
    'cups_archived', v_cups,
    'clubs_finance_archived', v_finance
  );
END;
$function$;


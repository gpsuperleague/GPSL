-- ---------------------------------------------------------------------------
-- Extend club season archive (full league row + season_id)
-- ---------------------------------------------------------------------------

DO $$
BEGIN
  IF NOT EXISTS (
    SELECT 1 FROM information_schema.columns
    WHERE table_schema = 'public' AND table_name = 'competition_club_season_archive'
      AND column_name = 'season_id'
  ) THEN
    ALTER TABLE public.competition_club_season_archive
      ADD COLUMN season_id bigint REFERENCES public.competition_seasons (id) ON DELETE SET NULL;
  END IF;

  IF NOT EXISTS (
    SELECT 1 FROM information_schema.columns
    WHERE table_schema = 'public' AND table_name = 'competition_club_season_archive'
      AND column_name = 'mp'
  ) THEN
    ALTER TABLE public.competition_club_season_archive
      ADD COLUMN mp smallint,
      ADD COLUMN won smallint,
      ADD COLUMN drawn smallint,
      ADD COLUMN lost smallint,
      ADD COLUMN gf smallint,
      ADD COLUMN ga smallint,
      ADD COLUMN gd smallint,
      ADD COLUMN pts smallint;
  END IF;
END $$;

-- ---------------------------------------------------------------------------
-- Cup champions per season
-- ---------------------------------------------------------------------------

CREATE TABLE IF NOT EXISTS public.competition_cup_season_winner (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  season_id bigint NOT NULL REFERENCES public.competition_seasons (id) ON DELETE CASCADE,
  season_label text NOT NULL,
  cup_code text NOT NULL CHECK (
    cup_code IN ('super8', 'plate', 'shield', 'bowl', 'league_cup')
  ),
  winner_club_short_name text NOT NULL REFERENCES public."Clubs" ("ShortName"),
  runner_up_club_short_name text REFERENCES public."Clubs" ("ShortName"),
  final_fixture_id bigint REFERENCES public.competition_fixtures (id) ON DELETE SET NULL,
  archived_at timestamptz NOT NULL DEFAULT now(),
  CONSTRAINT competition_cup_season_winner_unique UNIQUE (season_id, cup_code)
);

CREATE INDEX IF NOT EXISTS competition_cup_season_winner_club_idx
  ON public.competition_cup_season_winner (winner_club_short_name, season_id DESC);

-- ---------------------------------------------------------------------------
-- Player season archive (survives season rollover)
-- ---------------------------------------------------------------------------

CREATE TABLE IF NOT EXISTS public.competition_player_season_archive (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  season_id bigint NOT NULL REFERENCES public.competition_seasons (id) ON DELETE CASCADE,
  season_label text NOT NULL,
  player_id text NOT NULL,
  club_short_name text NOT NULL REFERENCES public."Clubs" ("ShortName"),
  division text CHECK (
    division IS NULL OR division IN ('superleague', 'championship_a', 'championship_b')
  ),
  player_position text,
  stat_role text NOT NULL DEFAULT 'outfield' CHECK (
    stat_role IN ('goalkeeper', 'defender', 'midfielder', 'forward', 'outfield')
  ),
  appearances int NOT NULL DEFAULT 0,
  starts int NOT NULL DEFAULT 0,
  goals int NOT NULL DEFAULT 0,
  assists int NOT NULL DEFAULT 0,
  avg_rating numeric(4, 2),
  potm_awards int NOT NULL DEFAULT 0,
  clean_sheets int NOT NULL DEFAULT 0,
  ballon_points numeric(10, 2) NOT NULL DEFAULT 0,
  archived_at timestamptz NOT NULL DEFAULT now(),
  CONSTRAINT competition_player_season_archive_unique
    UNIQUE (season_id, player_id, club_short_name)
);

CREATE INDEX IF NOT EXISTS competition_player_season_archive_player_idx
  ON public.competition_player_season_archive (player_id, season_id DESC);

CREATE INDEX IF NOT EXISTS competition_player_season_archive_club_idx
  ON public.competition_player_season_archive (club_short_name, season_id DESC);

-- ---------------------------------------------------------------------------
-- Season awards (Ballon d'Or, golden boot, etc.)
-- ---------------------------------------------------------------------------

CREATE TABLE IF NOT EXISTS public.competition_season_award (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  season_id bigint NOT NULL REFERENCES public.competition_seasons (id) ON DELETE CASCADE,
  season_label text NOT NULL,
  award_type text NOT NULL CHECK (
    award_type IN (
      'ballon_dor',
      'golden_boot',
      'golden_playmaker',
      'golden_glove',
      'season_potm'
    )
  ),
  player_id text NOT NULL,
  club_short_name text NOT NULL REFERENCES public."Clubs" ("ShortName"),
  stat_value numeric(12, 2) NOT NULL DEFAULT 0,
  detail jsonb NOT NULL DEFAULT '{}'::jsonb,
  awarded_at timestamptz NOT NULL DEFAULT now(),
  CONSTRAINT competition_season_award_unique UNIQUE (season_id, award_type)
);

CREATE INDEX IF NOT EXISTS competition_season_award_player_idx
  ON public.competition_season_award (player_id);


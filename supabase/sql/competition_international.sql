CREATE OR REPLACE FUNCTION public.international_owner_draft_order()
RETURNS TABLE (
  pick_order smallint,
  club_short_name text,
  club_name text,
  rank_points numeric,
  nation_code text,
  nation_name text,
  flag_emoji text
)
LANGUAGE sql
STABLE
SECURITY DEFINER
SET search_path = public
AS $$
  SELECT
    row_number() OVER (
      ORDER BY coalesce(r.rank_points, 0) DESC, c."ShortName"
    )::smallint AS pick_order,
    c."ShortName" AS club_short_name,
    c."Club" AS club_name,
    coalesce(r.rank_points, 0) AS rank_points,
    ion.nation_code,
    n.name AS nation_name,
    n.flag_emoji
  FROM public."Clubs" c
  LEFT JOIN public.international_owner_rank r ON r.club_short_name = c."ShortName"
  LEFT JOIN public.international_owner_nations ion
    ON ion.club_short_name = c."ShortName" AND ion.is_active = true
  LEFT JOIN public.international_nations n ON n.code = ion.nation_code
  WHERE c.owner_id IS NOT NULL
  ORDER BY pick_order;
$$;


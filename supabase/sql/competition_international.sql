CREATE OR REPLACE FUNCTION public.competition_season_ordinal(p_season_id bigint)
RETURNS integer
LANGUAGE sql
STABLE
SECURITY DEFINER
SET search_path = public
AS $$
  SELECT s.rn::integer
  FROM (
    SELECT id, row_number() OVER (ORDER BY id) AS rn
    FROM public.competition_seasons
  ) s
  WHERE s.id = p_season_id;
$$;

CREATE OR REPLACE FUNCTION public.international_my_nation_code()
RETURNS text
LANGUAGE sql
STABLE
SECURITY DEFINER
SET search_path = public
AS $$
  SELECT ion.nation_code
  FROM public.international_owner_nations ion
  JOIN public."Clubs" c ON c."ShortName" = ion.club_short_name
  WHERE ion.is_active = true
    AND c.owner_id = auth.uid()
  LIMIT 1;
$$;


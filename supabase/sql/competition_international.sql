-- ---------------------------------------------------------------------------
-- Nations (60 FIFA-style teams)
-- ---------------------------------------------------------------------------

CREATE TABLE IF NOT EXISTS public.international_nations (
  code text PRIMARY KEY CHECK (code ~ '^[A-Z]{3}$'),
  name text NOT NULL,
  flag_emoji text NOT NULL DEFAULT '🏳️',
  seed_rank smallint NOT NULL CHECK (seed_rank >= 1 AND seed_rank <= 32767),
  active boolean NOT NULL DEFAULT true,
  created_at timestamptz NOT NULL DEFAULT now()
);

-- ---------------------------------------------------------------------------
-- Owner rank points (admin sets; draft order = rank_points DESC, club name)
-- ---------------------------------------------------------------------------

CREATE TABLE IF NOT EXISTS public.international_owner_rank (
  club_short_name text PRIMARY KEY REFERENCES public."Clubs" ("ShortName") ON DELETE CASCADE,
  rank_points numeric(10, 2) NOT NULL DEFAULT 0,
  updated_at timestamptz NOT NULL DEFAULT now()
);

-- ---------------------------------------------------------------------------
-- Owner ↔ nation assignments (history kept; one active per club)
-- ---------------------------------------------------------------------------

CREATE TABLE IF NOT EXISTS public.international_owner_nations (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  club_short_name text NOT NULL REFERENCES public."Clubs" ("ShortName") ON DELETE CASCADE,
  nation_code text NOT NULL REFERENCES public.international_nations (code),
  cycle_id bigint,
  selection_phase text NOT NULL DEFAULT 'initial'
    CHECK (selection_phase IN ('initial', 'post_world_cup', 'admin')),
  is_active boolean NOT NULL DEFAULT true,
  locked_until_cycle_id bigint,
  assigned_at timestamptz NOT NULL DEFAULT now(),
  released_at timestamptz
);

CREATE UNIQUE INDEX IF NOT EXISTS international_owner_nations_one_active_club_idx
  ON public.international_owner_nations (club_short_name)
  WHERE is_active = true;

CREATE UNIQUE INDEX IF NOT EXISTS international_owner_nations_one_active_nation_idx
  ON public.international_owner_nations (nation_code)
  WHERE is_active = true;

-- ---------------------------------------------------------------------------
-- Nation selection windows (draft pick period)
-- ---------------------------------------------------------------------------

CREATE TABLE IF NOT EXISTS public.international_selection_windows (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  phase text NOT NULL CHECK (phase IN ('initial', 'post_world_cup')),
  is_open boolean NOT NULL DEFAULT false,
  opens_at timestamptz,
  closes_at timestamptz,
  current_pick_rank smallint NOT NULL DEFAULT 1,
  created_at timestamptz NOT NULL DEFAULT now()
);

-- ---------------------------------------------------------------------------
-- World Cup cycles (qualifying 2 seasons → finals between S4–5, S8–9, …)
-- ---------------------------------------------------------------------------

CREATE TABLE IF NOT EXISTS public.international_wc_cycles (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  cycle_no smallint NOT NULL UNIQUE CHECK (cycle_no >= 1),
  label text NOT NULL,
  qual_season_id_1 bigint REFERENCES public.competition_seasons (id),
  qual_season_id_2 bigint REFERENCES public.competition_seasons (id),
  finals_after_season_id bigint REFERENCES public.competition_seasons (id),
  status text NOT NULL DEFAULT 'setup'
    CHECK (status IN ('setup', 'qualifying', 'finals', 'complete')),
  created_at timestamptz NOT NULL DEFAULT now()
);

ALTER TABLE public.international_owner_nations
  ADD COLUMN IF NOT EXISTS cycle_id bigint REFERENCES public.international_wc_cycles (id);

-- ---------------------------------------------------------------------------
-- Qualifying: 12 groups × 5 nations
-- ---------------------------------------------------------------------------

CREATE TABLE IF NOT EXISTS public.international_qual_groups (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  cycle_id bigint NOT NULL REFERENCES public.international_wc_cycles (id) ON DELETE CASCADE,
  group_code text NOT NULL CHECK (group_code ~ '^[A-L]$'),
  CONSTRAINT international_qual_groups_unique UNIQUE (cycle_id, group_code)
);

CREATE TABLE IF NOT EXISTS public.international_qual_group_members (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  group_id bigint NOT NULL REFERENCES public.international_qual_groups (id) ON DELETE CASCADE,
  nation_code text NOT NULL REFERENCES public.international_nations (code),
  played smallint NOT NULL DEFAULT 0,
  won smallint NOT NULL DEFAULT 0,
  drawn smallint NOT NULL DEFAULT 0,
  lost smallint NOT NULL DEFAULT 0,
  goals_for smallint NOT NULL DEFAULT 0,
  goals_against smallint NOT NULL DEFAULT 0,
  points smallint NOT NULL DEFAULT 0,
  third_place_rank smallint,
  qualified boolean NOT NULL DEFAULT false,
  CONSTRAINT international_qual_group_members_unique UNIQUE (group_id, nation_code)
);

-- ---------------------------------------------------------------------------
-- Finals: 8 groups × 4 → 32 teams, top 2 per group to R16
-- ---------------------------------------------------------------------------

CREATE TABLE IF NOT EXISTS public.international_finals_groups (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  cycle_id bigint NOT NULL REFERENCES public.international_wc_cycles (id) ON DELETE CASCADE,
  group_code text NOT NULL CHECK (group_code ~ '^[A-H]$'),
  CONSTRAINT international_finals_groups_unique UNIQUE (cycle_id, group_code)
);

CREATE TABLE IF NOT EXISTS public.international_finals_group_members (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  group_id bigint NOT NULL REFERENCES public.international_finals_groups (id) ON DELETE CASCADE,
  nation_code text NOT NULL REFERENCES public.international_nations (code),
  played smallint NOT NULL DEFAULT 0,
  won smallint NOT NULL DEFAULT 0,
  drawn smallint NOT NULL DEFAULT 0,
  lost smallint NOT NULL DEFAULT 0,
  goals_for smallint NOT NULL DEFAULT 0,
  goals_against smallint NOT NULL DEFAULT 0,
  points smallint NOT NULL DEFAULT 0,
  qualified_knockout boolean NOT NULL DEFAULT false,
  CONSTRAINT international_finals_group_members_unique UNIQUE (group_id, nation_code)
);

-- ---------------------------------------------------------------------------
-- Knockout bracket (R16 → QF → SF → Final)
-- ---------------------------------------------------------------------------

CREATE TABLE IF NOT EXISTS public.international_knockout_nodes (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  cycle_id bigint NOT NULL REFERENCES public.international_wc_cycles (id) ON DELETE CASCADE,
  stage text NOT NULL CHECK (stage IN ('r16', 'qf', 'sf', 'final')),
  match_no smallint NOT NULL,
  nation_a text REFERENCES public.international_nations (code),
  nation_b text REFERENCES public.international_nations (code),
  goals_a smallint,
  goals_b smallint,
  winner_nation text REFERENCES public.international_nations (code),
  played boolean NOT NULL DEFAULT false,
  CONSTRAINT international_knockout_nodes_unique UNIQUE (cycle_id, stage, match_no)
);

-- ---------------------------------------------------------------------------
-- International fixtures (separate from club competition_fixtures)
-- ---------------------------------------------------------------------------

CREATE TABLE IF NOT EXISTS public.international_fixtures (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  cycle_id bigint NOT NULL REFERENCES public.international_wc_cycles (id) ON DELETE CASCADE,
  season_id bigint REFERENCES public.competition_seasons (id),
  phase text NOT NULL CHECK (phase IN ('qualifying', 'finals_group', 'knockout')),
  group_id bigint,
  knockout_node_id bigint REFERENCES public.international_knockout_nodes (id),
  home_nation text NOT NULL REFERENCES public.international_nations (code),
  away_nation text NOT NULL REFERENCES public.international_nations (code),
  home_goals smallint,
  away_goals smallint,
  played boolean NOT NULL DEFAULT false,
  played_at timestamptz,
  created_at timestamptz NOT NULL DEFAULT now()
);

-- ---------------------------------------------------------------------------
-- Squad call-ups (populates national team squad view)
-- ---------------------------------------------------------------------------

CREATE TABLE IF NOT EXISTS public.international_squad_callups (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  nation_code text NOT NULL REFERENCES public.international_nations (code),
  player_id text NOT NULL,
  club_short_name text NOT NULL REFERENCES public."Clubs" ("ShortName"),
  cycle_id bigint REFERENCES public.international_wc_cycles (id),
  called_at timestamptz NOT NULL DEFAULT now(),
  released_at timestamptz,
  is_active boolean NOT NULL DEFAULT true,
  CONSTRAINT international_squad_callups_unique UNIQUE (nation_code, player_id)
);

CREATE INDEX IF NOT EXISTS international_squad_callups_nation_active_idx
  ON public.international_squad_callups (nation_code)
  WHERE is_active = true;

-- ---------------------------------------------------------------------------
-- Lifetime international player stats (never reset)
-- ---------------------------------------------------------------------------

CREATE TABLE IF NOT EXISTS public.international_player_career (
  player_id text PRIMARY KEY,
  caps integer NOT NULL DEFAULT 0 CHECK (caps >= 0),
  goals integer NOT NULL DEFAULT 0 CHECK (goals >= 0),
  assists integer NOT NULL DEFAULT 0 CHECK (assists >= 0),
  potm integer NOT NULL DEFAULT 0 CHECK (potm >= 0),
  rating_sum numeric(12, 2) NOT NULL DEFAULT 0,
  rating_count integer NOT NULL DEFAULT 0 CHECK (rating_count >= 0),
  updated_at timestamptz NOT NULL DEFAULT now()
);


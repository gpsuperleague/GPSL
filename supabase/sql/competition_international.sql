CREATE OR REPLACE FUNCTION public.international_sync_owner_ranks()
RETURNS integer
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_count integer := 0;
BEGIN
  IF NOT public.is_gpsl_admin() AND current_user NOT IN ('postgres', 'supabase_admin', 'service_role') THEN

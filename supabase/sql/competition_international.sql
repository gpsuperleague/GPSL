CREATE OR REPLACE FUNCTION public.international_admin_assign_nation(
  p_club text,
  p_nation_code text
)
RETURNS void
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_cycle_id bigint;
BEGIN
  IF NOT public.is_gpsl_admin() THEN
    RAISE EXCEPTION 'Admin only';
  END IF;

  SELECT id INTO v_cycle_id
  FROM public.international_wc_cycles
  ORDER BY cycle_no DESC
  LIMIT 1;

  UPDATE public.international_owner_nations
  SET is_active = false,
      released_at = now()
  WHERE club_short_name = btrim(p_club)
    AND is_active = true;

  UPDATE public.international_owner_nations
  SET is_active = false,
      released_at = now()
  WHERE nation_code = btrim(upper(p_nation_code))
    AND is_active = true;

  INSERT INTO public.international_owner_nations (
    club_short_name,
    nation_code,
    cycle_id,
    selection_phase,
    is_active,
    locked_until_cycle_id
  )
  VALUES (
    btrim(p_club),
    btrim(upper(p_nation_code)),
    v_cycle_id,
    'admin',
    true,
    v_cycle_id
  );
END;
$function$;


-- Cup fixtures use division = 'cup', competition_type = 'cup', cup_code + cup_round + cup_match

ALTER TABLE public.competition_fixtures
  DROP CONSTRAINT IF EXISTS competition_fixtures_division_check;

ALTER TABLE public.competition_fixtures
  ADD CONSTRAINT competition_fixtures_division_check
  CHECK (division IN ('superleague', 'championship_a', 'championship_b', 'cup'));

ALTER TABLE public.competition_fixtures
  ADD COLUMN IF NOT EXISTS cup_code text,
  ADD COLUMN IF NOT EXISTS cup_round smallint,
  ADD COLUMN IF NOT EXISTS cup_match smallint;

ALTER TABLE public.competition_fixtures
  DROP CONSTRAINT IF EXISTS competition_fixtures_cup_fields_check;

ALTER TABLE public.competition_fixtures
  ADD CONSTRAINT competition_fixtures_cup_fields_check
  CHECK (
    (competition_type = 'league' AND cup_code IS NULL)
    OR (
      competition_type = 'cup'
      AND cup_code IN ('super8', 'plate', 'shield', 'spoon', 'league_cup')
      AND cup_round IS NOT NULL
      AND cup_match IS NOT NULL
    )
  );

CREATE UNIQUE INDEX IF NOT EXISTS competition_fixtures_cup_unique_idx
  ON public.competition_fixtures (season_id, cup_code, cup_round, cup_match)
  WHERE competition_type = 'cup';

-- ---------------------------------------------------------------------------
-- Bracket nodes (winner feeds child node home/away slot)
-- ---------------------------------------------------------------------------

CREATE TABLE IF NOT EXISTS public.competition_cup_bracket_nodes (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  season_id bigint NOT NULL REFERENCES public.competition_seasons (id) ON DELETE CASCADE,
  cup_code text NOT NULL CHECK (
    cup_code IN ('super8', 'plate', 'shield', 'spoon', 'league_cup')
  ),
  round_no smallint NOT NULL CHECK (round_no >= 1),
  match_no smallint NOT NULL CHECK (match_no >= 1),
  home_club_short_name text REFERENCES public."Clubs" ("ShortName"),
  away_club_short_name text REFERENCES public."Clubs" ("ShortName"),
  fixture_id bigint REFERENCES public.competition_fixtures (id) ON DELETE SET NULL,
  winner_club_short_name text REFERENCES public."Clubs" ("ShortName"),
  child_node_id bigint REFERENCES public.competition_cup_bracket_nodes (id) ON DELETE SET NULL,
  child_slot text CHECK (child_slot IS NULL OR child_slot IN ('home', 'away')),
  created_at timestamptz NOT NULL DEFAULT now(),
  CONSTRAINT competition_cup_bracket_nodes_unique UNIQUE (season_id, cup_code, round_no, match_no)
);

CREATE INDEX IF NOT EXISTS competition_cup_bracket_nodes_fixture_idx
  ON public.competition_cup_bracket_nodes (fixture_id);

-- Manual qualifiers (CH 16v17 shield/spoon playoffs before draw)
CREATE TABLE IF NOT EXISTS public.competition_cup_manual_qualifiers (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  season_id bigint NOT NULL REFERENCES public.competition_seasons (id) ON DELETE CASCADE,
  cup_code text NOT NULL CHECK (cup_code IN ('shield', 'spoon')),
  division text NOT NULL CHECK (division IN ('championship_a', 'championship_b')),
  club_short_name text NOT NULL REFERENCES public."Clubs" ("ShortName"),
  qualifier_role text NOT NULL CHECK (qualifier_role IN ('shield_playoff_winner', 'spoon_playoff_loser')),
  created_at timestamptz NOT NULL DEFAULT now(),
  CONSTRAINT competition_cup_manual_qualifiers_unique UNIQUE (
    season_id, cup_code, division, qualifier_role
  )
);

-- Prize money config (instant payout on confirm)
CREATE TABLE IF NOT EXISTS public.competition_cup_prize_config (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  season_id bigint NOT NULL REFERENCES public.competition_seasons (id) ON DELETE CASCADE,
  cup_code text NOT NULL CHECK (
    cup_code IN ('super8', 'plate', 'shield', 'spoon', 'league_cup')
  ),
  stage text NOT NULL CHECK (
    stage IN ('appearance', 'r1', 'r2', 'qf', 'sf', 'final', 'winner')
  ),
  amount numeric(14, 2) NOT NULL DEFAULT 0 CHECK (amount >= 0),
  CONSTRAINT competition_cup_prize_config_unique UNIQUE (season_id, cup_code, stage)
);

-- Track per-fixture prize paid (idempotent)
CREATE TABLE IF NOT EXISTS public.competition_cup_prize_paid (
  fixture_id bigint NOT NULL REFERENCES public.competition_fixtures (id) ON DELETE CASCADE,
  club_short_name text NOT NULL REFERENCES public."Clubs" ("ShortName"),
  stage text NOT NULL,
  amount numeric(14, 2) NOT NULL,
  PRIMARY KEY (fixture_id, club_short_name, stage)
);


CREATE OR REPLACE FUNCTION public.competition_shuffle_club_array(p_clubs text[])
RETURNS text[]
LANGUAGE plpgsql
IMMUTABLE
AS $function$
DECLARE
  v_result text[] := p_clubs;
  v_i int;
  v_j int;
  v_tmp text;
BEGIN
  IF v_result IS NULL THEN
    RETURN v_result;
  END IF;

  FOR v_i IN REVERSE array_length(v_result, 1)..2 LOOP
    v_j := 1 + floor(random() * v_i)::int;
    v_tmp := v_result[v_i];
    v_result[v_i] := v_result[v_j];
    v_result[v_j] := v_tmp;
  END LOOP;

  RETURN v_result;
END;
$function$;

CREATE OR REPLACE FUNCTION public.competition_draw_prestige_cup(
  p_season_id bigint,
  p_cup_code text
)
RETURNS jsonb
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_clubs text[];
BEGIN
  IF NOT public.is_gpsl_admin() THEN
    RAISE EXCEPTION 'Admin only';
  END IF;

  IF p_cup_code NOT IN ('super8', 'plate', 'shield', 'spoon') THEN
    RAISE EXCEPTION 'Invalid prestige cup code';
  END IF;

  v_clubs := public.competition_qualify_cup_clubs(p_season_id, p_cup_code);

  IF coalesce(array_length(v_clubs, 1), 0) < 2 THEN
    RAISE EXCEPTION 'Not enough qualified clubs for % (% found)', p_cup_code, coalesce(array_length(v_clubs, 1), 0);
  END IF;

  v_clubs := public.competition_shuffle_club_array(v_clubs);

  RETURN public.competition_build_knockout_bracket(p_season_id, p_cup_code, v_clubs);
END;
$function$;

CREATE OR REPLACE FUNCTION public.competition_draw_league_cup(
  p_season_id bigint,
  p_byes smallint DEFAULT 4
)
RETURNS jsonb
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_clubs text[];
  v_byes int := greatest(coalesce(p_byes, 4), 0);
  v_n int;
  v_target int := 1;
BEGIN
  IF NOT public.is_gpsl_admin() THEN
    RAISE EXCEPTION 'Admin only';
  END IF;

  v_clubs := public.competition_qualify_cup_clubs(p_season_id, 'league_cup');

  IF coalesce(array_length(v_clubs, 1), 0) < 8 THEN
    RAISE EXCEPTION 'Need at least 8 clubs in season for league cup';
  END IF;

  v_n := array_length(v_clubs, 1);
  WHILE v_target < v_n LOOP
    v_target := v_target * 2;
  END LOOP;

  IF v_byes <> v_target - v_n THEN
    RAISE EXCEPTION 'League cup needs exactly % byes for % clubs (got p_byes=%)', v_target - v_n, v_n, p_byes;
  END IF;

  v_clubs := public.competition_shuffle_club_array(v_clubs);

  RETURN public.competition_build_knockout_bracket(p_season_id, 'league_cup', v_clubs);
END;
$function$;


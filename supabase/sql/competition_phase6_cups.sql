CREATE OR REPLACE FUNCTION public.competition_active_season_id()
RETURNS bigint
LANGUAGE sql
STABLE
SECURITY DEFINER
SET search_path = public
AS $$
  SELECT id
  FROM public.competition_seasons
  WHERE is_current = true AND status = 'active'
  LIMIT 1;
$$;

CREATE OR REPLACE FUNCTION public.competition_cup_round_stage(p_cup_code text, p_round_no int, p_max_round int)
RETURNS text
LANGUAGE plpgsql
IMMUTABLE
AS $function$
BEGIN
  IF p_round_no = p_max_round THEN
    RETURN 'final';
  ELSIF p_round_no = p_max_round - 1 THEN
    RETURN 'sf';
  ELSIF p_round_no = p_max_round - 2 THEN
    RETURN 'qf';
  ELSIF p_round_no = 1 THEN
    RETURN 'r1';
  ELSE
    RETURN 'r2';
  END IF;
END;
$function$;

CREATE OR REPLACE FUNCTION public.competition_cup_fixture_label(p_fixture public.competition_fixtures)
RETURNS text
LANGUAGE sql
IMMUTABLE
AS $$
  SELECT format(
    '%s R%s M%s',
    upper(replace(coalesce(p_fixture.cup_code, 'cup'), '_', ' ')),
    p_fixture.cup_round,
    p_fixture.cup_match
  );
$$;

-- ---------------------------------------------------------------------------
-- Qualification lists from standings (+ manual playoff slots)
-- ---------------------------------------------------------------------------

CREATE OR REPLACE FUNCTION public.competition_qualify_cup_clubs(
  p_season_id bigint,
  p_cup_code text
)
RETURNS text[]
LANGUAGE plpgsql
STABLE
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_clubs text[] := ARRAY[]::text[];
BEGIN
  IF p_cup_code = 'super8' THEN
    SELECT array_agg(s.club_short_name ORDER BY s.table_position)
    INTO v_clubs
    FROM public.competition_standings_public s
    WHERE s.season_id = p_season_id
      AND s.division = 'superleague'
      AND s.table_position <= 8;
  ELSIF p_cup_code = 'plate' THEN
    SELECT array_agg(x.club ORDER BY x.sort_key, x.club)
    INTO v_clubs
    FROM (
      SELECT s.club_short_name AS club, s.table_position AS sort_key
      FROM public.competition_standings_public s
      WHERE s.season_id = p_season_id
        AND s.division = 'superleague'
        AND s.table_position BETWEEN 9 AND 16
      UNION ALL
      SELECT s.club_short_name, 100 + s.table_position
      FROM public.competition_standings_public s
      WHERE s.season_id = p_season_id
        AND s.division = 'championship_a'
        AND s.table_position <= 4
      UNION ALL
      SELECT s.club_short_name, 200 + s.table_position
      FROM public.competition_standings_public s
      WHERE s.season_id = p_season_id
        AND s.division = 'championship_b'
        AND s.table_position <= 4
    ) x;
  ELSIF p_cup_code = 'shield' THEN
    SELECT array_agg(x.club ORDER BY x.sort_key, x.club)
    INTO v_clubs
    FROM (
      SELECT s.club_short_name AS club, s.table_position AS sort_key
      FROM public.competition_standings_public s
      WHERE s.season_id = p_season_id
        AND s.division = 'superleague'
        AND s.table_position BETWEEN 17 AND 20
      UNION ALL
      SELECT s.club_short_name, 100 + s.table_position
      FROM public.competition_standings_public s
      WHERE s.season_id = p_season_id
        AND s.division = 'championship_a'
        AND s.table_position BETWEEN 5 AND 15
      UNION ALL
      SELECT s.club_short_name, 100 + s.table_position
      FROM public.competition_standings_public s
      WHERE s.season_id = p_season_id
        AND s.division = 'championship_b'
        AND s.table_position BETWEEN 5 AND 15
      UNION ALL
      SELECT q.club_short_name, 50
      FROM public.competition_cup_manual_qualifiers q
      WHERE q.season_id = p_season_id
        AND q.cup_code = 'shield'
        AND q.qualifier_role = 'shield_playoff_winner'
    ) x;
  ELSIF p_cup_code = 'spoon' THEN
    SELECT array_agg(x.club ORDER BY x.sort_key, x.club)
    INTO v_clubs
    FROM (
      SELECT s.club_short_name AS club, s.table_position AS sort_key
      FROM public.competition_standings_public s
      WHERE s.season_id = p_season_id
        AND s.division = 'championship_a'
        AND s.table_position >= 18
      UNION ALL
      SELECT s.club_short_name, 100 + s.table_position
      FROM public.competition_standings_public s
      WHERE s.season_id = p_season_id
        AND s.division = 'championship_b'
        AND s.table_position >= 18
      UNION ALL
      SELECT q.club_short_name, 50
      FROM public.competition_cup_manual_qualifiers q
      WHERE q.season_id = p_season_id
        AND q.cup_code = 'spoon'
        AND q.qualifier_role = 'spoon_playoff_loser'
    ) x;
  ELSIF p_cup_code = 'league_cup' THEN
    SELECT array_agg(ccs.club_short_name ORDER BY ccs.club_short_name)
    INTO v_clubs
    FROM public.competition_club_seasons ccs
    WHERE ccs.season_id = p_season_id
      AND ccs.division IN ('superleague', 'championship_a', 'championship_b');
  END IF;

  RETURN coalesce(v_clubs, ARRAY[]::text[]);
END;
$function$;

-- ---------------------------------------------------------------------------
-- Create cup fixture for a bracket node
-- ---------------------------------------------------------------------------

CREATE OR REPLACE FUNCTION public.competition_create_cup_fixture_for_node(p_node_id bigint)
RETURNS bigint
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_node public.competition_cup_bracket_nodes;
  v_fixture_id bigint;
BEGIN
  SELECT * INTO v_node FROM public.competition_cup_bracket_nodes WHERE id = p_node_id;
  IF NOT FOUND THEN
    RAISE EXCEPTION 'Bracket node not found';
  END IF;

  IF v_node.home_club_short_name IS NULL OR v_node.away_club_short_name IS NULL THEN
    RETURN NULL;
  END IF;

  IF v_node.fixture_id IS NOT NULL THEN
    RETURN v_node.fixture_id;
  END IF;

  INSERT INTO public.competition_fixtures (
    season_id,
    division,
    competition_type,
    matchday,
    gpsl_month,
    week_in_month,
    home_club_short_name,
    away_club_short_name,
    weather,
    cup_code,
    cup_round,
    cup_match
  )
  VALUES (
    v_node.season_id,
    'cup',
    'cup',
    v_node.round_no,
    'may',
    1,
    v_node.home_club_short_name,
    v_node.away_club_short_name,
    'clear',
    v_node.cup_code,
    v_node.round_no,
    v_node.match_no
  )
  RETURNING id INTO v_fixture_id;

  UPDATE public.competition_cup_bracket_nodes
  SET fixture_id = v_fixture_id
  WHERE id = p_node_id;

  RETURN v_fixture_id;
END;
$function$;

-- ---------------------------------------------------------------------------
-- Build knockout bracket (power-of-2 with byes for top seeds)
-- ---------------------------------------------------------------------------

CREATE OR REPLACE FUNCTION public.competition_build_knockout_bracket(
  p_season_id bigint,
  p_cup_code text,
  p_clubs text[]
)
RETURNS jsonb
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_n int;
  v_target int := 1;
  v_byes int;
  v_players text[];
  v_byes_clubs text[];
  v_round int;
  v_max_round int;
  v_matches_in_round int;
  v_match int;
  v_home text;
  v_away text;
  v_i int;
  v_idx int;
  v_node_id bigint;
  v_parent_id bigint;
  v_child_id bigint;
  v_prev_round_ids bigint[];
  v_curr_round_ids bigint[];
  v_r2_ids bigint[];
  v_node record;
  v_r1_count int := 0;
BEGIN
  IF NOT public.is_gpsl_admin() THEN
    RAISE EXCEPTION 'Admin only';
  END IF;

  IF p_clubs IS NULL OR array_length(p_clubs, 1) IS NULL OR array_length(p_clubs, 1) < 2 THEN
    RAISE EXCEPTION 'Need at least 2 clubs to draw %', p_cup_code;
  END IF;

  DELETE FROM public.competition_cup_bracket_nodes
  WHERE season_id = p_season_id AND cup_code = p_cup_code;

  DELETE FROM public.competition_fixtures
  WHERE season_id = p_season_id
    AND competition_type = 'cup'
    AND cup_code = p_cup_code;

  v_n := array_length(p_clubs, 1);
  WHILE v_target < v_n LOOP
    v_target := v_target * 2;
  END LOOP;

  v_byes := v_target - v_n;
  v_byes_clubs := p_clubs[1:v_byes];
  v_players := p_clubs[(v_byes + 1):v_n];

  v_max_round := 0;
  v_matches_in_round := v_target / 2;
  WHILE v_matches_in_round >= 1 LOOP
    v_max_round := v_max_round + 1;
    v_matches_in_round := v_matches_in_round / 2;
  END LOOP;

  v_prev_round_ids := NULL;

  FOR v_round IN 1..v_max_round LOOP
    v_matches_in_round := (v_target / 2) / power(2, v_round - 1)::int;
    v_curr_round_ids := ARRAY[]::bigint[];

    FOR v_match IN 1..v_matches_in_round LOOP
      v_home := NULL;
      v_away := NULL;

      IF v_round = 1 THEN
        v_idx := (v_match - 1) * 2 + 1;
        IF v_idx <= array_length(v_players, 1) THEN
          v_home := v_players[v_idx];
        END IF;
        IF v_idx + 1 <= array_length(v_players, 1) THEN
          v_away := v_players[v_idx + 1];
        END IF;
      END IF;

      INSERT INTO public.competition_cup_bracket_nodes (
        season_id, cup_code, round_no, match_no,
        home_club_short_name, away_club_short_name
      )
      VALUES (p_season_id, p_cup_code, v_round, v_match, v_home, v_away)
      RETURNING id INTO v_node_id;

      v_curr_round_ids := array_append(v_curr_round_ids, v_node_id);

      IF v_round = 1 AND v_home IS NOT NULL AND v_away IS NOT NULL THEN
        PERFORM public.competition_create_cup_fixture_for_node(v_node_id);
        v_r1_count := v_r1_count + 1;
      END IF;
    END LOOP;

    IF v_prev_round_ids IS NOT NULL THEN
      FOR v_i IN 1..array_length(v_prev_round_ids, 1) LOOP
        v_parent_id := v_prev_round_ids[v_i];
        v_child_id := v_curr_round_ids[(v_i + 1) / 2];
        UPDATE public.competition_cup_bracket_nodes
        SET child_node_id = v_child_id,
            child_slot = CASE WHEN v_i % 2 = 1 THEN 'home' ELSE 'away' END
        WHERE id = v_parent_id;
      END LOOP;
    END IF;

    v_prev_round_ids := v_curr_round_ids;
  END LOOP;

  -- Seed bye clubs into round 2 nodes (in match order)
  SELECT array_agg(id ORDER BY match_no)
  INTO v_r2_ids
  FROM public.competition_cup_bracket_nodes
  WHERE season_id = p_season_id
    AND cup_code = p_cup_code
    AND round_no = 2;

  v_i := 1;
  FOR v_match IN 1..coalesce(array_length(v_byes_clubs, 1), 0) LOOP
    EXIT WHEN v_r2_ids IS NULL OR v_i > array_length(v_r2_ids, 1);
    UPDATE public.competition_cup_bracket_nodes n
    SET home_club_short_name = coalesce(n.home_club_short_name, v_byes_clubs[v_match])
    WHERE n.id = v_r2_ids[v_i];
    v_i := v_i + 1;
  END LOOP;

  -- Auto-advance walkover R1 (no away)
  FOR v_node IN
    SELECT * FROM public.competition_cup_bracket_nodes
    WHERE season_id = p_season_id AND cup_code = p_cup_code AND round_no = 1
  LOOP
    IF v_node.away_club_short_name IS NULL
       AND v_node.home_club_short_name IS NOT NULL
       AND v_node.winner_club_short_name IS NULL THEN
      UPDATE public.competition_cup_bracket_nodes
      SET winner_club_short_name = v_node.home_club_short_name
      WHERE id = v_node.id;
      PERFORM public.competition_cup_advance_node_winner(v_node.id);
    END IF;
  END LOOP;

  -- Create fixtures for any round-2+ nodes that already have both clubs
  FOR v_node IN
    SELECT * FROM public.competition_cup_bracket_nodes
    WHERE season_id = p_season_id
      AND cup_code = p_cup_code
      AND home_club_short_name IS NOT NULL
      AND away_club_short_name IS NOT NULL
      AND fixture_id IS NULL
  LOOP
    PERFORM public.competition_create_cup_fixture_for_node(v_node.id);
  END LOOP;

  RETURN jsonb_build_object(
    'cup_code', p_cup_code,
    'clubs', v_n,
    'byes', v_byes,
    'rounds', v_max_round,
    'r1_fixtures', v_r1_count
  );
END;
$function$;

-- ---------------------------------------------------------------------------
-- Advance winner into child bracket slot
-- ---------------------------------------------------------------------------

CREATE OR REPLACE FUNCTION public.competition_cup_advance_node_winner(p_node_id bigint)
RETURNS void
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_node public.competition_cup_bracket_nodes;
  v_child public.competition_cup_bracket_nodes;
  v_winner text;
BEGIN
  SELECT * INTO v_node FROM public.competition_cup_bracket_nodes WHERE id = p_node_id;
  IF NOT FOUND THEN
    RETURN;
  END IF;

  v_winner := v_node.winner_club_short_name;
  IF v_winner IS NULL OR v_node.child_node_id IS NULL OR v_node.child_slot IS NULL THEN
    RETURN;
  END IF;

  SELECT * INTO v_child FROM public.competition_cup_bracket_nodes WHERE id = v_node.child_node_id;
  IF NOT FOUND THEN
    RETURN;
  END IF;

  IF v_node.child_slot = 'home' THEN
    UPDATE public.competition_cup_bracket_nodes
    SET home_club_short_name = v_winner
    WHERE id = v_child.id
      AND home_club_short_name IS DISTINCT FROM v_winner;
  ELSE
    UPDATE public.competition_cup_bracket_nodes
    SET away_club_short_name = v_winner
    WHERE id = v_child.id
      AND away_club_short_name IS DISTINCT FROM v_winner;
  END IF;

  SELECT * INTO v_child FROM public.competition_cup_bracket_nodes WHERE id = v_node.child_node_id;

  IF v_child.home_club_short_name IS NOT NULL AND v_child.away_club_short_name IS NOT NULL THEN
    PERFORM public.competition_create_cup_fixture_for_node(v_child.id);
  END IF;
END;
$function$;

-- ---------------------------------------------------------------------------
-- Cup prizes (instant on confirm)
-- ---------------------------------------------------------------------------

CREATE OR REPLACE FUNCTION public.competition_pay_cup_fixture_prizes(p_fixture_id bigint)
RETURNS void
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_fixture public.competition_fixtures;
  v_max_round int;
  v_stage text;
  v_winner text;
  v_amount numeric;
  v_club text;
BEGIN
  SELECT * INTO v_fixture
  FROM public.competition_fixtures
  WHERE id = p_fixture_id AND competition_type = 'cup';

  IF NOT FOUND THEN
    RETURN;
  END IF;

  SELECT max(round_no) INTO v_max_round
  FROM public.competition_cup_bracket_nodes
  WHERE season_id = v_fixture.season_id AND cup_code = v_fixture.cup_code;

  v_stage := public.competition_cup_round_stage(
    v_fixture.cup_code,
    v_fixture.cup_round,
    coalesce(v_max_round, v_fixture.cup_round)
  );

  IF v_fixture.home_goals > v_fixture.away_goals THEN
    v_winner := v_fixture.home_club_short_name;
  ELSIF v_fixture.away_goals > v_fixture.home_goals THEN
    v_winner := v_fixture.away_club_short_name;
  ELSE
    RETURN;
  END IF;

  FOREACH v_club IN ARRAY ARRAY[v_fixture.home_club_short_name, v_fixture.away_club_short_name]
  LOOP
    SELECT amount INTO v_amount
    FROM public.competition_cup_prize_config
    WHERE season_id = v_fixture.season_id
      AND cup_code = v_fixture.cup_code
      AND stage = 'appearance';

    IF v_amount IS NOT NULL AND v_amount > 0
       AND NOT EXISTS (
         SELECT 1 FROM public.competition_cup_prize_paid
         WHERE fixture_id = p_fixture_id
           AND club_short_name = v_club
           AND stage = 'appearance'
       ) THEN
      PERFORM public.competition_credit_club_balance(v_club, v_amount);
      INSERT INTO public.competition_finance_ledger (
        season_id, fixture_id, club_short_name, entry_type, amount, description, metadata
      )
      VALUES (
        v_fixture.season_id,
        p_fixture_id,
        v_club,
        'prize',
        v_amount,
        format('%s appearance — %s', upper(v_fixture.cup_code), public.competition_cup_fixture_label(v_fixture)),
        jsonb_build_object('cup_code', v_fixture.cup_code, 'stage', 'appearance')
      );
      INSERT INTO public.competition_cup_prize_paid (fixture_id, club_short_name, stage, amount)
      VALUES (p_fixture_id, v_club, 'appearance', v_amount);
    END IF;
  END LOOP;

  SELECT amount INTO v_amount
  FROM public.competition_cup_prize_config
  WHERE season_id = v_fixture.season_id
    AND cup_code = v_fixture.cup_code
    AND stage = v_stage;

  IF v_amount IS NOT NULL AND v_amount > 0
     AND NOT EXISTS (
       SELECT 1 FROM public.competition_cup_prize_paid
       WHERE fixture_id = p_fixture_id
         AND club_short_name = v_winner
         AND stage = v_stage
     ) THEN
    PERFORM public.competition_credit_club_balance(v_winner, v_amount);
    INSERT INTO public.competition_finance_ledger (
      season_id, fixture_id, club_short_name, entry_type, amount, description, metadata
    )
    VALUES (
      v_fixture.season_id,
      p_fixture_id,
      v_winner,
      'prize',
      v_amount,
      format('%s %s winner — %s', upper(v_fixture.cup_code), v_stage, public.competition_cup_fixture_label(v_fixture)),
      jsonb_build_object('cup_code', v_fixture.cup_code, 'stage', v_stage)
    );
    INSERT INTO public.competition_cup_prize_paid (fixture_id, club_short_name, stage, amount)
    VALUES (p_fixture_id, v_winner, v_stage, v_amount);
  END IF;

  IF v_fixture.cup_round = v_max_round THEN
    SELECT amount INTO v_amount
    FROM public.competition_cup_prize_config
    WHERE season_id = v_fixture.season_id
      AND cup_code = v_fixture.cup_code
      AND stage = 'winner';

    IF v_amount IS NOT NULL AND v_amount > 0
       AND NOT EXISTS (
         SELECT 1 FROM public.competition_cup_prize_paid
         WHERE fixture_id = p_fixture_id
           AND club_short_name = v_winner
           AND stage = 'winner'
       ) THEN
      PERFORM public.competition_credit_club_balance(v_winner, v_amount);
      INSERT INTO public.competition_finance_ledger (
        season_id, fixture_id, club_short_name, entry_type, amount, description, metadata
      )
      VALUES (
        v_fixture.season_id,
        p_fixture_id,
        v_winner,
        'prize',
        v_amount,
        format('%s champion — %s', upper(v_fixture.cup_code), public.competition_cup_fixture_label(v_fixture)),
        jsonb_build_object('cup_code', v_fixture.cup_code, 'stage', 'winner')
      );
      INSERT INTO public.competition_cup_prize_paid (fixture_id, club_short_name, stage, amount)
      VALUES (p_fixture_id, v_winner, 'winner', v_amount);
    END IF;
  END IF;
END;
$function$;

CREATE OR REPLACE FUNCTION public.competition_cup_on_fixture_played(p_fixture_id bigint)
RETURNS void
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_fixture public.competition_fixtures;
  v_node public.competition_cup_bracket_nodes;
  v_winner text;
BEGIN
  SELECT * INTO v_fixture FROM public.competition_fixtures WHERE id = p_fixture_id;
  IF NOT FOUND OR v_fixture.competition_type <> 'cup' THEN
    RETURN;
  END IF;

  IF v_fixture.home_goals = v_fixture.away_goals THEN
    RETURN;
  END IF;

  IF v_fixture.home_goals > v_fixture.away_goals THEN
    v_winner := v_fixture.home_club_short_name;
  ELSE
    v_winner := v_fixture.away_club_short_name;
  END IF;

  SELECT * INTO v_node
  FROM public.competition_cup_bracket_nodes
  WHERE fixture_id = p_fixture_id;

  IF FOUND THEN
    UPDATE public.competition_cup_bracket_nodes
    SET winner_club_short_name = v_winner
    WHERE id = v_node.id;
    PERFORM public.competition_cup_advance_node_winner(v_node.id);
  END IF;

  PERFORM public.competition_pay_cup_fixture_prizes(p_fixture_id);
END;
$function$;


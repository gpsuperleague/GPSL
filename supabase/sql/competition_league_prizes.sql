-- =============================================================================
-- GPSL Competition — League prize money (config, EOS payout, standings display)
-- Run once in Supabase SQL Editor after competition_phase5_finances.sql
-- (and after stadium_expansion.sql / central_bank_phase1.sql if those extended ledger types).
-- Also re-patches result confirmation to auto-pay when a division's 38 league
-- matches are complete.
-- =============================================================================

-- Constants: 20 clubs per division, 38 league matches each (double round-robin).

-- ---------------------------------------------------------------------------
-- Ledger: prize_league entry type
-- ---------------------------------------------------------------------------

ALTER TABLE public.competition_finance_ledger
  DROP CONSTRAINT IF EXISTS competition_finance_ledger_entry_type_check;

ALTER TABLE public.competition_finance_ledger
  ADD CONSTRAINT competition_finance_ledger_entry_type_check
  CHECK (
    entry_type IN (
      'gate_league_home',
      'gate_cup_share',
      'prize',
      'prize_league',
      'adjustment',
      'admin_one_off_injection',
      'admin_purchase_payment',
      'transfer_sale',
      'transfer_purchase',
      'transfer_agent_fee',
      'transfer_foreign_sale',
      'transfer_overflow_release',
      'loan_drawdown',
      'loan_repayment_principal',
      'loan_interest_payment',
      'infra_maintenance',
      'infra_purchase',
      'infra_expansion',
      'infra_expansion_refund',
      'infra_expansion_penalty'
    )
  );

-- ---------------------------------------------------------------------------
-- Config + paid tracking
-- ---------------------------------------------------------------------------

CREATE TABLE IF NOT EXISTS public.competition_league_prize_config (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  season_id bigint NOT NULL REFERENCES public.competition_seasons (id) ON DELETE CASCADE,
  division text NOT NULL CHECK (
    division IN ('superleague', 'championship_a', 'championship_b')
  ),
  position smallint NOT NULL CHECK (position >= 1 AND position <= 20),
  amount numeric(14, 2) NOT NULL DEFAULT 0 CHECK (amount >= 0),
  updated_at timestamptz NOT NULL DEFAULT now(),
  CONSTRAINT competition_league_prize_config_unique UNIQUE (season_id, division, position)
);

CREATE TABLE IF NOT EXISTS public.competition_league_prize_paid (
  season_id bigint NOT NULL REFERENCES public.competition_seasons (id) ON DELETE CASCADE,
  division text NOT NULL CHECK (
    division IN ('superleague', 'championship_a', 'championship_b')
  ),
  club_short_name text NOT NULL REFERENCES public."Clubs" ("ShortName"),
  table_position smallint NOT NULL CHECK (table_position >= 1 AND table_position <= 20),
  amount numeric(14, 2) NOT NULL CHECK (amount > 0),
  paid_at timestamptz NOT NULL DEFAULT now(),
  PRIMARY KEY (season_id, division, club_short_name)
);

CREATE INDEX IF NOT EXISTS competition_league_prize_config_season_idx
  ON public.competition_league_prize_config (season_id, division);

-- ---------------------------------------------------------------------------
-- Public views
-- ---------------------------------------------------------------------------

CREATE OR REPLACE VIEW public.competition_league_prize_config_public
WITH (security_invoker = false)
AS
SELECT season_id, division, position, amount
FROM public.competition_league_prize_config;

CREATE OR REPLACE VIEW public.competition_standings_prizes_public
WITH (security_invoker = false)
AS
SELECT
  s.season_id,
  s.division,
  s.club_short_name,
  s.club_name,
  s.table_position,
  s.mp,
  s.w,
  s.d,
  s.l,
  s.gf,
  s.ga,
  s.gd,
  s.pts,
  s.form_last10,
  coalesce(pc.amount, 0)::numeric(14, 2) AS league_prize_amount,
  (pp.club_short_name IS NOT NULL) AS league_prize_paid
FROM public.competition_standings_public s
LEFT JOIN public.competition_league_prize_config pc
  ON pc.season_id = s.season_id
 AND pc.division = s.division
 AND pc.position = s.table_position
LEFT JOIN public.competition_league_prize_paid pp
  ON pp.season_id = s.season_id
 AND pp.division = s.division
 AND pp.club_short_name = s.club_short_name;

GRANT SELECT ON public.competition_league_prize_config_public TO authenticated;

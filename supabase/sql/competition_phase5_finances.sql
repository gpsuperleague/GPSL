CREATE OR REPLACE FUNCTION public.competition_club_table_position(
  p_season_id bigint,
  p_division text,
  p_club_short_name text
)
RETURNS int
LANGUAGE sql
STABLE
SECURITY DEFINER
SET search_path = public
AS $$
  SELECT coalesce(s.table_position, 10)::int
  FROM public.competition_standings_public s
  WHERE s.season_id = p_season_id
    AND s.division = p_division
    AND s.club_short_name = p_club_short_name
  LIMIT 1;
$$;

CREATE OR REPLACE FUNCTION public.competition_club_history_avg_position(
  p_club_short_name text,
  p_limit int DEFAULT 5
)
RETURNS numeric
LANGUAGE sql
STABLE
SECURITY DEFINER
SET search_path = public
AS $$
  SELECT avg(a.final_position)::numeric
  FROM (
    SELECT final_position
    FROM public.competition_club_season_archive
    WHERE club_short_name = p_club_short_name
    ORDER BY created_at DESC
    LIMIT greatest(p_limit, 1)
  ) a;
$$;

CREATE OR REPLACE FUNCTION public.competition_compute_gate_total(
  p_capacity int,
  p_table_position int,
  p_history_avg_position numeric
)
RETURNS jsonb
LANGUAGE plpgsql
IMMUTABLE
AS $function$
DECLARE
  v_capacity int := greatest(coalesce(p_capacity, 0), 0);
  v_pos int := least(greatest(coalesce(p_table_position, 10), 1), 20);
  v_hist numeric := coalesce(p_history_avg_position, 10);
  v_base_fill numeric := 0.55;
  v_position_boost numeric;
  v_history_boost numeric;
  v_fill numeric;
  v_price_per_seat numeric := 20;
  v_total numeric;
BEGIN
  v_position_boost := 0.35 * ((21 - v_pos)::numeric / 20.0);
  v_history_boost := 0.05 * ((21 - least(greatest(v_hist, 1), 20)) / 20.0);
  v_fill := least(v_base_fill + v_position_boost + v_history_boost, 0.95);
  v_total := round(v_capacity * v_fill * v_price_per_seat);

  RETURN jsonb_build_object(
    'capacity', v_capacity,
    'table_position', v_pos,
    'history_avg_position', round(v_hist, 2),
    'attendance_rate', round(v_fill, 4),
    'price_per_seat', v_price_per_seat,
    'total_gate', v_total
  );
END;
$function$;

CREATE OR REPLACE FUNCTION public.competition_estimate_gate_for_club(
  p_club_short_name text,
  p_season_id bigint DEFAULT NULL,
  p_division text DEFAULT NULL
)
RETURNS jsonb
LANGUAGE plpgsql
STABLE
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_season_id bigint;
  v_division text;
  v_capacity int;
  v_pos int;
  v_hist numeric;
  v_breakdown jsonb;
BEGIN
  IF p_season_id IS NULL THEN
    SELECT id INTO v_season_id
    FROM public.competition_seasons
    WHERE is_current = true AND status = 'active'
    LIMIT 1;
  ELSE
    v_season_id := p_season_id;
  END IF;

  IF v_season_id IS NULL THEN
    RETURN jsonb_build_object('error', 'No active season');
  END IF;

  IF p_division IS NULL THEN
    SELECT ccs.division INTO v_division
    FROM public.competition_club_seasons ccs
    WHERE ccs.season_id = v_season_id
      AND ccs.club_short_name = p_club_short_name
    LIMIT 1;
  ELSE
    v_division := p_division;
  END IF;

  SELECT coalesce(c."Capacity", 0)::int INTO v_capacity
  FROM public."Clubs" c
  WHERE c."ShortName" = p_club_short_name;

  v_pos := public.competition_club_table_position(
    v_season_id,
    coalesce(v_division, 'superleague'),
    p_club_short_name
  );
  v_hist := public.competition_club_history_avg_position(p_club_short_name, 5);
  v_breakdown := public.competition_compute_gate_total(v_capacity, v_pos, v_hist);

  RETURN v_breakdown || jsonb_build_object(
    'club_short_name', p_club_short_name,
    'season_id', v_season_id,
    'division', v_division
  );
END;
$function$;

CREATE OR REPLACE FUNCTION public.competition_credit_club_balance(
  p_club_short_name text,
  p_amount numeric
)
RETURNS void
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_balance numeric;
BEGIN
  IF p_amount IS NULL OR p_amount = 0 THEN
    RETURN;
  END IF;

  SELECT balance INTO v_balance
  FROM public."Club_Finances"
  WHERE club_name = p_club_short_name
  FOR UPDATE;

  IF NOT FOUND THEN
    RAISE EXCEPTION 'Club_Finances row missing for %', p_club_short_name;
  END IF;

  UPDATE public."Club_Finances"
  SET balance = v_balance + p_amount
  WHERE club_name = p_club_short_name;
END;
$function$;

CREATE OR REPLACE FUNCTION public.competition_settle_fixture_gates(p_fixture_id bigint)
RETURNS void
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_fixture public.competition_fixtures;
  v_capacity int;
  v_pos int;
  v_hist numeric;
  v_breakdown jsonb;
  v_total numeric;
  v_home_share numeric;
  v_away_share numeric;
  v_desc text;
  v_home_club text;
  v_away_club text;
BEGIN
  IF p_fixture_id IS NULL THEN
    RETURN;
  END IF;

  IF EXISTS (
    SELECT 1 FROM public.competition_finance_ledger
    WHERE fixture_id = p_fixture_id
      AND entry_type IN ('gate_league_home', 'gate_cup_share')
  ) THEN
    RETURN;
  END IF;

  SELECT * INTO v_fixture
  FROM public.competition_fixtures
  WHERE id = p_fixture_id
    AND status = 'played'
    AND home_goals IS NOT NULL
    AND away_goals IS NOT NULL;

  IF NOT FOUND THEN
    RETURN;
  END IF;

  v_home_club := v_fixture.home_club_short_name;
  v_away_club := v_fixture.away_club_short_name;

  SELECT coalesce(c."Capacity", 0)::int INTO v_capacity
  FROM public."Clubs" c
  WHERE c."ShortName" = v_home_club;

  v_pos := public.competition_club_table_position(
    v_fixture.season_id,
    v_fixture.division,
    v_home_club
  );
  v_hist := public.competition_club_history_avg_position(v_home_club, 5);
  v_breakdown := public.competition_compute_gate_total(v_capacity, v_pos, v_hist);
  v_total := (v_breakdown ->> 'total_gate')::numeric;

  IF v_total IS NULL OR v_total <= 0 THEN
    RETURN;
  END IF;

  IF v_fixture.competition_type = 'league' THEN
    v_desc := format(
      'Gate MD%s — %s vs %s (home 100%%) · fill %s%%',
      v_fixture.matchday,
      v_home_club,
      v_away_club,
      round(((v_breakdown ->> 'attendance_rate')::numeric) * 100, 1)
    );

    PERFORM public.competition_credit_club_balance(v_home_club, v_total);

    INSERT INTO public.competition_finance_ledger (
      season_id, fixture_id, club_short_name, entry_type, amount, description, metadata
    )
    VALUES (
      v_fixture.season_id,
      p_fixture_id,
      v_home_club,
      'gate_league_home',
      v_total,
      v_desc,
      v_breakdown
    );
  ELSIF v_fixture.competition_type = 'cup' THEN
    v_home_share := round(v_total / 2.0, 2);
    v_away_share := v_total - v_home_share;

    PERFORM public.competition_credit_club_balance(v_home_club, v_home_share);
    PERFORM public.competition_credit_club_balance(v_away_club, v_away_share);

    INSERT INTO public.competition_finance_ledger (
      season_id, fixture_id, club_short_name, entry_type, amount, description, metadata
    )
    VALUES
      (
        v_fixture.season_id,
        p_fixture_id,
        v_home_club,
        'gate_cup_share',
        v_home_share,
        format('Cup gate MD%s — %s vs %s (50%% home share)', v_fixture.matchday, v_home_club, v_away_club),
        v_breakdown || jsonb_build_object('split', 'home')
      ),
      (
        v_fixture.season_id,
        p_fixture_id,
        v_away_club,
        'gate_cup_share',
        v_away_share,
        format('Cup gate MD%s — %s vs %s (50%% away share)', v_fixture.matchday, v_home_club, v_away_club),
        v_breakdown || jsonb_build_object('split', 'away')
      );
  END IF;
END;
$function$;

-- Admin: archive a finished season row (for 5-year gate history)
CREATE OR REPLACE FUNCTION public.competition_admin_archive_club_season(
  p_club_short_name text,
  p_season_label text,
  p_division text,
  p_final_position smallint
)
RETURNS void
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $function$
BEGIN
  IF NOT public.is_gpsl_admin() THEN
    RAISE EXCEPTION 'Admin only';
  END IF;

  INSERT INTO public.competition_club_season_archive (
    club_short_name, season_label, division, final_position
  )
  VALUES (
    upper(trim(p_club_short_name)),
    trim(p_season_label),
    p_division,
    p_final_position
  )
  ON CONFLICT (club_short_name, season_label)
  DO UPDATE SET
    division = excluded.division,
    final_position = excluded.final_position;
END;
$function$;

-- Backfill gates for played fixtures missing ledger rows (current season)
CREATE OR REPLACE FUNCTION public.competition_admin_backfill_gates(p_season_id bigint DEFAULT NULL)
RETURNS int
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_season_id bigint;
  v_fixture record;
  v_count int := 0;
BEGIN
  IF NOT public.is_gpsl_admin() THEN
    RAISE EXCEPTION 'Admin only';
  END IF;

  IF p_season_id IS NULL THEN
    SELECT id INTO v_season_id
    FROM public.competition_seasons
    WHERE is_current = true
    LIMIT 1;
  ELSE
    v_season_id := p_season_id;
  END IF;

  FOR v_fixture IN
    SELECT id
    FROM public.competition_fixtures
    WHERE season_id = v_season_id
      AND status = 'played'
      AND competition_type IN ('league', 'cup')
    ORDER BY matchday, id
  LOOP
    PERFORM public.competition_settle_fixture_gates(v_fixture.id);
    v_count := v_count + 1;
  END LOOP;

  RETURN v_count;
END;
$function$;


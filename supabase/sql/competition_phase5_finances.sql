CREATE OR REPLACE VIEW public.competition_finance_ledger_public
WITH (security_invoker = false)
AS
SELECT
  l.id,
  l.season_id,
  l.fixture_id,
  l.club_short_name,
  c."Club" AS club_name,
  l.entry_type,
  l.amount,
  l.description,
  l.metadata,
  l.created_at,
  f.matchday,
  f.competition_type,
  f.home_club_short_name,
  f.away_club_short_name
FROM public.competition_finance_ledger l
JOIN public."Clubs" c ON c."ShortName" = l.club_short_name
LEFT JOIN public.competition_fixtures f ON f.id = l.fixture_id
JOIN public.competition_seasons s ON s.id = l.season_id
WHERE s.is_current = true
  AND s.status = 'active';

GRANT SELECT ON public.competition_finance_ledger_public TO authenticated;
GRANT SELECT ON public.competition_finance_ledger_public TO anon;

CREATE OR REPLACE VIEW public.competition_club_season_archive_public
WITH (security_invoker = false)
AS
SELECT
  a.club_short_name,
  c."Club" AS club_name,
  a.season_label,
  a.division,
  a.final_position,
  a.created_at
FROM public.competition_club_season_archive a
JOIN public."Clubs" c ON c."ShortName" = a.club_short_name;

GRANT SELECT ON public.competition_club_season_archive_public TO authenticated;
GRANT SELECT ON public.competition_club_season_archive_public TO anon;


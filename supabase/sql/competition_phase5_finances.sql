-- =============================================================================
-- GPSL Competition — Phase 5: gate receipts + finance ledger
-- Requires: competition_phase0–4
-- Spec: docs/competition-spec.md — home league 100%, cups 50/50
--
-- Gate formula (v1, tunable constants below):
--   attendance_rate = base_fill + position_boost + history_boost (capped)
--   total_gate = capacity × attendance_rate × price_per_seat
--   position: current league table_position in fixture division (default mid-table 10)
--   history: avg final_position from competition_club_season_archive (last 5 seasons; neutral if none)
-- =============================================================================

-- Allow cup fixtures later (league only generated in phase 1 today)
ALTER TABLE public.competition_fixtures
  DROP CONSTRAINT IF EXISTS competition_fixtures_competition_type_check;

ALTER TABLE public.competition_fixtures
  ADD CONSTRAINT competition_fixtures_competition_type_check
  CHECK (competition_type IN ('league', 'cup'));

-- ---------------------------------------------------------------------------
-- Season archive (5-year history for gate multiplier; fill via admin / season end)
-- ---------------------------------------------------------------------------

CREATE TABLE IF NOT EXISTS public.competition_club_season_archive (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  club_short_name text NOT NULL REFERENCES public."Clubs" ("ShortName"),
  season_label text NOT NULL,
  division text NOT NULL CHECK (
    division IN ('superleague', 'championship_a', 'championship_b')
  ),
  final_position smallint NOT NULL CHECK (final_position >= 1 AND final_position <= 20),
  created_at timestamptz NOT NULL DEFAULT now(),
  CONSTRAINT competition_club_season_archive_unique UNIQUE (club_short_name, season_label)
);

CREATE INDEX IF NOT EXISTS competition_club_season_archive_club_idx
  ON public.competition_club_season_archive (club_short_name, created_at DESC);

-- ---------------------------------------------------------------------------
-- Ledger (gates now; prizes in phase 6+)
-- ---------------------------------------------------------------------------

CREATE TABLE IF NOT EXISTS public.competition_finance_ledger (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  season_id bigint NOT NULL REFERENCES public.competition_seasons (id) ON DELETE CASCADE,
  fixture_id bigint REFERENCES public.competition_fixtures (id) ON DELETE SET NULL,
  club_short_name text NOT NULL REFERENCES public."Clubs" ("ShortName"),
  entry_type text NOT NULL CHECK (
    entry_type IN ('gate_league_home', 'gate_cup_share', 'prize', 'adjustment')
  ),
  amount numeric(14, 2) NOT NULL CHECK (amount <> 0),
  description text NOT NULL,
  metadata jsonb NOT NULL DEFAULT '{}'::jsonb,
  created_at timestamptz NOT NULL DEFAULT now(),
  CONSTRAINT competition_finance_ledger_fixture_unique UNIQUE (fixture_id, club_short_name, entry_type)
);

CREATE INDEX IF NOT EXISTS competition_finance_ledger_club_idx
  ON public.competition_finance_ledger (club_short_name, created_at DESC);


-- =============================================================================
-- GPSL Owner ranking — season points, rolling 4 seasons (World Cup draft), all-time
-- Run after competition_history.sql and competition_international.sql
-- =============================================================================

CREATE TABLE IF NOT EXISTS public.competition_owner_season_ranking (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  season_id bigint NOT NULL REFERENCES public.competition_seasons (id) ON DELETE CASCADE,
  season_label text NOT NULL,
  club_short_name text NOT NULL REFERENCES public."Clubs" ("ShortName"),
  owner_id uuid,
  owner_tag text NOT NULL DEFAULT '',
  league_points numeric(8, 2) NOT NULL DEFAULT 0,
  super8_points numeric(8, 2) NOT NULL DEFAULT 0,
  plate_points numeric(8, 2) NOT NULL DEFAULT 0,
  shield_points numeric(8, 2) NOT NULL DEFAULT 0,
  spoon_points numeric(8, 2) NOT NULL DEFAULT 0,
  league_cup_points numeric(8, 2) NOT NULL DEFAULT 0,
  season_total numeric(10, 2) NOT NULL DEFAULT 0,
  detail jsonb NOT NULL DEFAULT '{}'::jsonb,
  computed_at timestamptz NOT NULL DEFAULT now(),
  CONSTRAINT competition_owner_season_ranking_unique UNIQUE (season_id, club_short_name)
);

CREATE INDEX IF NOT EXISTS competition_owner_season_ranking_owner_idx
  ON public.competition_owner_season_ranking (owner_id, season_id DESC);

CREATE INDEX IF NOT EXISTS competition_owner_season_ranking_season_idx
  ON public.competition_owner_season_ranking (season_id);

-- ---------------------------------------------------------------------------
-- League position points (SuperLeague only)
-- ---------------------------------------------------------------------------

CREATE OR REPLACE FUNCTION public.competition_owner_league_points(
  p_division text,
  p_position smallint
)
RETURNS numeric
LANGUAGE sql
IMMUTABLE
AS $$
  SELECT CASE
    WHEN p_division IS DISTINCT FROM 'superleague' OR p_position IS NULL THEN 0::numeric
    WHEN p_position = 1 THEN 10
    WHEN p_position = 2 THEN 8
    WHEN p_position BETWEEN 3 AND 8 THEN 5
    WHEN p_position BETWEEN 9 AND 15 THEN 3
    WHEN p_position BETWEEN 16 AND 17 THEN 2
    WHEN p_position BETWEEN 18 AND 20 THEN 0.5
    ELSE 0
  END;
$$;

-- ---------------------------------------------------------------------------
-- Cup achievement → points (best tier reached per cup per season)
-- ---------------------------------------------------------------------------

CREATE OR REPLACE FUNCTION public.competition_owner_cup_points(
  p_cup_code text,
  p_achievement text
)
RETURNS numeric
LANGUAGE sql
IMMUTABLE
AS $$
  SELECT CASE p_cup_code
    WHEN 'super8' THEN CASE p_achievement
      WHEN 'winner' THEN 6
      WHEN 'runner_up' THEN 4
      WHEN 'sf' THEN 3
      WHEN 'qf' THEN 2
      ELSE 0
    END
    WHEN 'plate' THEN CASE p_achievement
      WHEN 'winner' THEN 4
      WHEN 'runner_up' THEN 3
      WHEN 'sf' THEN 2
      WHEN 'qf' THEN 1
      WHEN 'r16' THEN 0.5
      ELSE 0
    END
    WHEN 'shield' THEN CASE p_achievement
      WHEN 'winner' THEN 3
      WHEN 'runner_up' THEN 2
      WHEN 'sf' THEN 1
      WHEN 'qf' THEN 0.5
      WHEN 'r16' THEN 0.25
      WHEN 'r32' THEN 0
      ELSE 0
    END
    WHEN 'spoon' THEN CASE p_achievement
      WHEN 'winner' THEN 1
      WHEN 'runner_up' THEN 0.5
      WHEN 'sf' THEN 0.25
      WHEN 'qf' THEN 0
      ELSE 0
    END
    WHEN 'league_cup' THEN CASE p_achievement
      WHEN 'winner' THEN 4.5
      WHEN 'runner_up' THEN 3
      WHEN 'sf' THEN 2
      WHEN 'qf' THEN 1
      WHEN 'r16' THEN 0.5
      WHEN 'r32' THEN 0.25
      WHEN 'r64' THEN 0
      ELSE 0
    END
    ELSE 0
  END;
$$;

CREATE OR REPLACE FUNCTION public.competition_club_cup_achievement(
  p_season_id bigint,
  p_club text,
  p_cup_code text
)
RETURNS text
LANGUAGE plpgsql
STABLE
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_club text := btrim(p_club);
  v_max_round int;
  v_winner text;
  v_runner text;
BEGIN
  IF v_club IS NULL OR v_club = '' THEN
    RETURN NULL;
  END IF;

  SELECT w.winner_club_short_name, w.runner_up_club_short_name
  INTO v_winner, v_runner
  FROM public.competition_cup_season_winner w
  WHERE w.season_id = p_season_id
    AND w.cup_code = p_cup_code;

  IF v_winner = v_club THEN
    RETURN 'winner';
  END IF;

  IF v_runner = v_club THEN
    RETURN 'runner_up';
  END IF;

  SELECT max(n.round_no)
  INTO v_max_round
  FROM public.competition_cup_bracket_nodes n
  WHERE n.season_id = p_season_id
    AND n.cup_code = p_cup_code
    AND (
      n.home_club_short_name = v_club
      OR n.away_club_short_name = v_club
    )
    AND (
      n.winner_club_short_name IS NOT NULL
      OR EXISTS (
        SELECT 1 FROM public.competition_fixtures f
        WHERE f.id = n.fixture_id AND f.status = 'played'
      )
    );

  IF v_max_round IS NULL THEN
    RETURN NULL;
  END IF;

  IF EXISTS (
    SELECT 1
    FROM public.competition_cup_bracket_nodes n
    WHERE n.season_id = p_season_id
      AND n.cup_code = p_cup_code
      AND n.round_no = v_max_round
      AND (n.home_club_short_name = v_club OR n.away_club_short_name = v_club)
  ) THEN
    IF p_cup_code = 'league_cup' THEN
      RETURN CASE v_max_round
        WHEN 6 THEN 'runner_up'
        WHEN 5 THEN 'sf'
        WHEN 4 THEN 'qf'
        WHEN 3 THEN 'r16'
        WHEN 2 THEN 'r32'
        WHEN 1 THEN 'r64'
        ELSE NULL
      END;
    ELSIF p_cup_code = 'shield' THEN
      RETURN CASE v_max_round
        WHEN 5 THEN 'runner_up'
        WHEN 4 THEN 'sf'
        WHEN 3 THEN 'qf'
        WHEN 2 THEN 'r16'
        WHEN 1 THEN 'r32'
        ELSE NULL
      END;
    ELSIF p_cup_code = 'plate' THEN
      RETURN CASE v_max_round
        WHEN 4 THEN 'runner_up'
        WHEN 3 THEN 'sf'
        WHEN 2 THEN 'qf'
        WHEN 1 THEN 'r16'
        ELSE NULL
      END;
    ELSIF p_cup_code IN ('super8', 'spoon') THEN
      RETURN CASE v_max_round
        WHEN 3 THEN 'runner_up'
        WHEN 2 THEN 'sf'
        WHEN 1 THEN 'qf'
        ELSE NULL
      END;
    END IF;
  END IF;

  RETURN NULL;
END;
$function$;

-- ---------------------------------------------------------------------------
-- Recompute one season (call on archive or admin refresh)
-- ---------------------------------------------------------------------------

CREATE OR REPLACE FUNCTION public.competition_owner_ranking_recompute_season(
  p_season_id bigint
)
RETURNS integer
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_season record;
  v_club record;
  v_league_pts numeric;
  v_super8_pts numeric;
  v_plate_pts numeric;
  v_shield_pts numeric;
  v_spoon_pts numeric;
  v_lc_pts numeric;
  v_total numeric;
  v_ach text;
  v_detail jsonb;
  v_count int := 0;
  v_owner_id uuid;
  v_owner_tag text;
BEGIN
  IF auth.uid() IS NOT NULL AND NOT public.is_gpsl_admin() THEN
    RAISE EXCEPTION 'Admin only';
  END IF;

  SELECT id, label INTO v_season
  FROM public.competition_seasons
  WHERE id = p_season_id;

  IF NOT FOUND THEN
    RAISE EXCEPTION 'Season not found';
  END IF;

  FOR v_club IN
    SELECT c."ShortName" AS club_short_name, c.owner_id
    FROM public."Clubs" c
    WHERE c.owner_id IS NOT NULL
  LOOP
    v_owner_id := v_club.owner_id;
    v_owner_tag := coalesce(public.owner_registry_resolve_tag(v_owner_id), '');

    SELECT coalesce(public.competition_owner_league_points(a.division, a.final_position), 0)
    INTO v_league_pts
    FROM public.competition_club_season_archive a
    WHERE a.season_id = p_season_id
      AND a.club_short_name = v_club.club_short_name;

    v_league_pts := coalesce(v_league_pts, 0);

    v_ach := public.competition_club_cup_achievement(p_season_id, v_club.club_short_name, 'super8');
    v_super8_pts := coalesce(public.competition_owner_cup_points('super8', v_ach), 0);

    v_ach := public.competition_club_cup_achievement(p_season_id, v_club.club_short_name, 'plate');
    v_plate_pts := coalesce(public.competition_owner_cup_points('plate', v_ach), 0);

    v_ach := public.competition_club_cup_achievement(p_season_id, v_club.club_short_name, 'shield');
    v_shield_pts := coalesce(public.competition_owner_cup_points('shield', v_ach), 0);

    v_ach := public.competition_club_cup_achievement(p_season_id, v_club.club_short_name, 'spoon');
    v_spoon_pts := coalesce(public.competition_owner_cup_points('spoon', v_ach), 0);

    v_ach := public.competition_club_cup_achievement(p_season_id, v_club.club_short_name, 'league_cup');
    v_lc_pts := coalesce(public.competition_owner_cup_points('league_cup', v_ach), 0);

    v_total := v_league_pts + v_super8_pts + v_plate_pts + v_shield_pts + v_spoon_pts + v_lc_pts;

    v_detail := jsonb_build_object(
      'league', jsonb_build_object('points', v_league_pts),
      'super8', jsonb_build_object(
        'achievement', public.competition_club_cup_achievement(p_season_id, v_club.club_short_name, 'super8'),
        'points', v_super8_pts
      ),
      'plate', jsonb_build_object(
        'achievement', public.competition_club_cup_achievement(p_season_id, v_club.club_short_name, 'plate'),
        'points', v_plate_pts
      ),
      'shield', jsonb_build_object(
        'achievement', public.competition_club_cup_achievement(p_season_id, v_club.club_short_name, 'shield'),
        'points', v_shield_pts
      ),
      'spoon', jsonb_build_object(
        'achievement', public.competition_club_cup_achievement(p_season_id, v_club.club_short_name, 'spoon'),
        'points', v_spoon_pts
      ),
      'league_cup', jsonb_build_object(
        'achievement', public.competition_club_cup_achievement(p_season_id, v_club.club_short_name, 'league_cup'),
        'points', v_lc_pts
      )
    );

    INSERT INTO public.competition_owner_season_ranking (
      season_id,
      season_label,
      club_short_name,
      owner_id,
      owner_tag,
      league_points,
      super8_points,
      plate_points,
      shield_points,
      spoon_points,
      league_cup_points,
      season_total,
      detail,
      computed_at
    )
    VALUES (
      p_season_id,
      v_season.label,
      v_club.club_short_name,
      v_owner_id,
      v_owner_tag,
      v_league_pts,
      v_super8_pts,
      v_plate_pts,
      v_shield_pts,
      v_spoon_pts,
      v_lc_pts,
      v_total,
      v_detail,
      now()
    )
    ON CONFLICT (season_id, club_short_name) DO UPDATE
    SET owner_tag = excluded.owner_tag,
        league_points = excluded.league_points,
        super8_points = excluded.super8_points,
        plate_points = excluded.plate_points,
        shield_points = excluded.shield_points,
        spoon_points = excluded.spoon_points,
        league_cup_points = excluded.league_cup_points,
        season_total = excluded.season_total,
        detail = excluded.detail,
        computed_at = now();

    v_count := v_count + 1;
  END LOOP;

  RETURN v_count;
END;
$function$;

CREATE OR REPLACE FUNCTION public.competition_owner_ranking_recompute_all()
RETURNS integer
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  r record;
  v_total int := 0;
  v_n int;
BEGIN
  IF auth.uid() IS NOT NULL AND NOT public.is_gpsl_admin() THEN
    RAISE EXCEPTION 'Admin only';
  END IF;

  FOR r IN
    SELECT id FROM public.competition_seasons
    WHERE status IN ('complete', 'active')
    ORDER BY id
  LOOP
    v_n := public.competition_owner_ranking_recompute_season(r.id);
    v_total := v_total + coalesce(v_n, 0);
  END LOOP;

  RETURN v_total;
END;
$function$;


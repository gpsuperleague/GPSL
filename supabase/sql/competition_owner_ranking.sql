CREATE OR REPLACE FUNCTION public.international_owner_draft_order()
RETURNS TABLE (
  pick_order smallint,
  club_short_name text,
  club_name text,
  owner_id uuid,
  owner_name text,
  owner_tag text,
  rank_points numeric,
  nation_code text,
  nation_name text,
  flag_emoji text
)
LANGUAGE sql
STABLE
SECURITY DEFINER
SET search_path = public
AS $$
  SELECT
    row_number() OVER (
      ORDER BY
        coalesce(r4.rolling_points, 0) DESC,
        public.competition_owner_display_name(c.owner_id),
        c."ShortName"
    )::smallint AS pick_order,
    c."ShortName" AS club_short_name,
    c."Club" AS club_name,
    c.owner_id,
    public.competition_owner_display_name(c.owner_id) AS owner_name,
    public.owner_registry_resolve_tag(c.owner_id) AS owner_tag,
    coalesce(r4.rolling_points, 0) AS rank_points,
    ion.nation_code,
    n.name AS nation_name,
    n.flag_emoji
  FROM public."Clubs" c
  LEFT JOIN public.competition_owner_ranking_rolling4_public r4
    ON r4.club_short_name = c."ShortName"
  LEFT JOIN public.international_owner_nations ion
    ON ion.club_short_name = c."ShortName" AND ion.is_active = true
  LEFT JOIN public.international_nations n ON n.code = ion.nation_code
  WHERE c.owner_id IS NOT NULL
  ORDER BY pick_order, c."ShortName";
$$;

CREATE VIEW public.international_owner_rank_public
WITH (security_invoker = false)
AS
SELECT * FROM public.international_owner_draft_order();

CREATE VIEW public.international_selection_public
WITH (security_invoker = false)
AS
SELECT
  w.id,
  w.phase,
  w.is_open,
  w.opens_at,
  w.closes_at,
  w.current_pick_rank,
  (
    SELECT d.club_short_name
    FROM public.international_owner_draft_order() d
    WHERE d.pick_order = w.current_pick_rank
    LIMIT 1
  ) AS current_pick_club,
  (
    SELECT count(*)::integer
    FROM public.international_owner_nations ion
    WHERE ion.is_active = true
  ) AS nations_assigned
FROM public.international_selection_windows w
WHERE w.is_open = true
ORDER BY w.id DESC
LIMIT 1;


CREATE OR REPLACE VIEW public.stadium_expansion_status_public
WITH (security_invoker = false)
AS
SELECT
  c."ShortName" AS club_short_name,
  coalesce(c.base_capacity, c."Capacity", 0)::int AS base_capacity,
  coalesce(c."Capacity", 0)::int AS current_capacity,
  public.stadium_max_capacity(coalesce(c.base_capacity, c."Capacity", 0)::int) AS max_capacity,
  public.stadium_expansion_headroom(c."ShortName") AS headroom,
  public.stadium_expansion_cost_per_seat(coalesce(c."Capacity", 0)::int) AS cost_per_seat,
  o.id AS active_order_id,
  o.seats_ordered,
  o.seats_delivered,
  o.total_cost_paid,
  o.status AS order_status,
  o.ordered_at,
  o.build_started_at,
  public.stadium_expansion_pre_build_day(o.ordered_at) AS pre_build_day,
  public.stadium_expansion_pre_build_message(
    least(public.stadium_expansion_pre_build_day(o.ordered_at), 6),
    o.seats_ordered
  ) AS pre_build_message,
  public.stadium_expansion_day7_deadline_uk(o.ordered_at) AS day7_deadline_uk,
  (o.status = 'awaiting_goahead' AND now() < public.stadium_expansion_day7_deadline_uk(o.ordered_at)) AS day7_decision_open,
  o.build_weeks_applied,
  CASE
    WHEN o.status = 'building' THEN round(100.0 * o.seats_delivered / nullif(o.seats_ordered, 0), 0)
    ELSE NULL
  END AS build_progress_pct
FROM public."Clubs" c
LEFT JOIN LATERAL (
  SELECT ord.*
  FROM public.stadium_expansion_orders ord
  WHERE ord.club_short_name = c."ShortName"
    AND ord.status IN ('pre_build', 'awaiting_goahead', 'building')
  ORDER BY ord.ordered_at DESC
  LIMIT 1
) o ON true
WHERE c."ShortName" = public.my_club_shortname();

CREATE OR REPLACE VIEW public.stadium_expansion_quotes_public
WITH (security_invoker = false)
AS
SELECT
  q.id,
  q.club_short_name,
  q.seats,
  q.capacity_at_quote,
  q.cost_per_seat,
  q.total_cost,
  q.created_at
FROM public.stadium_expansion_quotes q
WHERE q.club_short_name = public.my_club_shortname()
  AND q.consumed_at IS NULL
ORDER BY q.created_at DESC;

-- ---------------------------------------------------------------------------
-- Admin
-- ---------------------------------------------------------------------------

CREATE OR REPLACE FUNCTION public.admin_update_stadium_cost_settings(
  p_tier1 numeric,
  p_tier2 numeric,
  p_tier3 numeric,
  p_tier_mid integer DEFAULT NULL,
  p_tier_high integer DEFAULT NULL,
  p_cancel_penalty numeric DEFAULT NULL
)
RETURNS void
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $function$
BEGIN
  IF NOT public.is_gpsl_admin() THEN
    RAISE EXCEPTION 'Admin only';
  END IF;

  IF p_tier1 IS NULL OR p_tier1 <= 0 OR p_tier2 IS NULL OR p_tier2 <= 0 OR p_tier3 IS NULL OR p_tier3 <= 0 THEN
    RAISE EXCEPTION 'Tier costs must be positive';
  END IF;

  UPDATE public.global_settings
  SET
    stadium_cost_tier1 = round(p_tier1, 2),
    stadium_cost_tier2 = round(p_tier2, 2),
    stadium_cost_tier3 = round(p_tier3, 2),
    stadium_capacity_tier_mid = coalesce(p_tier_mid, stadium_capacity_tier_mid),
    stadium_capacity_tier_high = coalesce(p_tier_high, stadium_capacity_tier_high),
    stadium_expansion_cancel_penalty = coalesce(round(p_cancel_penalty, 2), stadium_expansion_cancel_penalty),
    updated_at = now()
  WHERE id = 1;
END;
$function$;


CREATE TABLE IF NOT EXISTS public.stadium_expansion_quotes (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  club_short_name text NOT NULL REFERENCES public."Clubs" ("ShortName"),
  seats integer NOT NULL CHECK (seats > 0),
  capacity_at_quote integer NOT NULL CHECK (capacity_at_quote >= 0),
  cost_per_seat numeric(14, 2) NOT NULL CHECK (cost_per_seat > 0),
  total_cost numeric(14, 2) NOT NULL CHECK (total_cost > 0),
  created_at timestamptz NOT NULL DEFAULT now(),
  consumed_at timestamptz,
  consumed_order_id bigint
);

CREATE TABLE IF NOT EXISTS public.stadium_expansion_orders (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  club_short_name text NOT NULL REFERENCES public."Clubs" ("ShortName"),
  season_id_ordered bigint NOT NULL REFERENCES public.competition_seasons (id),
  quote_id bigint REFERENCES public.stadium_expansion_quotes (id),
  seats_ordered integer NOT NULL CHECK (seats_ordered > 0),
  total_cost_paid numeric(14, 2) NOT NULL CHECK (total_cost_paid > 0),
  cost_per_seat numeric(14, 2) NOT NULL,
  capacity_at_order integer NOT NULL,
  base_capacity_at_order integer NOT NULL,
  max_capacity_at_order integer NOT NULL,
  status text NOT NULL DEFAULT 'pre_build' CHECK (
    status IN (
      'pre_build',
      'awaiting_goahead',
      'building',
      'completed',
      'cancelled'
    )
  ),
  seats_delivered integer NOT NULL DEFAULT 0 CHECK (seats_delivered >= 0),
  build_weeks_applied smallint NOT NULL DEFAULT 0 CHECK (build_weeks_applied >= 0 AND build_weeks_applied <= 4),
  ordered_at timestamptz NOT NULL DEFAULT now(),
  build_started_at timestamptz,
  completed_at timestamptz,
  cancelled_at timestamptz,
  goahead_decision text CHECK (goahead_decision IN ('continue', 'cancel', 'auto_continue')),
  ledger_payment_id bigint,
  CONSTRAINT stadium_expansion_orders_delivered_le_seats
    CHECK (seats_delivered <= seats_ordered)
);

CREATE INDEX IF NOT EXISTS stadium_expansion_orders_club_idx
  ON public.stadium_expansion_orders (club_short_name, ordered_at DESC);

CREATE INDEX IF NOT EXISTS stadium_expansion_quotes_club_idx
  ON public.stadium_expansion_quotes (club_short_name, created_at DESC);

-- ---------------------------------------------------------------------------
-- Capacity cap (from original / base_capacity)
-- ---------------------------------------------------------------------------

CREATE OR REPLACE FUNCTION public.stadium_max_capacity(p_base_capacity integer)
RETURNS integer
LANGUAGE sql
IMMUTABLE
AS $$
  SELECT CASE
    WHEN coalesce(p_base_capacity, 0) <= 15000 THEN 25000
    WHEN p_base_capacity <= 25000 THEN 30000
    WHEN p_base_capacity <= 35000 THEN 40000
    WHEN p_base_capacity <= 45000 THEN 50000
    WHEN p_base_capacity <= 50000 THEN 55000
    WHEN p_base_capacity <= 55000 THEN 60000
    ELSE p_base_capacity
  END;
$$;

CREATE OR REPLACE FUNCTION public.stadium_expansion_headroom(
  p_club_short_name text
)
RETURNS integer
LANGUAGE plpgsql
STABLE
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_base int;
  v_current int;
  v_max int;
  v_reserved int := 0;
BEGIN
  SELECT
    coalesce(c.base_capacity, c."Capacity", 0)::int,
    coalesce(c."Capacity", 0)::int
  INTO v_base, v_current
  FROM public."Clubs" c
  WHERE c."ShortName" = p_club_short_name;

  IF NOT FOUND THEN
    RETURN 0;
  END IF;

  v_max := public.stadium_max_capacity(v_base);

  SELECT coalesce(sum(greatest(o.seats_ordered - o.seats_delivered, 0)), 0)::int
  INTO v_reserved
  FROM public.stadium_expansion_orders o
  WHERE o.club_short_name = p_club_short_name
    AND o.status IN ('pre_build', 'awaiting_goahead', 'building');

  RETURN greatest(v_max - v_current - v_reserved, 0);
END;
$function$;

-- ---------------------------------------------------------------------------
-- Tiered cost per seat (current capacity × admin tier rates)
-- ---------------------------------------------------------------------------

CREATE OR REPLACE FUNCTION public.stadium_expansion_cost_per_seat(p_current_capacity integer)
RETURNS numeric
LANGUAGE plpgsql
STABLE
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_current int := greatest(coalesce(p_current_capacity, 0), 0);
  v_t1 numeric;
  v_t2 numeric;
  v_t3 numeric;
  v_mid int;
  v_high int;
BEGIN
  SELECT
    stadium_cost_tier1,
    stadium_cost_tier2,
    stadium_cost_tier3,
    stadium_capacity_tier_mid,
    stadium_capacity_tier_high
  INTO v_t1, v_t2, v_t3, v_mid, v_high
  FROM public.global_settings
  WHERE id = 1;

  v_t1 := coalesce(v_t1, 5000);
  v_t2 := coalesce(v_t2, 7500);
  v_t3 := coalesce(v_t3, 10000);
  v_mid := coalesce(v_mid, 30000);
  v_high := coalesce(v_high, 50000);

  IF v_current < v_mid THEN
    RETURN v_t1;
  ELSIF v_current <= v_high THEN
    RETURN v_t2;
  END IF;

  RETURN v_t3;
END;
$function$;

-- Next Friday 19:00 UK strictly after p_ts (GPSL week boundary).
CREATE OR REPLACE FUNCTION public.stadium_next_friday_7pm_uk_after(p_ts timestamptz)
RETURNS timestamptz
LANGUAGE plpgsql
IMMUTABLE
AS $function$
DECLARE
  v_local timestamp;
  v_dow int;
  v_days int;
  v_candidate timestamp;
BEGIN
  v_local := p_ts AT TIME ZONE 'Europe/London';
  v_dow := extract(dow from v_local)::int; -- 0=Sun … 5=Fri

  v_days := (5 - v_dow + 7) % 7;
  v_candidate := date_trunc('day', v_local) + make_interval(days => v_days) + interval '19 hours';

  IF v_candidate <= v_local THEN
    v_candidate := v_candidate + interval '7 days';
  END IF;

  RETURN v_candidate AT TIME ZONE 'Europe/London';
END;
$function$;

CREATE OR REPLACE FUNCTION public.stadium_expansion_pre_build_day(p_ordered_at timestamptz)
RETURNS integer
LANGUAGE sql
IMMUTABLE
AS $$
  SELECT greatest(
    1,
    (
      (now() AT TIME ZONE 'Europe/London')::date
      - (p_ordered_at AT TIME ZONE 'Europe/London')::date
      + 1
    )::integer
  );
$$;

CREATE OR REPLACE FUNCTION public.stadium_expansion_pre_build_message(p_day integer, p_seats integer)
RETURNS text
LANGUAGE sql
IMMUTABLE
AS $$
  SELECT CASE p_day
    WHEN 1 THEN format('%s seats ordered', p_seats)
    WHEN 2 THEN 'Rapid Build Co confirmed order'
    WHEN 3 THEN 'Rapid Build Co sourcing materials and workforce'
    WHEN 4 THEN 'Rapid Build Co deploying to site'
    WHEN 5 THEN 'Rapid Build Co preparing site'
    WHEN 6 THEN 'Rapid Build Co beginning work'
    ELSE 'Rapid Build Co — awaiting your decision'
  END;
$$;

CREATE OR REPLACE FUNCTION public.stadium_expansion_day7_deadline_uk(p_ordered_at timestamptz)
RETURNS timestamptz
LANGUAGE sql
IMMUTABLE
AS $$
  SELECT (
    ((p_ordered_at AT TIME ZONE 'Europe/London')::date + 6)::timestamp
    + interval '19 hours'
  ) AT TIME ZONE 'Europe/London';
$$;

-- ---------------------------------------------------------------------------
-- Progress sync (call on stadium page load / after admin calendar tick)
-- ---------------------------------------------------------------------------

CREATE OR REPLACE FUNCTION public.stadium_expansion_sync_progress(p_club_short_name text DEFAULT NULL)
RETURNS void
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_row public.stadium_expansion_orders;
  v_day int;
  v_deadline timestamptz;
  v_weeks int;
  v_target int;
  v_add int;
  v_boundary timestamptz;
  v_ts timestamptz;
  i int;
BEGIN
  FOR v_row IN
    SELECT o.*
    FROM public.stadium_expansion_orders o
    WHERE o.status IN ('pre_build', 'awaiting_goahead', 'building')
      AND (p_club_short_name IS NULL OR o.club_short_name = p_club_short_name)
    FOR UPDATE OF o
  LOOP
    IF v_row.status IN ('pre_build', 'awaiting_goahead') THEN
      v_day := public.stadium_expansion_pre_build_day(v_row.ordered_at);
      v_deadline := public.stadium_expansion_day7_deadline_uk(v_row.ordered_at);

      IF v_day < 7 THEN
        UPDATE public.stadium_expansion_orders
        SET status = 'pre_build'
        WHERE id = v_row.id;
        CONTINUE;
      END IF;

      IF v_row.status = 'pre_build' AND now() < v_deadline THEN
        UPDATE public.stadium_expansion_orders
        SET status = 'awaiting_goahead'
        WHERE id = v_row.id;
        CONTINUE;
      END IF;

      IF v_row.status = 'awaiting_goahead' AND now() >= v_deadline AND v_row.goahead_decision IS NULL THEN
        UPDATE public.stadium_expansion_orders
        SET
          status = 'building',
          build_started_at = now(),
          goahead_decision = 'auto_continue'
        WHERE id = v_row.id;

        SELECT * INTO v_row FROM public.stadium_expansion_orders WHERE id = v_row.id;
      END IF;
    END IF;

    IF v_row.status = 'building' AND v_row.build_started_at IS NOT NULL THEN
      v_weeks := 0;
      v_ts := v_row.build_started_at;

      FOR i IN 1..4 LOOP
        v_boundary := public.stadium_next_friday_7pm_uk_after(v_ts);
        EXIT WHEN now() < v_boundary;
        v_weeks := i;
        v_ts := v_boundary;
      END LOOP;

      v_target := (v_row.seats_ordered * least(v_weeks, 4) / 4)::int;
      v_add := greatest(v_target - v_row.seats_delivered, 0);

      IF v_add > 0 THEN
        UPDATE public."Clubs"
        SET "Capacity" = coalesce("Capacity", 0) + v_add
        WHERE "ShortName" = v_row.club_short_name;

        UPDATE public.stadium_expansion_orders
        SET
          seats_delivered = v_target,
          build_weeks_applied = least(v_weeks, 4)::smallint
        WHERE id = v_row.id;
      END IF;

      IF v_weeks >= 4 OR v_target >= v_row.seats_ordered THEN
        UPDATE public.stadium_expansion_orders
        SET
          status = 'completed',
          completed_at = now(),
          seats_delivered = v_row.seats_ordered,
          build_weeks_applied = 4
        WHERE id = v_row.id
          AND status = 'building';
      END IF;
    END IF;
  END LOOP;
END;
$function$;


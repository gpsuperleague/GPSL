    gpsl_month_to IN (
      'august', 'september', 'october', 'november', 'december',
      'january', 'february', 'march', 'april', 'may'
    )
  ),
  stat_type text NOT NULL CHECK (
    stat_type IN (
      'player_max_goals',
      'player_max_assists',
      'club_wins',
      'club_goals_for',
      'club_clean_sheets',
      'club_potm_awards'
    )

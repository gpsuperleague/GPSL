  updated_at timestamptz NOT NULL DEFAULT now(),
  CONSTRAINT competition_challenge_month_order CHECK (
    public.competition_gpsl_month_sort(gpsl_month_from)
    <= public.competition_gpsl_month_sort(gpsl_month_to)
  )
);


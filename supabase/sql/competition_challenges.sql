CREATE OR REPLACE FUNCTION public.competition_award_challenge(
  p_challenge_id bigint,
  p_club_short_name text,
  p_stat_value int
)
RETURNS boolean
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_c public.competition_challenge_config;
  v_amount numeric;
BEGIN
  SELECT * INTO v_c
  FROM public.competition_challenge_config
  WHERE id = p_challenge_id
    AND is_active = true;

  IF NOT FOUND THEN
    RETURN false;
  END IF;

  IF EXISTS (
    SELECT 1 FROM public.competition_challenge_awarded
    WHERE challenge_id = p_challenge_id
      AND club_short_name = p_club_short_name
  ) THEN
    RETURN false;
  END IF;

  IF NOT public.competition_challenge_window_open(
    v_c.season_id, v_c.window_phase, v_c.gpsl_month_to
  ) THEN
    RETURN false;
  END IF;

  v_amount := v_c.prize_amount;
  PERFORM public.competition_credit_club_balance(p_club_short_name, v_amount);

  INSERT INTO public.competition_finance_ledger (
    season_id, fixture_id, club_short_name, entry_type, amount, description, metadata
  )
  VALUES (
    v_c.season_id,
    NULL,
    p_club_short_name,
    'prize_challenge',
    v_amount,
    format('Challenge — %s', v_c.title),
    jsonb_build_object(
      'challenge_id', v_c.id,
      'window_phase', v_c.window_phase,
      'stat_type', v_c.stat_type,
      'target_value', v_c.target_value,
      'stat_value', p_stat_value
    )
  );

  INSERT INTO public.competition_challenge_awarded (
    season_id, challenge_id, club_short_name, amount, stat_value, metadata
  )
  VALUES (
    v_c.season_id,
    p_challenge_id,
    p_club_short_name,
    v_amount,
    p_stat_value,
    jsonb_build_object('title', v_c.title)
  );

  RETURN true;
END;
$function$;

CREATE OR REPLACE FUNCTION public.competition_try_award_period_bonus(
  p_season_id bigint,
  p_club_short_name text,
  p_window_phase text
)
RETURNS boolean
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_total int;
  v_done int;
  v_bonus numeric;
  v_deadline text;
BEGIN
  IF EXISTS (
    SELECT 1 FROM public.competition_challenge_period_bonus_awarded
    WHERE season_id = p_season_id
      AND window_phase = p_window_phase
  ) THEN
    RETURN false;
  END IF;

  SELECT count(*)::int INTO v_total
  FROM public.competition_challenge_config
  WHERE season_id = p_season_id
    AND window_phase = p_window_phase
    AND is_active = true;

  IF v_total = 0 THEN
    RETURN false;
  END IF;

  SELECT count(*)::int INTO v_done
  FROM public.competition_challenge_awarded a
  JOIN public.competition_challenge_config c ON c.id = a.challenge_id
  WHERE a.season_id = p_season_id
    AND a.club_short_name = p_club_short_name
    AND c.window_phase = p_window_phase
    AND c.is_active = true;

  IF v_done < v_total THEN
    RETURN false;
  END IF;

  SELECT max(c.gpsl_month_to) INTO v_deadline
  FROM public.competition_challenge_config c
  WHERE c.season_id = p_season_id
    AND c.window_phase = p_window_phase
    AND c.is_active = true;

  IF NOT public.competition_challenge_window_open(p_season_id, p_window_phase, v_deadline) THEN
    RETURN false;
  END IF;

  v_bonus := (SELECT challenge_period_bonus FROM public.global_settings WHERE id = 1);

  IF v_bonus IS NULL OR v_bonus <= 0 THEN
    RETURN false;
  END IF;

  PERFORM public.competition_credit_club_balance(p_club_short_name, v_bonus);

  INSERT INTO public.competition_finance_ledger (
    season_id, fixture_id, club_short_name, entry_type, amount, description, metadata
  )
  VALUES (
    p_season_id,
    NULL,
    p_club_short_name,
    'prize_challenge',
    v_bonus,
    format('Challenge bonus — first to complete all %s targets', p_window_phase),
    jsonb_build_object(
      'window_phase', p_window_phase,
      'bonus', true,
      'challenges_completed', v_done
    )
  );

  INSERT INTO public.competition_challenge_period_bonus_awarded (
    season_id, window_phase, club_short_name, amount
  )
  VALUES (p_season_id, p_window_phase, p_club_short_name, v_bonus);

  RETURN true;
END;
$function$;

CREATE OR REPLACE FUNCTION public.competition_try_award_challenges(
  p_season_id bigint,
  p_club_short_name text
)
RETURNS int
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_row public.competition_challenge_config;
  v_val int;
  v_awarded int := 0;
  v_phase text;
BEGIN
  IF p_season_id IS NULL OR p_club_short_name IS NULL OR btrim(p_club_short_name) = '' THEN
    RETURN 0;
  END IF;

  FOR v_row IN
    SELECT *
    FROM public.competition_challenge_config
    WHERE season_id = p_season_id
      AND is_active = true
    ORDER BY sort_order, id
  LOOP
    IF EXISTS (
      SELECT 1 FROM public.competition_challenge_awarded
      WHERE challenge_id = v_row.id
        AND club_short_name = p_club_short_name
    ) THEN
      CONTINUE;
    END IF;

    IF NOT public.competition_challenge_window_open(
      p_season_id, v_row.window_phase, v_row.gpsl_month_to
    ) THEN
      CONTINUE;
    END IF;

    v_val := public.competition_challenge_stat_value(
      p_season_id,
      p_club_short_name,
      v_row.stat_type,
      v_row.gpsl_month_from,
      v_row.gpsl_month_to,
      v_row.include_league,
      v_row.include_cup
    );

    IF v_val >= v_row.target_value THEN
      IF public.competition_award_challenge(v_row.id, p_club_short_name, v_val) THEN
        v_awarded := v_awarded + 1;
        PERFORM public.competition_try_award_period_bonus(
          p_season_id, p_club_short_name, v_row.window_phase
        );
      END IF;
    END IF;
  END LOOP;

  RETURN v_awarded;
END;
$function$;


CREATE TABLE IF NOT EXISTS public.competition_challenge_config (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  season_id bigint NOT NULL REFERENCES public.competition_seasons (id) ON DELETE CASCADE,
  title text NOT NULL,
  description text,
  window_phase text NOT NULL CHECK (window_phase IN ('start', 'mid')),
  gpsl_month_from text NOT NULL CHECK (
    gpsl_month_from IN (
      'august', 'september', 'october', 'november', 'december',
      'january', 'february', 'march', 'april', 'may'
    )
  ),

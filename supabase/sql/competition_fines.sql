-- ---------------------------------------------------------------------------
-- Tariff catalogue (admin-editable)
-- ---------------------------------------------------------------------------

CREATE TABLE IF NOT EXISTS public.competition_fine_tariff (
  code text PRIMARY KEY,
  label text NOT NULL,
  category text NOT NULL DEFAULT 'matchday' CHECK (
    category IN ('matchday', 'squad', 'transfer', 'manager', 'compensation', 'other')
  ),
  direction text NOT NULL DEFAULT 'fine' CHECK (direction IN ('fine', 'compensation')),
  amount numeric(14, 2) CHECK (amount IS NULL OR amount >= 0),
  amount_mode text NOT NULL DEFAULT 'fixed' CHECK (amount_mode IN ('fixed', 'manual')),
  is_active boolean NOT NULL DEFAULT true,
  sort_order smallint NOT NULL DEFAULT 0,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now()
);

CREATE TABLE IF NOT EXISTS public.competition_fine_applied (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  season_id bigint REFERENCES public.competition_seasons (id) ON DELETE SET NULL,
  tariff_code text NOT NULL REFERENCES public.competition_fine_tariff (code),
  club_short_name text NOT NULL REFERENCES public."Clubs" ("ShortName"),
  amount numeric(14, 2) NOT NULL CHECK (amount <> 0),
  direction text NOT NULL CHECK (direction IN ('fine', 'compensation')),
  description text NOT NULL,
  note text,
  fixture_id bigint REFERENCES public.competition_fixtures (id) ON DELETE SET NULL,
  ledger_id bigint REFERENCES public.competition_finance_ledger (id) ON DELETE SET NULL,
  applied_by text NOT NULL DEFAULT 'ADMIN',
  applied_at timestamptz NOT NULL DEFAULT now()
);

CREATE INDEX IF NOT EXISTS competition_fine_applied_club_idx
  ON public.competition_fine_applied (club_short_name, applied_at DESC);

ALTER TABLE public.competition_fine_tariff ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.competition_fine_applied ENABLE ROW LEVEL SECURITY;

DROP POLICY IF EXISTS competition_fine_tariff_select ON public.competition_fine_tariff;
CREATE POLICY competition_fine_tariff_select ON public.competition_fine_tariff
  FOR SELECT TO authenticated USING (true);

DROP POLICY IF EXISTS competition_fine_applied_select ON public.competition_fine_applied;
CREATE POLICY competition_fine_applied_select ON public.competition_fine_applied
  FOR SELECT TO authenticated USING (true);

CREATE OR REPLACE VIEW public.competition_fine_tariffs_public
WITH (security_invoker = false)
AS
SELECT
  code,
  label,
  category,
  direction,
  amount,
  amount_mode,
  is_active,
  sort_order
FROM public.competition_fine_tariff
WHERE is_active = true
ORDER BY category, sort_order, label;

GRANT SELECT ON public.competition_fine_tariffs_public TO authenticated;
GRANT SELECT ON public.competition_fine_tariffs_public TO anon;

-- ---------------------------------------------------------------------------
-- Apply fine / compensation (instant ledger)
-- ---------------------------------------------------------------------------

CREATE OR REPLACE FUNCTION public.competition_apply_club_fine_tariff(
  p_club_short_name text,
  p_tariff_code text,
  p_amount_override numeric DEFAULT NULL,
  p_note text DEFAULT NULL,
  p_fixture_id bigint DEFAULT NULL,
  p_season_id bigint DEFAULT NULL
)
RETURNS jsonb
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_tariff public.competition_fine_tariff;
  v_club text := btrim(p_club_short_name);
  v_amount numeric;
  v_ledger_amount numeric;
  v_season_id bigint;
  v_desc text;
  v_ledger_id bigint;
  v_applied_id bigint;
BEGIN
  IF v_club IS NULL OR v_club = '' THEN
    RAISE EXCEPTION 'Club required';
  END IF;

  SELECT * INTO v_tariff
  FROM public.competition_fine_tariff
  WHERE code = p_tariff_code AND is_active = true;

  IF NOT FOUND THEN
    RAISE EXCEPTION 'Unknown or inactive tariff: %', p_tariff_code;
  END IF;

  IF v_tariff.amount_mode = 'manual' THEN
    v_amount := p_amount_override;
    IF v_amount IS NULL OR v_amount <= 0 THEN
      RAISE EXCEPTION 'Manual amount required for %', v_tariff.label;
    END IF;
  ELSE
    v_amount := coalesce(p_amount_override, v_tariff.amount);
    IF v_amount IS NULL OR v_amount <= 0 THEN
      RAISE EXCEPTION 'Tariff % has no amount configured', v_tariff.label;
    END IF;
  END IF;

  IF p_season_id IS NULL THEN
    SELECT id INTO v_season_id
    FROM public.competition_seasons
    WHERE is_current = true
    ORDER BY id DESC
    LIMIT 1;
  ELSE
    v_season_id := p_season_id;
  END IF;

  IF v_tariff.direction = 'fine' THEN
    v_ledger_amount := -abs(v_amount);
    PERFORM public.competition_credit_club_balance(v_club, v_ledger_amount);
    v_desc := format('Fine — %s', v_tariff.label);
  ELSE
    v_ledger_amount := abs(v_amount);
    PERFORM public.competition_credit_club_balance(v_club, v_ledger_amount);
    v_desc := format('Compensation — %s', v_tariff.label);
  END IF;

  IF p_note IS NOT NULL AND btrim(p_note) <> '' THEN
    v_desc := v_desc || ' — ' || btrim(p_note);
  END IF;

  INSERT INTO public.competition_finance_ledger (
    season_id, fixture_id, club_short_name, entry_type, amount, description, metadata
  )
  VALUES (
    v_season_id,
    p_fixture_id,
    v_club,
    'gov_fine_compensation',
    v_ledger_amount,
    v_desc,
    jsonb_build_object(
      'tariff_code', v_tariff.code,
      'direction', v_tariff.direction,
      'category', v_tariff.category
    )
  )
  RETURNING id INTO v_ledger_id;

  INSERT INTO public.competition_fine_applied (
    season_id, tariff_code, club_short_name, amount, direction,
    description, note, fixture_id, ledger_id, applied_by
  )
  VALUES (
    v_season_id, v_tariff.code, v_club, abs(v_amount), v_tariff.direction,
    v_desc, p_note, p_fixture_id, v_ledger_id,
    CASE WHEN public.is_gpsl_admin() THEN 'ADMIN' ELSE 'SYSTEM' END
  )
  RETURNING id INTO v_applied_id;

  RETURN jsonb_build_object(
    'applied_id', v_applied_id,
    'ledger_id', v_ledger_id,
    'club_short_name', v_club,
    'tariff_code', v_tariff.code,
    'amount', abs(v_amount),
    'direction', v_tariff.direction,
    'ledger_amount', v_ledger_amount
  );
END;
$function$;


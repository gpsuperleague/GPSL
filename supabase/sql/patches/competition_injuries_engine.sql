CREATE OR REPLACE FUNCTION public.competition_injury_settings_row()
RETURNS public.competition_injury_settings
LANGUAGE sql
STABLE
SET search_path = public
AS $$
  SELECT * FROM public.competition_injury_settings WHERE id = 1;
$$;

CREATE OR REPLACE FUNCTION public.competition_injury_is_preseason_month(p_month text)
RETURNS boolean
LANGUAGE sql
STABLE
SET search_path = public
AS $$
  SELECT lower(btrim(p_month)) = ANY (
    SELECT lower(unnest(s.preseason_months))
    FROM public.competition_injury_settings s
    WHERE s.id = 1
  );
$$;

CREATE OR REPLACE FUNCTION public.competition_injury_player_is_out(p_player_id text)
RETURNS boolean
LANGUAGE sql
STABLE
SET search_path = public
AS $$
  SELECT EXISTS (
    SELECT 1
    FROM public.competition_player_injuries i
    WHERE i.player_id = p_player_id
      AND i.status = 'active'
      AND coalesce(i.matches_out_remaining, 0) > 0
  );
$$;

CREATE OR REPLACE FUNCTION public.competition_injury_fit_gk_count(p_club text)
RETURNS int
LANGUAGE sql
STABLE
SET search_path = public
AS $$
  SELECT count(*)::int
  FROM public."Players" p
  WHERE p."Contracted_Team" = p_club
    AND upper(coalesce(p."Position", '')) = 'GK'
    AND NOT public.competition_injury_player_is_out(p."Konami_ID"::text);
$$;

CREATE OR REPLACE FUNCTION public.competition_injury_ensure_club_season(
  p_season_id bigint,
  p_club text
)
RETURNS public.competition_club_injury_season
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_settings public.competition_injury_settings;
  v_row public.competition_club_injury_season;
  v_risk numeric;
BEGIN
  v_settings := public.competition_injury_settings_row();

  SELECT * INTO v_row
  FROM public.competition_club_injury_season
  WHERE season_id = p_season_id AND club_short_name = p_club;

  IF FOUND THEN
    RETURN v_row;
  END IF;

  v_risk := v_settings.risk_min
    + random() * (v_settings.risk_max - v_settings.risk_min);
  v_risk := round(v_risk::numeric, 3);

  INSERT INTO public.competition_club_injury_season (
    season_id, club_short_name, injury_risk
  )
  VALUES (p_season_id, p_club, v_risk)
  ON CONFLICT (season_id, club_short_name) DO NOTHING
  RETURNING * INTO v_row;

  IF v_row.club_short_name IS NULL THEN
    SELECT * INTO v_row
    FROM public.competition_club_injury_season
    WHERE season_id = p_season_id AND club_short_name = p_club;
  END IF;

  RETURN v_row;
END;
$function$;

CREATE OR REPLACE FUNCTION public.competition_injury_assign_fixtures(
  p_injury_id bigint
)
RETURNS void
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_inj public.competition_player_injuries%rowtype;
  v_season_id bigint;
  v_fx record;
  v_seq int := 0;
  v_need_out int;
  v_need_rec int;
BEGIN
  SELECT * INTO v_inj FROM public.competition_player_injuries WHERE id = p_injury_id;
  IF NOT FOUND THEN RETURN; END IF;

  v_need_out := greatest(coalesce(v_inj.matches_out_remaining, 0), 0);
  v_need_rec := greatest(coalesce(v_inj.recovery_remaining, 0), 0);

  DELETE FROM public.competition_player_injury_fixtures
  WHERE injury_id = p_injury_id AND served = false;

  SELECT s.id INTO v_season_id
  FROM public.competition_seasons s
  WHERE s.is_current = true
  ORDER BY s.id DESC
  LIMIT 1;

  IF v_season_id IS NULL THEN
    v_season_id := coalesce(v_inj.season_id, v_inj.incurred_season_id);
  END IF;

  FOR v_fx IN
    SELECT f.id
    FROM public.competition_fixtures f
    WHERE f.season_id = v_season_id
      AND f.status = 'scheduled'
      AND (f.home_club_short_name = v_inj.club_short_name
           OR f.away_club_short_name = v_inj.club_short_name)
      AND (v_inj.source_fixture_id IS NULL OR f.id IS DISTINCT FROM v_inj.source_fixture_id)
    ORDER BY coalesce(f.matchday, 9999), f.id
  LOOP
    EXIT WHEN v_need_out <= 0 AND v_need_rec <= 0;
    v_seq := v_seq + 1;

    IF v_need_out > 0 THEN
      INSERT INTO public.competition_player_injury_fixtures (
        injury_id, fixture_id, phase, served
      ) VALUES (p_injury_id, v_fx.id, 'out', false)
      ON CONFLICT (injury_id, fixture_id) DO UPDATE
        SET phase = 'out', served = false;
      v_need_out := v_need_out - 1;
    ELSIF v_need_rec > 0 THEN
      INSERT INTO public.competition_player_injury_fixtures (
        injury_id, fixture_id, phase, served
      ) VALUES (p_injury_id, v_fx.id, 'recovery', false)
      ON CONFLICT (injury_id, fixture_id) DO UPDATE
        SET phase = 'recovery', served = false;
      v_need_rec := v_need_rec - 1;
    END IF;
  END LOOP;
END;
$function$;

CREATE OR REPLACE FUNCTION public.competition_injury_issue(
  p_season_id bigint,
  p_club text,
  p_player_id text,
  p_catalogue_id bigint,
  p_source_fixture_id bigint
)
RETURNS bigint
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_cat public.competition_injury_catalogue%rowtype;
  v_id bigint;
  v_state public.competition_club_injury_season;
BEGIN
  SELECT * INTO v_cat FROM public.competition_injury_catalogue WHERE id = p_catalogue_id;
  IF NOT FOUND THEN RAISE EXCEPTION 'Injury catalogue row not found'; END IF;

  v_state := public.competition_injury_ensure_club_season(p_season_id, p_club);

  INSERT INTO public.competition_player_injuries (
    season_id, incurred_season_id, player_id, club_short_name,
    label, status, catalogue_id, severity,
    matches_out, recovery_matches,
    matches_out_remaining, recovery_remaining,
    source_fixture_id
  )
  VALUES (
    p_season_id, p_season_id, p_player_id, p_club,
    v_cat.name, 'active', v_cat.id, v_cat.severity,
    v_cat.matches_out, v_cat.recovery_matches,
    v_cat.matches_out, v_cat.recovery_matches,
    p_source_fixture_id
  )
  RETURNING id INTO v_id;

  UPDATE public.competition_club_injury_season
  SET count_total = count_total + 1,
      count_major = count_major + CASE WHEN v_cat.severity = 'Major' THEN 1 ELSE 0 END,
      count_moderate = count_moderate + CASE WHEN v_cat.severity = 'Moderate' THEN 1 ELSE 0 END,
      count_minor = count_minor + CASE WHEN v_cat.severity = 'Minor' THEN 1 ELSE 0 END
  WHERE season_id = p_season_id AND club_short_name = p_club;

  PERFORM public.competition_injury_assign_fixtures(v_id);
  RETURN v_id;
END;
$function$;

CREATE OR REPLACE FUNCTION public.competition_injury_pick_severity(
  p_state public.competition_club_injury_season,
  p_settings public.competition_injury_settings
)
RETURNS text
LANGUAGE plpgsql
STABLE
SET search_path = public
AS $function$
DECLARE
  v_w_min numeric := 0;
  v_w_mod numeric := 0;
  v_w_maj numeric := 0;
  v_total numeric;
  v_roll numeric;
BEGIN
  IF p_state.count_total >= p_settings.max_total THEN
    RETURN NULL;
  END IF;

  IF p_state.count_minor < p_settings.max_minor THEN
    v_w_min := p_settings.weight_minor;
  END IF;
  IF p_state.count_moderate < p_settings.max_moderate THEN
    v_w_mod := p_settings.weight_moderate;
  END IF;
  IF p_state.count_major < p_settings.max_major THEN
    v_w_maj := p_settings.weight_major;
  END IF;

  v_total := v_w_min + v_w_mod + v_w_maj;
  IF v_total <= 0 THEN
    RETURN NULL;
  END IF;

  v_roll := random() * v_total;
  IF v_roll < v_w_min THEN
    RETURN 'Minor';
  ELSIF v_roll < v_w_min + v_w_mod THEN
    RETURN 'Moderate';
  END IF;
  RETURN 'Major';
END;
$function$;

CREATE OR REPLACE FUNCTION public.competition_injury_roll_for_club(
  p_fixture_id bigint,
  p_club text
)
RETURNS jsonb
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_fixture public.competition_fixtures%rowtype;
  v_settings public.competition_injury_settings;
  v_state public.competition_club_injury_season;
  v_chance numeric;
  v_severity text;
  v_cat public.competition_injury_catalogue%rowtype;
  v_player_id text;
  v_injury_id bigint;
  v_fit_gk int;
BEGIN
  SELECT * INTO v_fixture FROM public.competition_fixtures WHERE id = p_fixture_id;
  IF NOT FOUND THEN
    RETURN jsonb_build_object('ok', false, 'reason', 'fixture_not_found');
  END IF;

  IF v_fixture.status <> 'played' THEN
    RETURN jsonb_build_object('ok', false, 'reason', 'fixture_not_played');
  END IF;

  IF public.competition_injury_is_preseason_month(v_fixture.gpsl_month) THEN
    INSERT INTO public.competition_fixture_injury_roll (fixture_id, club_short_name, did_injure)
    VALUES (p_fixture_id, p_club, false)
    ON CONFLICT DO NOTHING;
    RETURN jsonb_build_object('ok', true, 'did_injure', false, 'reason', 'preseason_month');
  END IF;

  IF NOT EXISTS (
    SELECT 1
    FROM public.competition_match_player_stats m
    WHERE m.fixture_id = p_fixture_id
      AND m.club_short_name = p_club
      AND m.appeared = true
  ) THEN
    RETURN jsonb_build_object('ok', false, 'reason', 'no_stats_yet');
  END IF;

  IF EXISTS (
    SELECT 1 FROM public.competition_fixture_injury_roll r
    WHERE r.fixture_id = p_fixture_id AND r.club_short_name = p_club
  ) THEN
    RETURN jsonb_build_object('ok', true, 'did_injure', false, 'reason', 'already_rolled');
  END IF;

  v_settings := public.competition_injury_settings_row();
  v_state := public.competition_injury_ensure_club_season(v_fixture.season_id, p_club);

  IF v_state.count_total >= v_settings.max_total THEN
    INSERT INTO public.competition_fixture_injury_roll (fixture_id, club_short_name, did_injure)
    VALUES (p_fixture_id, p_club, false);
    RETURN jsonb_build_object('ok', true, 'did_injure', false, 'reason', 'cap_total');
  END IF;

  v_chance := least(1.0, greatest(0.0, v_settings.base_match_chance * v_state.injury_risk));
  IF random() >= v_chance THEN
    INSERT INTO public.competition_fixture_injury_roll (fixture_id, club_short_name, did_injure)
    VALUES (p_fixture_id, p_club, false);
    RETURN jsonb_build_object('ok', true, 'did_injure', false, 'reason', 'no_hit');
  END IF;

  v_severity := public.competition_injury_pick_severity(v_state, v_settings);
  IF v_severity IS NULL THEN
    INSERT INTO public.competition_fixture_injury_roll (fixture_id, club_short_name, did_injure)
    VALUES (p_fixture_id, p_club, false);
    RETURN jsonb_build_object('ok', true, 'did_injure', false, 'reason', 'no_severity_left');
  END IF;

  SELECT * INTO v_cat
  FROM public.competition_injury_catalogue c
  WHERE c.active AND c.severity = v_severity
  ORDER BY random()
  LIMIT 1;

  IF NOT FOUND THEN
    INSERT INTO public.competition_fixture_injury_roll (fixture_id, club_short_name, did_injure)
    VALUES (p_fixture_id, p_club, false);
    RETURN jsonb_build_object('ok', true, 'did_injure', false, 'reason', 'empty_catalogue');
  END IF;

  v_fit_gk := public.competition_injury_fit_gk_count(p_club);

  SELECT m.player_id INTO v_player_id
  FROM public.competition_match_player_stats m
  JOIN public."Players" p ON p."Konami_ID"::text = m.player_id
  WHERE m.fixture_id = p_fixture_id
    AND m.club_short_name = p_club
    AND m.appeared = true
    AND NOT public.competition_injury_player_is_out(m.player_id)
    AND NOT (
      upper(coalesce(p."Position", '')) = 'GK'
      AND v_fit_gk <= 1
    )
  ORDER BY random()
  LIMIT 1;

  IF v_player_id IS NULL THEN
    INSERT INTO public.competition_fixture_injury_roll (fixture_id, club_short_name, did_injure)
    VALUES (p_fixture_id, p_club, false);
    RETURN jsonb_build_object('ok', true, 'did_injure', false, 'reason', 'no_eligible_player');
  END IF;

  v_injury_id := public.competition_injury_issue(
    v_fixture.season_id, p_club, v_player_id, v_cat.id, p_fixture_id
  );

  INSERT INTO public.competition_fixture_injury_roll (
    fixture_id, club_short_name, did_injure, injury_id
  ) VALUES (p_fixture_id, p_club, true, v_injury_id);

  RETURN jsonb_build_object(
    'ok', true,
    'did_injure', true,
    'injury_id', v_injury_id,
    'player_id', v_player_id,
    'injury', v_cat.name,
    'severity', v_cat.severity
  );
END;
$function$;

CREATE OR REPLACE FUNCTION public.competition_injury_roll_for_fixture(p_fixture_id bigint)
RETURNS jsonb
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_fixture public.competition_fixtures%rowtype;
  v_home jsonb;
  v_away jsonb;
BEGIN
  SELECT * INTO v_fixture FROM public.competition_fixtures WHERE id = p_fixture_id;
  IF NOT FOUND THEN
    RETURN jsonb_build_object('ok', false);
  END IF;

  v_home := public.competition_injury_roll_for_club(p_fixture_id, v_fixture.home_club_short_name);
  v_away := public.competition_injury_roll_for_club(p_fixture_id, v_fixture.away_club_short_name);

  RETURN jsonb_build_object('ok', true, 'home', v_home, 'away', v_away);
END;
$function$;

-- Serve injury fixture absences + decrement remaining
CREATE OR REPLACE FUNCTION public.competition_serve_injuries_for_fixture(
  p_fixture_id bigint
)
RETURNS void
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_row record;
BEGIN
  FOR v_row IN
    SELECT iff.id, iff.injury_id, iff.phase
    FROM public.competition_player_injury_fixtures iff
    WHERE iff.fixture_id = p_fixture_id
      AND iff.served = false
  LOOP
    UPDATE public.competition_player_injury_fixtures
    SET served = true
    WHERE id = v_row.id;

    IF v_row.phase = 'out' THEN
      UPDATE public.competition_player_injuries
      SET matches_out_remaining = greatest(coalesce(matches_out_remaining, 1) - 1, 0)
      WHERE id = v_row.injury_id;
    ELSE
      UPDATE public.competition_player_injuries
      SET recovery_remaining = greatest(coalesce(recovery_remaining, 1) - 1, 0)
      WHERE id = v_row.injury_id;
    END IF;
  END LOOP;

  UPDATE public.competition_player_injuries i
  SET status = 'recovered',
      recovered_at = coalesce(i.recovered_at, now())
  WHERE i.status = 'active'
    AND coalesce(i.matches_out_remaining, 0) <= 0
    AND coalesce(i.recovery_remaining, 0) <= 0
    AND EXISTS (
      SELECT 1 FROM public.competition_player_injury_fixtures iff
      WHERE iff.injury_id = i.id
    )
    AND NOT EXISTS (
      SELECT 1 FROM public.competition_player_injury_fixtures iff
      WHERE iff.injury_id = i.id AND iff.served = false
    );

  -- Roll new injuries for this fixture (idempotent per club)
  PERFORM public.competition_injury_roll_for_fixture(p_fixture_id);
END;
$function$;

-- Pre-season virtual match ticks
CREATE OR REPLACE FUNCTION public.competition_injury_tick_preseason_month(
  p_season_id bigint,
  p_gpsl_month text
)
RETURNS jsonb
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_settings public.competition_injury_settings;
  v_month text := lower(btrim(p_gpsl_month));
  v_ticks int;
  v_inj record;
  v_left int;
  v_updated int := 0;
BEGIN
  IF NOT public.is_gpsl_admin()
     AND current_user NOT IN ('postgres', 'supabase_admin', 'service_role') THEN
    RAISE EXCEPTION 'Admin only';
  END IF;

  v_settings := public.competition_injury_settings_row();
  IF NOT public.competition_injury_is_preseason_month(v_month) THEN
    RAISE EXCEPTION 'Month % is not configured as a pre-season month', v_month;
  END IF;

  IF EXISTS (
    SELECT 1 FROM public.competition_injury_preseason_tick t
    WHERE t.season_id = p_season_id AND t.gpsl_month = v_month
  ) THEN
    RETURN jsonb_build_object('ok', true, 'already_applied', true, 'month', v_month);
  END IF;

  v_ticks := greatest(v_settings.preseason_matches_per_month, 0);

  FOR v_inj IN
    SELECT *
    FROM public.competition_player_injuries i
    WHERE i.status = 'active'
      AND (
        i.season_id = p_season_id
        OR i.incurred_season_id = p_season_id
        OR EXISTS (
          SELECT 1 FROM public.competition_club_injury_season cis
          WHERE cis.season_id = p_season_id
            AND cis.club_short_name = i.club_short_name
        )
      )
  LOOP
    v_left := v_ticks;

    WHILE v_left > 0 AND coalesce(v_inj.matches_out_remaining, 0) > 0 LOOP
      v_inj.matches_out_remaining := v_inj.matches_out_remaining - 1;
      v_left := v_left - 1;
    END LOOP;

    WHILE v_left > 0 AND coalesce(v_inj.recovery_remaining, 0) > 0 LOOP
      v_inj.recovery_remaining := v_inj.recovery_remaining - 1;
      v_left := v_left - 1;
    END LOOP;

    UPDATE public.competition_player_injuries
    SET matches_out_remaining = v_inj.matches_out_remaining,
        recovery_remaining = v_inj.recovery_remaining,
        status = CASE
          WHEN coalesce(v_inj.matches_out_remaining, 0) <= 0
           AND coalesce(v_inj.recovery_remaining, 0) <= 0
          THEN 'recovered'
          ELSE status
        END,
        recovered_at = CASE
          WHEN coalesce(v_inj.matches_out_remaining, 0) <= 0
           AND coalesce(v_inj.recovery_remaining, 0) <= 0
          THEN coalesce(recovered_at, now())
          ELSE recovered_at
        END
    WHERE id = v_inj.id;

    DELETE FROM public.competition_player_injury_fixtures
    WHERE injury_id = v_inj.id AND served = false;

    IF coalesce(v_inj.matches_out_remaining, 0) > 0
       OR coalesce(v_inj.recovery_remaining, 0) > 0 THEN
      PERFORM public.competition_injury_assign_fixtures(v_inj.id);
    END IF;

    v_updated := v_updated + 1;
  END LOOP;

  INSERT INTO public.competition_injury_preseason_tick (season_id, gpsl_month, ticks_applied)
  VALUES (p_season_id, v_month, v_ticks);

  RETURN jsonb_build_object(
    'ok', true,
    'month', v_month,
    'ticks', v_ticks,
    'injuries_updated', v_updated
  );
END;
$function$;

CREATE OR REPLACE FUNCTION public.competition_injury_init_season_risks(p_season_id bigint)
RETURNS jsonb
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_club text;
  v_n int := 0;
BEGIN
  IF NOT public.is_gpsl_admin()
     AND current_user NOT IN ('postgres', 'supabase_admin', 'service_role') THEN
    RAISE EXCEPTION 'Admin only';
  END IF;

  FOR v_club IN
    SELECT DISTINCT r.club_short_name
    FROM public.competition_club_seasons r
    WHERE r.season_id = p_season_id
  LOOP
    PERFORM public.competition_injury_ensure_club_season(p_season_id, v_club);
    v_n := v_n + 1;
  END LOOP;

  -- Also ensure any club with players
  IF v_n = 0 THEN
    FOR v_club IN
      SELECT DISTINCT p."Contracted_Team"
      FROM public."Players" p
      WHERE p."Contracted_Team" IS NOT NULL AND btrim(p."Contracted_Team") <> ''
    LOOP
      PERFORM public.competition_injury_ensure_club_season(p_season_id, v_club);
      v_n := v_n + 1;
    END LOOP;
  END IF;

  RETURN jsonb_build_object('ok', true, 'clubs', v_n);
END;
$function$;


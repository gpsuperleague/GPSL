-- =============================================================================
-- GPSL Injuries engine
-- Catalogue + season caps + per-club secret risk + post-match rolls +
-- out/recovery fixture absences + pre-season recovery ticks.
--
-- Rules (confirmed):
--   Roll once per club per played fixture (league + cup)
--   Caps: 1 major, 2 moderate, 4 minor, 4 total per club/season
--   Out + recovery both unavailable (Injured vs Gaining match fitness)
--   Anyone can be injured except sole remaining fit GK
--   Pre-season months (default may/june/july): no new injuries; 4 virtual
--     matches per month tick off remaining out then recovery
--   Club injury_risk is admin-secret only
--
-- Run after competition_fixture_unavailable.sql. Safe re-run.
-- =============================================================================

-- ---------------------------------------------------------------------------
-- Settings (singleton)
-- ---------------------------------------------------------------------------

CREATE TABLE IF NOT EXISTS public.competition_injury_settings (
  id smallint PRIMARY KEY DEFAULT 1 CHECK (id = 1),
  max_major int NOT NULL DEFAULT 1,
  max_moderate int NOT NULL DEFAULT 2,
  max_minor int NOT NULL DEFAULT 4,
  max_total int NOT NULL DEFAULT 4,
  base_match_chance numeric(6,4) NOT NULL DEFAULT 0.1500,
  weight_minor numeric(6,2) NOT NULL DEFAULT 70,
  weight_moderate numeric(6,2) NOT NULL DEFAULT 25,
  weight_major numeric(6,2) NOT NULL DEFAULT 5,
  preseason_months text[] NOT NULL DEFAULT ARRAY['may','june','july']::text[],
  preseason_matches_per_month int NOT NULL DEFAULT 4,
  risk_min numeric(6,3) NOT NULL DEFAULT 0.700,
  risk_max numeric(6,3) NOT NULL DEFAULT 1.300,
  updated_at timestamptz NOT NULL DEFAULT now()
);

INSERT INTO public.competition_injury_settings (id)
VALUES (1)
ON CONFLICT (id) DO NOTHING;

-- ---------------------------------------------------------------------------
-- Catalogue
-- ---------------------------------------------------------------------------

CREATE TABLE IF NOT EXISTS public.competition_injury_catalogue (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name text NOT NULL,
  severity text NOT NULL CHECK (severity IN ('Minor', 'Moderate', 'Major')),
  matches_out int NOT NULL CHECK (matches_out >= 0),
  recovery_matches int NOT NULL DEFAULT 0 CHECK (recovery_matches >= 0),
  active boolean NOT NULL DEFAULT true,
  CONSTRAINT competition_injury_catalogue_name_key UNIQUE (name)
);

-- ---------------------------------------------------------------------------
-- Extend injury rows
-- ---------------------------------------------------------------------------

ALTER TABLE public.competition_player_injuries
  ADD COLUMN IF NOT EXISTS catalogue_id bigint
    REFERENCES public.competition_injury_catalogue (id) ON DELETE SET NULL,
  ADD COLUMN IF NOT EXISTS severity text,
  ADD COLUMN IF NOT EXISTS matches_out int,
  ADD COLUMN IF NOT EXISTS recovery_matches int,
  ADD COLUMN IF NOT EXISTS matches_out_remaining int,
  ADD COLUMN IF NOT EXISTS recovery_remaining int,
  ADD COLUMN IF NOT EXISTS source_fixture_id bigint
    REFERENCES public.competition_fixtures (id) ON DELETE SET NULL,
  ADD COLUMN IF NOT EXISTS incurred_season_id bigint
    REFERENCES public.competition_seasons (id) ON DELETE SET NULL;

ALTER TABLE public.competition_player_injury_fixtures
  ADD COLUMN IF NOT EXISTS phase text NOT NULL DEFAULT 'out';

DO $$
BEGIN
  IF NOT EXISTS (
    SELECT 1 FROM pg_constraint
    WHERE conname = 'competition_player_injury_fixtures_phase_check'
  ) THEN
    ALTER TABLE public.competition_player_injury_fixtures
      ADD CONSTRAINT competition_player_injury_fixtures_phase_check
      CHECK (phase IN ('out', 'recovery'));
  END IF;
END $$;

-- ---------------------------------------------------------------------------
-- Per-club season state (secret risk + counters)
-- ---------------------------------------------------------------------------

CREATE TABLE IF NOT EXISTS public.competition_club_injury_season (
  season_id bigint NOT NULL REFERENCES public.competition_seasons (id) ON DELETE CASCADE,
  club_short_name text NOT NULL,
  injury_risk numeric(6,3) NOT NULL DEFAULT 1.000,
  count_major int NOT NULL DEFAULT 0,
  count_moderate int NOT NULL DEFAULT 0,
  count_minor int NOT NULL DEFAULT 0,
  count_total int NOT NULL DEFAULT 0,
  created_at timestamptz NOT NULL DEFAULT now(),
  PRIMARY KEY (season_id, club_short_name)
);

CREATE TABLE IF NOT EXISTS public.competition_fixture_injury_roll (
  fixture_id bigint NOT NULL REFERENCES public.competition_fixtures (id) ON DELETE CASCADE,
  club_short_name text NOT NULL,
  rolled_at timestamptz NOT NULL DEFAULT now(),
  did_injure boolean NOT NULL DEFAULT false,
  injury_id bigint REFERENCES public.competition_player_injuries (id) ON DELETE SET NULL,
  PRIMARY KEY (fixture_id, club_short_name)
);

CREATE TABLE IF NOT EXISTS public.competition_injury_preseason_tick (
  season_id bigint NOT NULL REFERENCES public.competition_seasons (id) ON DELETE CASCADE,
  gpsl_month text NOT NULL,
  ticks_applied int NOT NULL DEFAULT 0,
  applied_at timestamptz NOT NULL DEFAULT now(),
  PRIMARY KEY (season_id, gpsl_month)
);


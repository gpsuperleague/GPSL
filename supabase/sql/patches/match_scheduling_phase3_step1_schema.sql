-- Phase 3 step 1/3 — schema only (run first; wait for success before step 2)

ALTER TABLE public.competition_fixture_schedule
  ADD COLUMN IF NOT EXISTS mutual_override_used boolean NOT NULL DEFAULT false;

CREATE TABLE IF NOT EXISTS public.competition_fixture_mutual_override (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  fixture_id bigint NOT NULL REFERENCES public.competition_fixtures (id) ON DELETE CASCADE,
  requested_by_club text NOT NULL REFERENCES public."Clubs" ("ShortName"),
  kind text NOT NULL CHECK (kind IN ('play_now', 'new_time')),
  proposed_kickoff_at timestamptz NOT NULL,
  status text NOT NULL DEFAULT 'pending' CHECK (status IN ('pending', 'applied', 'cancelled')),
  home_confirmed_at timestamptz,
  away_confirmed_at timestamptz,
  created_at timestamptz NOT NULL DEFAULT now(),
  expires_at timestamptz NOT NULL DEFAULT (now() + interval '24 hours'),
  applied_at timestamptz
);

CREATE INDEX IF NOT EXISTS competition_fixture_mutual_override_fixture_idx
  ON public.competition_fixture_mutual_override (fixture_id, status);

CREATE UNIQUE INDEX IF NOT EXISTS competition_fixture_mutual_override_one_pending_idx
  ON public.competition_fixture_mutual_override (fixture_id)
  WHERE status = 'pending';

ALTER TABLE public.competition_fixture_mutual_override ENABLE ROW LEVEL SECURITY;

DROP POLICY IF EXISTS competition_fixture_mutual_override_select ON public.competition_fixture_mutual_override;
CREATE POLICY competition_fixture_mutual_override_select ON public.competition_fixture_mutual_override
  FOR SELECT TO authenticated
  USING (
    public.is_gpsl_admin()
    OR EXISTS (
      SELECT 1 FROM public.competition_fixtures f
      WHERE f.id = fixture_id
        AND public.my_club_shortname() IN (f.home_club_short_name, f.away_club_short_name)
    )
  );

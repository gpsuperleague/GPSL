DROP VIEW IF EXISTS public.competition_season_awards_public;

CREATE VIEW public.competition_season_awards_public
WITH (security_invoker = false)
AS
SELECT
  a.season_id,
  a.season_label,
  a.award_type,
  a.gpsl_month,
  a.player_id,
  p."Name" AS player_name,
  a.club_short_name,
  c."Club" AS club_name,
  a.stat_value,
  a.detail,
  a.awarded_at
FROM public.competition_season_award a
JOIN public."Players" p ON p."Konami_ID"::text = a.player_id
JOIN public."Clubs" c ON c."ShortName" = a.club_short_name;

GRANT SELECT ON public.competition_season_awards_public TO authenticated, anon;

DROP VIEW IF EXISTS public.competition_period_team_public;

CREATE VIEW public.competition_period_team_public
WITH (security_invoker = false)
AS
SELECT
  t.id AS team_id,
  t.season_id,
  t.season_label,
  t.gpsl_month,
  t.period_kind,
  t.division_scope,
  t.formation_id,
  t.computed_at,
  m.pitch_slot,
  m.slot_label,
  m.player_id,
  p."Name" AS player_name,
  m.club_short_name,
  c."Club" AS club_name,
  p."Position" AS player_position,
  m.selection_score,
  m.appearances,
  m.goals,
  m.assists,
  m.avg_rating,
  m.clean_sheets
FROM public.competition_period_team t
JOIN public.competition_period_team_member m ON m.team_id = t.id
JOIN public."Players" p ON p."Konami_ID"::text = m.player_id
JOIN public."Clubs" c ON c."ShortName" = m.club_short_name;

GRANT SELECT ON public.competition_period_team_public TO authenticated, anon;


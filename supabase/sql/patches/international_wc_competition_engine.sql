CREATE OR REPLACE FUNCTION public.international_assert_cycle_admin(p_cycle_id bigint)
RETURNS public.international_wc_cycles
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_cycle public.international_wc_cycles;
BEGIN
  IF NOT public.is_gpsl_admin() THEN
    RAISE EXCEPTION 'Admin only';
  END IF;

  SELECT * INTO v_cycle
  FROM public.international_wc_cycles
  WHERE id = p_cycle_id;

  IF NOT FOUND THEN
    RAISE EXCEPTION 'World Cup cycle % not found', p_cycle_id;
  END IF;

  RETURN v_cycle;
END;
$function$;

CREATE OR REPLACE FUNCTION public.international_shuffle_text_array(p_arr text[])
RETURNS text[]
LANGUAGE plpgsql
VOLATILE
AS $function$
DECLARE
  v_i int;
  v_j int;
  v_tmp text;
  v_arr text[] := p_arr;
BEGIN
  IF v_arr IS NULL OR array_length(v_arr, 1) IS NULL THEN
    RETURN v_arr;
  END IF;
  FOR v_i IN REVERSE array_length(v_arr, 1)..2 LOOP
    v_j := 1 + floor(random() * v_i)::int;
    v_tmp := v_arr[v_i];
    v_arr[v_i] := v_arr[v_j];
    v_arr[v_j] := v_tmp;
  END LOOP;
  RETURN v_arr;
END;
$function$;

-- Map qual match_no 1..10 onto GPSL months (spread through a season)
CREATE OR REPLACE FUNCTION public.international_qual_match_calendar(p_match_no integer)
RETURNS TABLE (gpsl_month text, week_in_month smallint)
LANGUAGE sql
IMMUTABLE
AS $$
  SELECT
    CASE
      WHEN p_match_no BETWEEN 1 AND 2 THEN 'august'
      WHEN p_match_no BETWEEN 3 AND 4 THEN 'october'
      WHEN p_match_no BETWEEN 5 AND 6 THEN 'november'
      WHEN p_match_no BETWEEN 7 AND 8 THEN 'february'
      WHEN p_match_no BETWEEN 9 AND 10 THEN 'april'
      ELSE 'may'
    END,
    CASE
      WHEN p_match_no IN (1, 3, 5, 7, 9) THEN 1::smallint
      ELSE 3::smallint
    END;
$$;


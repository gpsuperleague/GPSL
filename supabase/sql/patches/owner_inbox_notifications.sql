CREATE OR REPLACE FUNCTION public.owner_inbox_notify_all_clubs(
  p_message_type text,
  p_title text,
  p_body text,
  p_action_href text DEFAULT NULL,
  p_dedupe_key text DEFAULT NULL,
  p_season_id bigint DEFAULT NULL
)
RETURNS int
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_club record;
  v_count int := 0;
BEGIN
  FOR v_club IN
    SELECT c."ShortName" AS short_name
    FROM public."Clubs" c
    WHERE c.owner_id IS NOT NULL
    ORDER BY c."ShortName"
  LOOP
    IF public.owner_inbox_send(
      p_message_type, p_title, p_body, v_club.short_name, NULL,
      NULL, NULL, NULL, NULL, p_action_href,
      CASE WHEN p_dedupe_key IS NOT NULL THEN p_dedupe_key || ':' || v_club.short_name ELSE NULL END,
      NULL, p_season_id
    ) IS NOT NULL THEN
      v_count := v_count + 1;
    END IF;
  END LOOP;
  RETURN v_count;
END;
$function$;


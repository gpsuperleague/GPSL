CREATE OR REPLACE FUNCTION public.competition_admin_adjust_league_points(
  p_club_short_name text,
  p_points_delta smallint,
  p_reason text,
  p_season_id bigint DEFAULT NULL
)
RETURNS jsonb
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_season_id bigint;
  v_club text := upper(btrim(p_club_short_name));
  v_adj_id bigint;
BEGIN
  IF NOT public.is_gpsl_admin() THEN
    RAISE EXCEPTION 'Admin only';
  END IF;

  IF p_points_delta IS NULL OR p_points_delta = 0 THEN
    RAISE EXCEPTION 'points_delta must be non-zero';
  END IF;

  IF p_reason IS NULL OR btrim(p_reason) = '' THEN
    RAISE EXCEPTION 'reason is required';
  END IF;

  v_season_id := p_season_id;
  IF v_season_id IS NULL THEN
    SELECT id INTO v_season_id FROM public.competition_seasons WHERE is_current = true LIMIT 1;
  END IF;

  INSERT INTO public.competition_league_points_adjustments (
    season_id, club_short_name, points_delta, reason
  )
  VALUES (v_season_id, v_club, p_points_delta, btrim(p_reason))
  RETURNING id INTO v_adj_id;

  PERFORM public.owner_inbox_send(
    'points_deduction',
    CASE WHEN p_points_delta < 0 THEN 'League points deduction' ELSE 'League points adjustment' END,
    format(
      E'%s point(s) %s.\nReason: %s',
      abs(p_points_delta),
      CASE WHEN p_points_delta < 0 THEN 'deducted' ELSE 'added' END,
      btrim(p_reason)
    ),
    v_club,
    NULL,
    NULL, NULL, NULL, NULL,
    'progress.html',
    'points_adj:' || v_adj_id::text,
    NULL,
    v_season_id
  );

  RETURN jsonb_build_object('ok', true, 'adjustment_id', v_adj_id, 'club', v_club, 'delta', p_points_delta);
END;
$function$;


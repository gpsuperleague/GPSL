CREATE OR REPLACE FUNCTION public.natter_club_display_name(p_club_short text)
RETURNS text
LANGUAGE sql
STABLE
SECURITY DEFINER
SET search_path = public
AS $$
  SELECT coalesce(
    nullif(btrim(c."Club"), ''),
    p_club_short
  )
  FROM public."Clubs" c
  WHERE c."ShortName" = p_club_short
  LIMIT 1;
$$;

CREATE OR REPLACE FUNCTION public.natter_compose_open()
RETURNS boolean
LANGUAGE plpgsql
STABLE
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_season_id bigint;
  v_status text;
  v_month text;
BEGIN
  SELECT s.id, s.status
  INTO v_season_id, v_status
  FROM public.competition_seasons s
  WHERE s.is_current = true
  ORDER BY s.id DESC
  LIMIT 1;

  IF v_season_id IS NULL THEN
    RETURN false;
  END IF;

  -- Pre-season: allow a June/July style post keyed to a synthetic window when needed
  IF lower(coalesce(v_status, '')) = 'preseason' THEN
    RETURN true;
  END IF;

  v_month := public.competition_active_gpsl_month(v_season_id, now());
  RETURN v_month IS NOT NULL AND btrim(v_month) <> '';
END;
$function$;

CREATE OR REPLACE FUNCTION public.natter_active_month_context()
RETURNS jsonb
LANGUAGE plpgsql
STABLE
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_season_id bigint;
  v_status text;
  v_label text;
  v_month text;
BEGIN
  SELECT s.id, s.status, s.label
  INTO v_season_id, v_status, v_label
  FROM public.competition_seasons s
  WHERE s.is_current = true
  ORDER BY s.id DESC
  LIMIT 1;

  IF v_season_id IS NULL THEN
    RETURN jsonb_build_object('ok', false, 'reason', 'no_season');
  END IF;

  IF lower(coalesce(v_status, '')) = 'preseason' THEN
    v_month := 'july';
  ELSE
    v_month := public.competition_active_gpsl_month(v_season_id, now());
  END IF;

  RETURN jsonb_build_object(
    'ok', true,
    'season_id', v_season_id,
    'season_label', v_label,
    'season_status', v_status,
    'gpsl_month', v_month,
    'month_label', CASE WHEN v_month IS NULL THEN NULL ELSE public.competition_gpsl_month_label(v_month) END,
    'compose_open', public.natter_compose_open() AND v_month IS NOT NULL
  );
END;
$function$;


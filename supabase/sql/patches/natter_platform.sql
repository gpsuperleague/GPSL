-- =============================================================================
-- GPSL Natter — owner monthly social posts (X-like feed, original branding)
-- One post per club per GPSL month; 1000 chars; optional image (Storage).
-- Feeds GPSL Sport Club news for the FOLLOWING month (always one month behind).
-- Run after gpsl_sport_owner_comments.sql / gpsl_sport_inseason_rich_edition.sql
-- =============================================================================

CREATE TABLE IF NOT EXISTS public.natter_posts (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  season_id bigint NOT NULL REFERENCES public.competition_seasons (id) ON DELETE CASCADE,
  gpsl_month text NOT NULL,
  club_short_name text NOT NULL REFERENCES public."Clubs" ("ShortName") ON DELETE CASCADE,
  owner_id uuid NOT NULL,
  owner_tag text NOT NULL DEFAULT '',
  body text NOT NULL,
  image_path text,
  created_at timestamptz NOT NULL DEFAULT now(),
  CONSTRAINT natter_posts_month_check CHECK (
    gpsl_month IN (
      'august', 'september', 'october', 'november', 'december',
      'january', 'february', 'march', 'april', 'may', 'june', 'july'
    )
  ),
  CONSTRAINT natter_posts_body_len CHECK (char_length(body) >= 1 AND char_length(body) <= 1000),
  CONSTRAINT natter_posts_unique UNIQUE (season_id, gpsl_month, club_short_name)
);

CREATE INDEX IF NOT EXISTS natter_posts_season_month_idx
  ON public.natter_posts (season_id, gpsl_month, created_at DESC);

CREATE INDEX IF NOT EXISTS natter_posts_club_idx
  ON public.natter_posts (club_short_name, created_at DESC);

COMMENT ON TABLE public.natter_posts IS
  'Natter: one-and-done monthly owner posts. Appear in next GPSL Sport Club news.';

ALTER TABLE public.natter_posts ENABLE ROW LEVEL SECURITY;

DROP POLICY IF EXISTS natter_posts_select ON public.natter_posts;
CREATE POLICY natter_posts_select ON public.natter_posts
  FOR SELECT TO authenticated USING (true);


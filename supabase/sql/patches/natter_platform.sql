CREATE OR REPLACE FUNCTION public.natter_get_compose_state()
RETURNS jsonb
LANGUAGE plpgsql
STABLE
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_club text;
  v_club_name text;
  v_ctx jsonb;
  v_posted boolean := false;
  v_post jsonb;
BEGIN
  IF auth.uid() IS NULL THEN
    RETURN jsonb_build_object('ok', false, 'reason', 'not_authenticated');
  END IF;

  SELECT c."ShortName", c."Club"
  INTO v_club, v_club_name
  FROM public."Clubs" c
  WHERE c.owner_id = auth.uid()
  LIMIT 1;

  v_ctx := public.natter_active_month_context();

  IF v_club IS NOT NULL
    AND (v_ctx->>'ok')::boolean
    AND nullif(v_ctx->>'gpsl_month', '') IS NOT NULL THEN
    SELECT true,
           jsonb_build_object(
             'id', p.id,
             'body', p.body,
             'image_path', p.image_path,
             'created_at', p.created_at
           )
    INTO v_posted, v_post
    FROM public.natter_posts p
    WHERE p.season_id = (v_ctx->>'season_id')::bigint
      AND p.gpsl_month = v_ctx->>'gpsl_month'
      AND p.club_short_name = v_club
    LIMIT 1;
  END IF;

  RETURN jsonb_build_object(
    'ok', true,
    'club_short_name', v_club,
    'club_name', coalesce(v_club_name, v_club),
    'context', v_ctx,
    'already_posted', coalesce(v_posted, false),
    'my_post', v_post,
    'max_chars', 1000,
    'can_compose', (
      v_club IS NOT NULL
      AND coalesce((v_ctx->>'compose_open')::boolean, false)
      AND NOT coalesce(v_posted, false)
    )
  );
END;
$function$;

CREATE OR REPLACE FUNCTION public.natter_create_post(
  p_body text,
  p_image_path text DEFAULT NULL
)
RETURNS jsonb
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_uid uuid := auth.uid();
  v_club text;
  v_club_name text;
  v_tag text;
  v_ctx jsonb;
  v_month text;
  v_season_id bigint;
  v_body text;
  v_image text;
  v_id bigint;
BEGIN
  IF v_uid IS NULL THEN
    RETURN jsonb_build_object('ok', false, 'reason', 'not_authenticated');
  END IF;

  SELECT c."ShortName", c."Club",
         coalesce(
           nullif(btrim(c.owner), ''),
           nullif(btrim(public.owner_registry_resolve_tag(c.owner_id)), ''),
           ''
         )
  INTO v_club, v_club_name, v_tag
  FROM public."Clubs" c
  WHERE c.owner_id = v_uid
  LIMIT 1;

  IF v_club IS NULL THEN
    RETURN jsonb_build_object('ok', false, 'reason', 'no_club');
  END IF;

  v_ctx := public.natter_active_month_context();
  IF NOT coalesce((v_ctx->>'compose_open')::boolean, false) THEN
    RETURN jsonb_build_object('ok', false, 'reason', 'window_closed');
  END IF;

  v_month := nullif(v_ctx->>'gpsl_month', '');
  v_season_id := nullif(v_ctx->>'season_id', '')::bigint;
  IF v_month IS NULL OR v_season_id IS NULL THEN
    RETURN jsonb_build_object('ok', false, 'reason', 'no_active_month');
  END IF;

  IF EXISTS (
    SELECT 1 FROM public.natter_posts p
    WHERE p.season_id = v_season_id
      AND p.gpsl_month = v_month
      AND p.club_short_name = v_club
  ) THEN
    RETURN jsonb_build_object('ok', false, 'reason', 'already_posted');
  END IF;

  v_body := nullif(btrim(coalesce(p_body, '')), '');
  IF v_body IS NULL THEN
    RETURN jsonb_build_object('ok', false, 'reason', 'empty');
  END IF;
  IF char_length(v_body) > 1000 THEN
    RETURN jsonb_build_object('ok', false, 'reason', 'too_long', 'max_chars', 1000);
  END IF;

  v_image := nullif(btrim(coalesce(p_image_path, '')), '');
  IF v_image IS NOT NULL AND split_part(v_image, '/', 1) IS DISTINCT FROM v_club THEN
    RETURN jsonb_build_object('ok', false, 'reason', 'invalid_image_path');
  END IF;

  INSERT INTO public.natter_posts (
    season_id, gpsl_month, club_short_name, owner_id, owner_tag, body, image_path
  )
  VALUES (
    v_season_id, v_month, v_club, v_uid, coalesce(v_tag, ''), v_body, v_image
  )
  RETURNING id INTO v_id;

  RETURN jsonb_build_object(
    'ok', true,
    'id', v_id,
    'club_short_name', v_club,
    'club_name', coalesce(v_club_name, v_club),
    'gpsl_month', v_month,
    'month_label', public.competition_gpsl_month_label(v_month),
    'body', v_body,
    'image_path', v_image
  );
END;
$function$;

CREATE OR REPLACE FUNCTION public.natter_list_months(p_season_id bigint DEFAULT NULL)
RETURNS jsonb
LANGUAGE plpgsql
STABLE
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_season_id bigint := p_season_id;
BEGIN
  IF v_season_id IS NULL THEN
    SELECT id INTO v_season_id
    FROM public.competition_seasons
    WHERE is_current = true
    ORDER BY id DESC
    LIMIT 1;
  END IF;

  IF v_season_id IS NULL THEN
    RETURN jsonb_build_object('ok', false, 'reason', 'no_season');
  END IF;

  RETURN jsonb_build_object(
    'ok', true,
    'season_id', v_season_id,
    'months', coalesce((
      SELECT jsonb_agg(
        jsonb_build_object(
          'gpsl_month', q.gpsl_month,
          'month_label', public.competition_gpsl_month_label(q.gpsl_month),
          'post_count', q.post_count,
          'sort_key', public.competition_gpsl_month_sort(q.gpsl_month)
        )
        ORDER BY public.competition_gpsl_month_sort(q.gpsl_month) DESC NULLS LAST
      )
      FROM (
        SELECT p.gpsl_month, count(*)::int AS post_count
        FROM public.natter_posts p
        WHERE p.season_id = v_season_id
        GROUP BY p.gpsl_month
      ) q
    ), '[]'::jsonb)
  );
END;
$function$;

CREATE OR REPLACE FUNCTION public.natter_list_posts(
  p_season_id bigint DEFAULT NULL,
  p_gpsl_month text DEFAULT NULL,
  p_limit int DEFAULT 100
)
RETURNS jsonb
LANGUAGE plpgsql
STABLE
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_season_id bigint := p_season_id;
  v_month text := lower(nullif(btrim(coalesce(p_gpsl_month, '')), ''));
  v_lim int := greatest(1, least(coalesce(p_limit, 100), 200));
BEGIN
  IF v_season_id IS NULL THEN
    SELECT id INTO v_season_id
    FROM public.competition_seasons
    WHERE is_current = true
    ORDER BY id DESC
    LIMIT 1;
  END IF;

  IF v_season_id IS NULL THEN
    RETURN jsonb_build_object('ok', false, 'reason', 'no_season', 'posts', '[]'::jsonb);
  END IF;

  RETURN jsonb_build_object(
    'ok', true,
    'season_id', v_season_id,
    'gpsl_month', v_month,
    'posts', coalesce((
      SELECT jsonb_agg(row_data ORDER BY sort_at DESC)
      FROM (
        SELECT
          jsonb_build_object(
            'id', p.id,
            'season_id', p.season_id,
            'gpsl_month', p.gpsl_month,
            'month_label', public.competition_gpsl_month_label(p.gpsl_month),
            'club_short', p.club_short_name,
            'club_name', public.natter_club_display_name(p.club_short_name),
            'owner_tag', p.owner_tag,
            'body', p.body,
            'image_path', p.image_path,
            'created_at', p.created_at
          ) AS row_data,
          p.created_at AS sort_at
        FROM public.natter_posts p
        WHERE p.season_id = v_season_id
          AND (v_month IS NULL OR p.gpsl_month = v_month)
        ORDER BY p.created_at DESC
        LIMIT v_lim
      ) q
    ), '[]'::jsonb)
  );
END;
$function$;

-- GPSL Sport Club news: prefer Natter (prior month), fallback to legacy sport comments
CREATE OR REPLACE FUNCTION public.gpsl_sport_fetch_club_news(
  p_season_id bigint,
  p_edition_gpsl_month text
)
RETURNS jsonb
LANGUAGE plpgsql
STABLE
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_prev_month text;
  v_natter jsonb;
BEGIN
  v_prev_month := public.gpsl_sport_previous_gpsl_month(p_edition_gpsl_month);
  IF v_prev_month IS NULL OR p_season_id IS NULL THEN
    RETURN '[]'::jsonb;
  END IF;

  SELECT coalesce(jsonb_agg(row_data ORDER BY row_data->>'club_name'), '[]'::jsonb)
  INTO v_natter
  FROM (
    SELECT jsonb_build_object(
      'club_short', p.club_short_name,
      'club_name', public.natter_club_display_name(p.club_short_name),
      'owner', coalesce(nullif(btrim(p.owner_tag), ''), 'Club owner'),
      'comment', p.body,
      'image_path', p.image_path,
      'source', 'natter',
      'source_gpsl_month', p.gpsl_month,
      'source_month_label', public.competition_gpsl_month_label(p.gpsl_month)
    ) AS row_data
    FROM public.natter_posts p
    WHERE p.season_id = p_season_id
      AND p.gpsl_month = v_prev_month
  ) q;

  IF jsonb_array_length(v_natter) > 0 THEN
    RETURN v_natter;
  END IF;

  -- Legacy fallback (Club Details drafts captured at month lock)
  RETURN coalesce((
    SELECT jsonb_agg(row_data ORDER BY row_data->>'club_name')
    FROM (
      SELECT jsonb_build_object(
        'club_short', oc.club_short_name,
        'club_name', public.natter_club_display_name(oc.club_short_name),
        'owner', oc.owner_tag,
        'comment', oc.comment_text,
        'source', 'legacy_sport_comment',
        'source_gpsl_month', oc.source_gpsl_month,
        'source_month_label', public.competition_gpsl_month_label(oc.source_gpsl_month)
      ) AS row_data
      FROM public.gpsl_sport_owner_comments oc
      WHERE oc.season_id = p_season_id
        AND oc.source_gpsl_month = v_prev_month
    ) q
  ), '[]'::jsonb);
END;
$function$;


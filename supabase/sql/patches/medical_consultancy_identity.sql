CREATE OR REPLACE FUNCTION public.medical_create_named_consult(
  p_club text,
  p_matches int,
  p_source text DEFAULT NULL,
  p_inventory_id bigint DEFAULT NULL,
  p_identity jsonb DEFAULT NULL
)
RETURNS bigint
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_club text := nullif(btrim(p_club), '');
  v_id bigint;
  v_ident jsonb;
  v_matches int := coalesce(p_matches, 2);
BEGIN
  IF v_club IS NULL THEN RAISE EXCEPTION 'Club required'; END IF;
  IF v_matches NOT IN (2, 4, 6, 8, 10) THEN
    RAISE EXCEPTION 'Invalid consult strength %', v_matches;
  END IF;

  IF p_identity IS NOT NULL
     AND coalesce(nullif(p_identity->>'label', ''), '') <> '' THEN
    v_ident := p_identity;
  ELSE
    v_ident := public.medical_random_consultancy_identity();
  END IF;

  INSERT INTO public.club_medical_consults (
    club_short_name, matches_removed, status,
    group_name, consultant_name, label,
    inventory_id, source
  )
  VALUES (
    v_club, v_matches, 'available',
    coalesce(v_ident->>'group_name', 'Harley Medical Group'),
    coalesce(v_ident->>'consultant_name', 'Helen Roberts'),
    coalesce(
      v_ident->>'label',
      (v_ident->>'group_name') || ' - Specialist Consultant ' || (v_ident->>'consultant_name')
    ),
    p_inventory_id,
    p_source
  )
  RETURNING id INTO v_id;

  RETURN v_id;
END;
$function$;

-- Stamp prize medical tokens with consultancy identity + linked consult row
CREATE OR REPLACE FUNCTION public.prize_grant_inventory_item(
  p_club text,
  p_prize_type text,
  p_param_int int,
  p_source text DEFAULT NULL,
  p_season_id bigint DEFAULT NULL,
  p_window_phase text DEFAULT NULL,
  p_metadata jsonb DEFAULT '{}'::jsonb
)
RETURNS bigint
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_id bigint;
  v_club text := btrim(p_club);
  v_meta jsonb := coalesce(p_metadata, '{}'::jsonb);
  v_ident jsonb;
  v_consult_id bigint;
BEGIN
  IF v_club IS NULL OR v_club = '' THEN
    RAISE EXCEPTION 'Club required';
  END IF;

  IF p_prize_type NOT IN ('medical_token', 'fee_discount', 'appeal_card', 'draft_token') THEN
    RAISE EXCEPTION 'Invalid prize type %', p_prize_type;
  END IF;

  IF p_prize_type = 'medical_token' THEN
    v_ident := public.medical_random_consultancy_identity();
    v_meta := v_meta || jsonb_build_object(
      'matches_removed', p_param_int,
      'group_name', v_ident->>'group_name',
      'consultant_name', v_ident->>'consultant_name',
      'label', v_ident->>'label',
      'consultancy_label', v_ident->>'label'
    );
  END IF;

  INSERT INTO public.club_prize_inventory (
    club_short_name, prize_type, param_int, source, season_id, window_phase, metadata
  )
  VALUES (
    v_club,
    p_prize_type,
    CASE
      WHEN p_prize_type IN ('appeal_card', 'draft_token') THEN NULL
      ELSE p_param_int
    END,
    p_source,
    p_season_id,
    p_window_phase,
    v_meta
  )
  RETURNING id INTO v_id;

  IF p_prize_type = 'medical_token' THEN
    v_consult_id := public.medical_create_named_consult(
      v_club, p_param_int, coalesce(p_source, 'prize'), v_id, v_ident
    );
    UPDATE public.club_prize_inventory
    SET metadata = coalesce(metadata, '{}'::jsonb) || jsonb_build_object('consult_id', v_consult_id)
    WHERE id = v_id;
  END IF;

  RETURN v_id;
END;
$function$;

GRANT EXECUTE ON FUNCTION public.prize_grant_inventory_item(text, text, int, text, bigint, text, jsonb) TO authenticated;

-- Grant vault consults as named rows (and keep specialist_tokens count in sync)
CREATE OR REPLACE FUNCTION public.medical_grant_specialist_tokens(
  p_club text,
  p_tokens int DEFAULT 1
)
RETURNS jsonb
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_club text := nullif(btrim(p_club), '');
  v_n int := greatest(coalesce(p_tokens, 0), 0);
  v_row public.club_medical_centre;
  v_i int;
  v_ids bigint[] := ARRAY[]::bigint[];
  v_id bigint;
  v_room int;
BEGIN
  IF NOT public.is_gpsl_admin()
     AND current_user NOT IN ('postgres', 'supabase_admin', 'service_role') THEN
    RAISE EXCEPTION 'Admin only';
  END IF;
  IF v_club IS NULL THEN RAISE EXCEPTION 'Club required'; END IF;
  IF v_n <= 0 THEN RAISE EXCEPTION 'Token count must be > 0'; END IF;

  v_row := public.medical_ensure_centre(v_club);
  v_room := greatest(0, 20 - coalesce(v_row.specialist_tokens, 0));
  v_n := least(v_n, v_room);
  IF v_n <= 0 THEN
    RETURN jsonb_build_object(
      'ok', true,
      'club_short_name', v_club,
      'specialist_tokens', v_row.specialist_tokens,
      'granted', 0,
      'max_specialist_tokens', 20,
      'consult_ids', '[]'::jsonb
    );
  END IF;

  FOR v_i IN 1..v_n LOOP
    v_id := public.medical_create_named_consult(v_club, 2, 'specialist_vault', NULL);
    v_ids := v_ids || v_id;
  END LOOP;

  UPDATE public.club_medical_centre
  SET specialist_tokens = least(20, specialist_tokens + v_n),
      updated_at = now()
  WHERE club_short_name = v_club
  RETURNING * INTO v_row;

  RETURN jsonb_build_object(
    'ok', true,
    'club_short_name', v_club,
    'specialist_tokens', v_row.specialist_tokens,
    'granted', v_n,
    'max_specialist_tokens', 20,
    'consult_ids', to_jsonb(v_ids)
  );
END;
$function$;

GRANT EXECUTE ON FUNCTION public.medical_grant_specialist_tokens(text, int) TO authenticated;

-- Backfill named vault consults for existing specialist_tokens without rows
CREATE OR REPLACE FUNCTION public.medical_sync_named_consults(p_club text DEFAULT NULL)
RETURNS int
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  r record;
  v_have int;
  v_need int;
  v_created int := 0;
  v_i int;
BEGIN
  FOR r IN
    SELECT c.club_short_name, c.specialist_tokens
    FROM public.club_medical_centre c
    WHERE p_club IS NULL OR c.club_short_name = upper(trim(p_club))
  LOOP
    SELECT count(*)::int INTO v_have
    FROM public.club_medical_consults x
    WHERE x.club_short_name = r.club_short_name
      AND x.status = 'available'
      AND x.inventory_id IS NULL;

    v_need := greatest(0, coalesce(r.specialist_tokens, 0) - v_have);
    FOR v_i IN 1..v_need LOOP
      PERFORM public.medical_create_named_consult(r.club_short_name, 2, 'backfill_vault', NULL);
      v_created := v_created + 1;
    END LOOP;
  END LOOP;
  RETURN v_created;
END;
$function$;

-- Unified available consults (prize + vault) with display labels
CREATE OR REPLACE FUNCTION public.medical_list_available_consults(p_club text DEFAULT NULL)
RETURNS jsonb
LANGUAGE plpgsql
VOLATILE
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_club text := coalesce(nullif(btrim(p_club), ''), public.my_club_shortname());
  v_out jsonb;
BEGIN
  IF v_club IS NULL THEN
    RETURN '[]'::jsonb;
  END IF;

  PERFORM public.medical_sync_named_consults(v_club);

  SELECT coalesce(jsonb_agg(row_to_json(x)::jsonb ORDER BY x.matches_removed DESC, x.id), '[]'::jsonb)
  INTO v_out
  FROM (
    SELECT
      c.id AS consult_id,
      c.matches_removed AS param_int,
      c.matches_removed,
      c.inventory_id,
      c.group_name,
      c.consultant_name,
      c.label,
      c.label AS consultancy_label,
      CASE WHEN c.inventory_id IS NULL THEN 'vault' ELSE 'prize' END AS kind,
      c.status
    FROM public.club_medical_consults c
    WHERE c.club_short_name = v_club
      AND c.status = 'available'
  ) x;

  RETURN coalesce(v_out, '[]'::jsonb);
END;
$function$;


-- Random identity: group + consultant person name
CREATE OR REPLACE FUNCTION public.medical_random_consultancy_identity()
RETURNS jsonb
LANGUAGE plpgsql
VOLATILE
SET search_path = public
AS $function$
DECLARE
  v_group text;
  v_person text;
  v_gender text;
BEGIN
  SELECT g.group_name INTO v_group
  FROM public.medical_consultancy_groups g
  WHERE g.is_active
  ORDER BY random()
  LIMIT 1;

  IF v_group IS NULL THEN
    v_group := 'Harley Medical Group';
  END IF;

  v_gender := CASE WHEN random() < 0.5 THEN 'female' ELSE 'male' END;

  IF to_regprocedure('public.medical_staff_random_name(text)') IS NOT NULL THEN
    v_person := public.medical_staff_random_name(v_gender);
  ELSE
    v_person := CASE v_gender
      WHEN 'female' THEN 'Helen Roberts'
      ELSE 'Tim Robins'
    END;
  END IF;

  RETURN jsonb_build_object(
    'group_name', v_group,
    'consultant_name', v_person,
    'label', v_group || ' - Specialist Consultant ' || v_person
  );
END;
$function$;

-- Named vault consults (each chip / −2 specialist token is a row)
CREATE TABLE IF NOT EXISTS public.club_medical_consults (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  club_short_name text NOT NULL,
  matches_removed int NOT NULL CHECK (matches_removed IN (2, 4, 6, 8, 10)),
  status text NOT NULL DEFAULT 'available'
    CHECK (status IN ('available', 'consumed')),
  group_name text NOT NULL,
  consultant_name text NOT NULL,
  label text NOT NULL,
  inventory_id bigint REFERENCES public.club_prize_inventory (id) ON DELETE SET NULL,
  source text,
  created_at timestamptz NOT NULL DEFAULT now(),
  consumed_at timestamptz
);

CREATE INDEX IF NOT EXISTS club_medical_consults_club_avail_idx
  ON public.club_medical_consults (club_short_name, status, matches_removed DESC, id);


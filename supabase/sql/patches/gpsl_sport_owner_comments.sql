-- =============================================================================
-- GPSL Sport — owner monthly club news comments
-- Owners draft up to 50 words in Club Details; on GPSL month lock the draft is
-- archived and cleared, then appears in the NEXT month's GPSL Sport edition.
-- Run after gpsl_sport_inseason_rich_edition.sql (then re-run rich edition patch
-- or this file — it replaces capture + process hooks).
-- =============================================================================

ALTER TABLE public."Clubs"
  ADD COLUMN IF NOT EXISTS sport_comment_draft text;

COMMENT ON COLUMN public."Clubs".sport_comment_draft IS
  'Owner draft for GPSL Sport club news (max 50 words). Archived and cleared when the GPSL month locks.';

CREATE TABLE IF NOT EXISTS public.gpsl_sport_owner_comments (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  season_id bigint NOT NULL REFERENCES public.competition_seasons (id) ON DELETE CASCADE,
  club_short_name text NOT NULL,
  source_gpsl_month text NOT NULL,
  comment_text text NOT NULL,
  owner_tag text NOT NULL DEFAULT '',
  captured_at timestamptz NOT NULL DEFAULT now(),
  CONSTRAINT gpsl_sport_owner_comments_unique
    UNIQUE (season_id, club_short_name, source_gpsl_month)
);

CREATE INDEX IF NOT EXISTS gpsl_sport_owner_comments_season_month_idx
  ON public.gpsl_sport_owner_comments (season_id, source_gpsl_month);

ALTER TABLE public.gpsl_sport_owner_comments ENABLE ROW LEVEL SECURITY;

COMMENT ON TABLE public.gpsl_sport_owner_comments IS
  'Archived owner club-news comments captured at GPSL month lock for the following Sport edition.';

-- ---------------------------------------------------------------------------
-- Helpers
-- ---------------------------------------------------------------------------

CREATE OR REPLACE FUNCTION public.club_sport_comment_word_count(p_text text)
RETURNS int
LANGUAGE sql
IMMUTABLE
AS $$
  SELECT CASE
    WHEN p_text IS NULL OR btrim(p_text) = '' THEN 0
    ELSE coalesce(
      array_length(
        regexp_split_to_array(btrim(regexp_replace(p_text, '\s+', ' ', 'g')), '\s'),
        1
      ),
      0
    )
  END;
$$;

CREATE OR REPLACE FUNCTION public.gpsl_sport_previous_gpsl_month(p_month text)
RETURNS text
LANGUAGE sql
IMMUTABLE
AS $$
  SELECT CASE public.competition_gpsl_month_sort(lower(btrim(p_month)))
    WHEN 2 THEN 'august'
    WHEN 3 THEN 'september'
    WHEN 4 THEN 'october'
    WHEN 5 THEN 'november'
    WHEN 6 THEN 'december'
    WHEN 7 THEN 'january'
    WHEN 8 THEN 'february'
    WHEN 9 THEN 'march'
    WHEN 10 THEN 'april'
    WHEN 1 THEN 'july'
    ELSE NULL
  END;
$$;

CREATE OR REPLACE FUNCTION public.gpsl_sport_fetch_club_news(
  p_season_id bigint,
  p_edition_gpsl_month text
)
RETURNS jsonb
LANGUAGE plpgsql
STABLE
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_prev_month text;
BEGIN
  v_prev_month := public.gpsl_sport_previous_gpsl_month(p_edition_gpsl_month);
  IF v_prev_month IS NULL OR p_season_id IS NULL THEN
    RETURN '[]'::jsonb;
  END IF;

  RETURN coalesce((
    SELECT jsonb_agg(row_data ORDER BY row_data->>'club_short')
    FROM (
      SELECT jsonb_build_object(
        'club_short', oc.club_short_name,
        'club_name', public.gpsl_sport_club_display_name(oc.club_short_name),
        'owner', oc.owner_tag,
        'comment', oc.comment_text,
        'source_gpsl_month', oc.source_gpsl_month,
        'source_month_label', public.competition_gpsl_month_label(oc.source_gpsl_month)
      ) AS row_data
      FROM public.gpsl_sport_owner_comments oc
      WHERE oc.season_id = p_season_id
        AND oc.source_gpsl_month = v_prev_month
    ) q
  ), '[]'::jsonb);
END;
$function$;


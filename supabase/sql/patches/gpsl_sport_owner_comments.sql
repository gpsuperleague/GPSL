CREATE OR REPLACE FUNCTION public.club_sport_comment_get()
RETURNS jsonb
LANGUAGE plpgsql
STABLE
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_short text;
  v_draft text;
  v_words int;
BEGIN
  SELECT c."ShortName", c.sport_comment_draft
  INTO v_short, v_draft
  FROM public."Clubs" c
  WHERE c.owner_id = auth.uid()
  LIMIT 1;

  IF v_short IS NULL THEN
    RETURN jsonb_build_object('ok', false, 'reason', 'no_club');
  END IF;

  v_draft := nullif(btrim(coalesce(v_draft, '')), '');
  v_words := public.club_sport_comment_word_count(v_draft);

  RETURN jsonb_build_object(
    'ok', true,
    'club_short_name', v_short,
    'draft', v_draft,
    'word_count', v_words,
    'max_words', 50,
    'has_draft', v_draft IS NOT NULL
  );
END;
$function$;

CREATE OR REPLACE FUNCTION public.club_sport_comment_set(p_comment text)
RETURNS jsonb
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_short text;
  v_clean text;
  v_words int;
BEGIN
  SELECT c."ShortName" INTO v_short
  FROM public."Clubs" c
  WHERE c.owner_id = auth.uid()
  LIMIT 1;

  IF v_short IS NULL THEN
    RETURN jsonb_build_object('ok', false, 'reason', 'no_club');
  END IF;

  v_clean := nullif(btrim(regexp_replace(coalesce(p_comment, ''), '\s+', ' ', 'g')), '');

  IF v_clean IS NOT NULL THEN
    v_words := public.club_sport_comment_word_count(v_clean);
    IF v_words > 50 THEN
      RETURN jsonb_build_object(
        'ok', false,
        'reason', 'too_long',
        'word_count', v_words,
        'max_words', 50
      );
    END IF;
    IF length(v_clean) > 500 THEN
      RETURN jsonb_build_object('ok', false, 'reason', 'too_long', 'max_chars', 500);
    END IF;
  END IF;

  UPDATE public."Clubs" c
  SET sport_comment_draft = v_clean
  WHERE c."ShortName" = v_short
    AND c.owner_id = auth.uid();

  RETURN jsonb_build_object(
    'ok', true,
    'club_short_name', v_short,
    'draft', v_clean,
    'word_count', coalesce(public.club_sport_comment_word_count(v_clean), 0),
    'max_words', 50,
    'has_draft', v_clean IS NOT NULL
  );
END;
$function$;

-- ---------------------------------------------------------------------------
-- Month lock: archive drafts for the month that just locked
-- ---------------------------------------------------------------------------

CREATE OR REPLACE FUNCTION public.gpsl_sport_capture_owner_comments(
  p_season_id bigint,
  p_gpsl_month text
)
RETURNS jsonb
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_month text := lower(btrim(p_gpsl_month));
  v_captured int := 0;
BEGIN
  IF p_season_id IS NULL OR v_month IS NULL OR v_month = '' THEN
    RETURN jsonb_build_object('ok', false, 'reason', 'invalid_args');
  END IF;

  WITH drafts AS (
    SELECT
      c."ShortName" AS club_short_name,
      btrim(c.sport_comment_draft) AS comment_text,
      coalesce(
        nullif(btrim(c.owner), ''),
        nullif(btrim(public.owner_registry_resolve_tag(c.owner_id)), ''),
        'Club owner'
      ) AS owner_tag
    FROM public."Clubs" c
    WHERE c.owner_id IS NOT NULL
      AND btrim(coalesce(c.sport_comment_draft, '')) <> ''
  ),
  inserted AS (
    INSERT INTO public.gpsl_sport_owner_comments (
      season_id,
      club_short_name,
      source_gpsl_month,
      comment_text,
      owner_tag
    )
    SELECT
      p_season_id,
      d.club_short_name,
      v_month,
      d.comment_text,
      d.owner_tag
    FROM drafts d
    ON CONFLICT (season_id, club_short_name, source_gpsl_month) DO NOTHING
    RETURNING club_short_name
  )
  SELECT count(*)::int INTO v_captured FROM inserted;

  UPDATE public."Clubs" c
  SET sport_comment_draft = NULL
  WHERE btrim(coalesce(c.sport_comment_draft, '')) <> '';

  RETURN jsonb_build_object(
    'ok', true,
    'gpsl_month', v_month,
    'captured', v_captured
  );
END;
$function$;

-- Capture before generating each pending Sport edition
CREATE OR REPLACE FUNCTION public.gpsl_sport_process_pending_editions(p_season_id bigint)
RETURNS jsonb
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_cal record;
  v_win record;
  v_job_key text;
  v_id bigint;
  v_results jsonb := '[]'::jsonb;
  v_capture jsonb;
BEGIN
  IF p_season_id IS NULL THEN
    RETURN jsonb_build_object('ok', false, 'reason', 'no_season');
  END IF;

  FOR v_cal IN
    SELECT c.gpsl_month
    FROM public.competition_season_calendar c
    WHERE c.season_id = p_season_id
      AND c.gpsl_month IS NOT NULL
      AND c.lock_at IS NOT NULL
      AND c.lock_at <= now()
    ORDER BY public.competition_gpsl_month_sort(c.gpsl_month)
  LOOP
    v_job_key := 'gpsl_sport:' || v_cal.gpsl_month;
    IF EXISTS (
      SELECT 1 FROM public.competition_season_calendar_jobs j
      WHERE j.season_id = p_season_id AND j.job_key = v_job_key
    ) THEN
      CONTINUE;
    END IF;

    IF to_regprocedure('public.gpsl_sport_capture_owner_comments(bigint,text)') IS NOT NULL THEN
      v_capture := public.gpsl_sport_capture_owner_comments(p_season_id, v_cal.gpsl_month);
    END IF;

    v_id := public.gpsl_sport_generate_edition(p_season_id, v_cal.gpsl_month);

    INSERT INTO public.competition_season_calendar_jobs (
      season_id, job_key, gpsl_month, result
    )
    VALUES (
      p_season_id, v_job_key, v_cal.gpsl_month,
      jsonb_build_object(
        'edition_id', v_id,
        'ok', v_id IS NOT NULL,
        'owner_comments', coalesce(v_capture, '{}'::jsonb)
      )
    )
    ON CONFLICT (season_id, job_key) DO UPDATE
      SET result = excluded.result, gpsl_month = excluded.gpsl_month, ran_at = now();

    v_results := v_results || jsonb_build_array(
      jsonb_build_object('gpsl_month', v_cal.gpsl_month, 'edition_id', v_id)
    );
  END LOOP;

  SELECT * INTO v_win FROM public.gpsl_sport_preseason_window(p_season_id);

  IF FOUND AND now() < v_win.august_start THEN
    IF coalesce(v_win.include_june, false) AND now() >= v_win.publish_june_at THEN
      v_job_key := 'gpsl_sport:june';
      IF NOT EXISTS (
        SELECT 1 FROM public.competition_season_calendar_jobs j
        WHERE j.season_id = p_season_id AND j.job_key = v_job_key
      ) THEN
        v_id := public.gpsl_sport_generate_edition(p_season_id, 'june');
        INSERT INTO public.competition_season_calendar_jobs (
          season_id, job_key, gpsl_month, result
        )
        VALUES (
          p_season_id, v_job_key, 'june',
          jsonb_build_object('edition_id', v_id, 'ok', v_id IS NOT NULL)
        )
        ON CONFLICT (season_id, job_key) DO UPDATE
          SET result = excluded.result, gpsl_month = excluded.gpsl_month, ran_at = now();
        v_results := v_results || jsonb_build_array(
          jsonb_build_object('gpsl_month', 'june', 'edition_id', v_id)
        );
      END IF;
    END IF;

    IF coalesce(v_win.include_july, false) AND now() >= v_win.publish_july_at THEN
      v_job_key := 'gpsl_sport:july';
      IF NOT EXISTS (
        SELECT 1 FROM public.competition_season_calendar_jobs j
        WHERE j.season_id = p_season_id AND j.job_key = v_job_key
      ) THEN
        v_id := public.gpsl_sport_generate_edition(p_season_id, 'july');
        INSERT INTO public.competition_season_calendar_jobs (
          season_id, job_key, gpsl_month, result
        )
        VALUES (
          p_season_id, v_job_key, 'july',
          jsonb_build_object('edition_id', v_id, 'ok', v_id IS NOT NULL)
        )
        ON CONFLICT (season_id, job_key) DO UPDATE
          SET result = excluded.result, gpsl_month = excluded.gpsl_month, ran_at = now();
        v_results := v_results || jsonb_build_array(
          jsonb_build_object('gpsl_month', 'july', 'edition_id', v_id)
        );
      END IF;
    END IF;
  END IF;

  RETURN jsonb_build_object('ok', true, 'processed', v_results);
END;
$function$;


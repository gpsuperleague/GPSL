CREATE OR REPLACE FUNCTION public.admin_waiting_list_set_absence(
  p_owner_email text,
  p_on_absence boolean,
  p_note text DEFAULT NULL
)
RETURNS jsonb
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_user_id uuid;
  v_status text;
BEGIN
  IF NOT public.is_gpsl_admin() THEN
    RAISE EXCEPTION 'Admin only';
  END IF;

  SELECT u.id INTO v_user_id
  FROM auth.users u
  WHERE lower(u.email) = lower(btrim(p_owner_email))
  LIMIT 1;

  IF v_user_id IS NULL THEN
    RAISE EXCEPTION 'No auth user with email %', p_owner_email;
  END IF;

  v_status := CASE WHEN p_on_absence THEN 'on_absence' ELSE 'member' END;

  UPDATE public.gpsl_owner_registry
  SET status = v_status,
      absence_note = CASE WHEN p_on_absence THEN nullif(btrim(p_note), '') ELSE NULL END,
      status_changed_at = now()
  WHERE owner_id = v_user_id
    AND (public.waiting_list_on_list_status(status) OR status IN ('member', 'on_absence'));

  IF NOT FOUND THEN
    RAISE EXCEPTION 'Owner is not on the waiting list';
  END IF;

  RETURN jsonb_build_object('ok', true, 'status', v_status);
END;
$function$;

CREATE OR REPLACE FUNCTION public.admin_waiting_list_invite_auction(
  p_owner_email text,
  p_starting_balance numeric DEFAULT NULL
)
RETURNS jsonb
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_user_id uuid;
  v_starting numeric;
BEGIN
  IF NOT public.is_gpsl_admin() THEN
    RAISE EXCEPTION 'Admin only';
  END IF;

  v_starting := greatest(coalesce(p_starting_balance, public.club_auction_default_starting_balance()), 0);

  SELECT u.id INTO v_user_id
  FROM auth.users u
  WHERE lower(u.email) = lower(btrim(p_owner_email))
  LIMIT 1;

  IF v_user_id IS NULL THEN
    RAISE EXCEPTION 'No auth user with email %', p_owner_email;
  END IF;

  IF EXISTS (SELECT 1 FROM public."Clubs" c WHERE c.owner_id = v_user_id) THEN
    RAISE EXCEPTION 'Owner already has a club';
  END IF;

  UPDATE public.gpsl_owner_registry
  SET status = 'awaiting_club_auction',
      pending_starting_balance = v_starting,
      waiting_list_tier = NULL,
      waiting_list_admin_sort = NULL,
      waiting_list_use_admin_sort = false,
      absence_note = NULL,
      status_changed_at = now()
  WHERE owner_id = v_user_id;

  IF NOT FOUND THEN
    INSERT INTO public.gpsl_owner_registry (
      owner_id, status, pending_starting_balance, status_changed_at
    )
    VALUES (v_user_id, 'awaiting_club_auction', v_starting, now());
  END IF;

  RETURN jsonb_build_object(
    'ok', true,
    'owner_id', v_user_id,
    'status', 'awaiting_club_auction',
    'pending_starting_balance', v_starting
  );
END;
$function$;


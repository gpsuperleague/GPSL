CREATE OR REPLACE FUNCTION public.waiting_list_ordered_rows(p_include_email boolean DEFAULT false)
RETURNS TABLE (
  owner_id uuid,
  position int,
  owner_tag text,
  registry_status text,
  waiting_list_tier text,
  account_created_at timestamptz,
  email text,
  last_club_short_name text,
  absence_note text,
  returned_to_list_at timestamptz,
  pending_starting_balance numeric
)
LANGUAGE plpgsql
STABLE
SECURITY DEFINER
SET search_path = public
AS $function$
BEGIN
  RETURN QUERY
  WITH base AS (
    SELECT
      r.owner_id,
      coalesce(nullif(btrim(public.owner_registry_resolve_tag(r.owner_id)), ''), '—') AS owner_tag,
      r.status AS registry_status,
      r.waiting_list_tier,
      u.created_at AS account_created_at,
      CASE WHEN p_include_email THEN u.email::text ELSE NULL END AS email,
      r.last_club_short_name,
      r.absence_note,
      r.returned_to_list_at,
      r.pending_starting_balance
    FROM public.gpsl_owner_registry r
    JOIN auth.users u ON u.id = r.owner_id
    WHERE public.waiting_list_on_list_status(r.status)
      AND NOT EXISTS (SELECT 1 FROM public."Clubs" c WHERE c.owner_id = r.owner_id)
  ),
  ranked AS (
    SELECT
      b.*,
      row_number() OVER (
        ORDER BY
          public.waiting_list_tier_rank(b.waiting_list_tier),
          CASE
            WHEN EXISTS (
              SELECT 1 FROM public.gpsl_owner_registry r2
              WHERE r2.owner_id = b.owner_id
                AND r2.waiting_list_use_admin_sort
                AND r2.waiting_list_admin_sort IS NOT NULL
            ) THEN (
              SELECT r2.waiting_list_admin_sort
              FROM public.gpsl_owner_registry r2
              WHERE r2.owner_id = b.owner_id
            )
          END NULLS LAST,
          b.account_created_at,
          b.owner_id
      )::int AS position
    FROM base b
  )
  SELECT
    ranked.owner_id,
    ranked.position,
    ranked.owner_tag,
    ranked.registry_status,
    ranked.waiting_list_tier,
    ranked.account_created_at,
    ranked.email,
    ranked.last_club_short_name,
    ranked.absence_note,
    ranked.returned_to_list_at,
    ranked.pending_starting_balance
  FROM ranked
  ORDER BY ranked.position;
END;
$function$;

CREATE OR REPLACE FUNCTION public.waiting_list_public()
RETURNS jsonb
LANGUAGE plpgsql
STABLE
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_rows jsonb;
  v_total int;
  v_self_pos int;
BEGIN
  SELECT coalesce(jsonb_agg(
    jsonb_build_object(
      'position', w.position,
      'owner_tag', w.owner_tag,
      'status', w.registry_status
    )
    ORDER BY w.position
  ), '[]'::jsonb)
  INTO v_rows
  FROM public.waiting_list_ordered_rows(false) w;

  SELECT count(*)::int INTO v_total FROM public.waiting_list_ordered_rows(false);

  SELECT w.position INTO v_self_pos
  FROM public.waiting_list_ordered_rows(false) w
  WHERE w.owner_id = auth.uid();

  RETURN jsonb_build_object(
    'total', coalesce(v_total, 0),
    'rows', v_rows,
    'my_position', v_self_pos
  );
END;
$function$;

CREATE OR REPLACE FUNCTION public.waiting_list_admin()
RETURNS jsonb
LANGUAGE plpgsql
STABLE
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_waiting jsonb;
  v_other jsonb;
BEGIN
  IF NOT public.is_gpsl_admin() THEN
    RAISE EXCEPTION 'Admin only';
  END IF;

  SELECT coalesce(jsonb_agg(
    jsonb_build_object(
      'position', w.position,
      'owner_id', w.owner_id,
      'email', w.email,
      'owner_tag', w.owner_tag,
      'status', w.registry_status,
      'tier', w.waiting_list_tier,
      'account_created_at', w.account_created_at,
      'last_club_short_name', w.last_club_short_name,
      'absence_note', w.absence_note,
      'returned_to_list_at', w.returned_to_list_at,
      'pending_starting_balance', w.pending_starting_balance,
      'use_admin_sort', (
        SELECT r.waiting_list_use_admin_sort
        FROM public.gpsl_owner_registry r
        WHERE r.owner_id = w.owner_id
      )
    )
    ORDER BY w.position
  ), '[]'::jsonb)
  INTO v_waiting
  FROM public.waiting_list_ordered_rows(true) w;

  SELECT coalesce(jsonb_agg(
    jsonb_build_object(
      'owner_id', r.owner_id,
      'email', u.email,
      'owner_tag', public.owner_registry_resolve_tag(r.owner_id),
      'status', r.status,
      'last_club_short_name', r.last_club_short_name
    )
    ORDER BY u.email
  ), '[]'::jsonb)
  INTO v_other
  FROM public.gpsl_owner_registry r
  JOIN auth.users u ON u.id = r.owner_id
  WHERE r.status = 'awaiting_club_auction'
    AND NOT EXISTS (SELECT 1 FROM public."Clubs" c WHERE c.owner_id = r.owner_id);

  RETURN jsonb_build_object(
    'waiting', v_waiting,
    'invited_to_auction', v_other
  );
END;
$function$;


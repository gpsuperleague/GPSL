CREATE OR REPLACE FUNCTION public.admin_waiting_list_add_member(p_owner_email text)
RETURNS jsonb
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_user_id uuid;
BEGIN
  IF NOT public.is_gpsl_admin() THEN
    RAISE EXCEPTION 'Admin only';
  END IF;

  SELECT u.id INTO v_user_id
  FROM auth.users u
  WHERE lower(u.email) = lower(btrim(p_owner_email))
  LIMIT 1;

  IF v_user_id IS NULL THEN
    RAISE EXCEPTION 'No auth user with email %', p_owner_email;
  END IF;

  IF EXISTS (SELECT 1 FROM public."Clubs" c WHERE c.owner_id = v_user_id) THEN
    RAISE EXCEPTION 'User already has a club';
  END IF;

  IF EXISTS (
    SELECT 1 FROM public.gpsl_owner_registry r
    WHERE r.owner_id = v_user_id AND r.status = 'archived'
  ) THEN
    RAISE EXCEPTION 'User is archived — unarchive first';
  END IF;

  PERFORM public.waiting_list_enqueue_new(v_user_id);

  RETURN jsonb_build_object(
    'ok', true,
    'owner_id', v_user_id,
    'email', p_owner_email,
    'status', 'member',
    'tier', 'new'
  );
END;
$function$;

CREATE OR REPLACE FUNCTION public.admin_waiting_list_move(
  p_owner_id uuid,
  p_direction integer
)
RETURNS jsonb
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_ids uuid[];
  v_idx int;
  v_swap int;
  v_tmp uuid;
BEGIN
  IF NOT public.is_gpsl_admin() THEN
    RAISE EXCEPTION 'Admin only';
  END IF;

  IF p_direction NOT IN (-1, 1) THEN
    RAISE EXCEPTION 'direction must be -1 or 1';
  END IF;

  SELECT array_agg(w.owner_id ORDER BY w.position)
  INTO v_ids
  FROM public.waiting_list_ordered_rows(false) w;

  IF v_ids IS NULL OR array_length(v_ids, 1) IS NULL THEN
    RAISE EXCEPTION 'Waiting list is empty';
  END IF;

  SELECT ord INTO v_idx
  FROM unnest(v_ids) WITH ORDINALITY AS t(owner_id, ord)
  WHERE owner_id = p_owner_id;

  IF v_idx IS NULL THEN
    RAISE EXCEPTION 'Owner is not on the waiting list';
  END IF;

  v_swap := v_idx + p_direction;
  IF v_swap < 1 OR v_swap > array_length(v_ids, 1) THEN
    RETURN jsonb_build_object('ok', true, 'moved', false, 'reason', 'already_at_edge');
  END IF;

  v_tmp := v_ids[v_idx];
  v_ids[v_idx] := v_ids[v_swap];
  v_ids[v_swap] := v_tmp;

  FOR v_idx IN 1..array_length(v_ids, 1)
  LOOP
    UPDATE public.gpsl_owner_registry
    SET waiting_list_admin_sort = v_idx * 1000,
        waiting_list_use_admin_sort = true
    WHERE owner_id = v_ids[v_idx];
  END LOOP;

  RETURN jsonb_build_object('ok', true, 'moved', true);
END;
$function$;


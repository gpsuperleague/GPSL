ALTER TABLE public.gpsl_owner_registry
  DROP CONSTRAINT IF EXISTS gpsl_owner_registry_status_check;

ALTER TABLE public.gpsl_owner_registry
  ADD CONSTRAINT gpsl_owner_registry_status_check
  CHECK (status IN (
    'active',
    'member',
    'awaiting_club_auction',
    'on_absence',
    'on_break',
    'archived'
  ));

ALTER TABLE public.gpsl_owner_registry
  ADD COLUMN IF NOT EXISTS waiting_list_tier text
    CHECK (waiting_list_tier IS NULL OR waiting_list_tier IN ('standard', 'returning', 'new')),
  ADD COLUMN IF NOT EXISTS waiting_list_admin_sort integer,
  ADD COLUMN IF NOT EXISTS waiting_list_use_admin_sort boolean NOT NULL DEFAULT false,
  ADD COLUMN IF NOT EXISTS returned_to_list_at timestamptz,
  ADD COLUMN IF NOT EXISTS absence_note text;

COMMENT ON COLUMN public.gpsl_owner_registry.waiting_list_tier IS
  'standard = on list before a returner left; returning = ex-owner back on list; new = fresh admin add (bottom tier).';
COMMENT ON COLUMN public.gpsl_owner_registry.waiting_list_admin_sort IS
  'Lower values sort earlier when waiting_list_use_admin_sort is true.';
COMMENT ON COLUMN public.gpsl_owner_registry.waiting_list_use_admin_sort IS
  'When false, order within tier uses auth.users.created_at.';

-- Migrate legacy on_break → member (returning tier)
UPDATE public.gpsl_owner_registry r
SET status = 'member',
    waiting_list_tier = coalesce(r.waiting_list_tier, 'returning'),
    returned_to_list_at = coalesce(r.returned_to_list_at, r.status_changed_at)
WHERE r.status = 'on_break'
  AND NOT EXISTS (SELECT 1 FROM public."Clubs" c WHERE c.owner_id = r.owner_id);

-- Temporary club caretaker (long leave) — separate from waiting list
CREATE TABLE IF NOT EXISTS public.gpsl_club_caretaker (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  club_short_name text NOT NULL REFERENCES public."Clubs" ("ShortName") ON DELETE CASCADE,
  primary_owner_id uuid NOT NULL REFERENCES auth.users (id) ON DELETE CASCADE,
  caretaker_owner_id uuid NOT NULL REFERENCES auth.users (id) ON DELETE CASCADE,
  starts_at timestamptz NOT NULL DEFAULT now(),
  ends_at timestamptz,
  note text,
  ended_at timestamptz,
  ended_by text,
  created_at timestamptz NOT NULL DEFAULT now(),
  CONSTRAINT gpsl_club_caretaker_distinct_owners CHECK (primary_owner_id <> caretaker_owner_id)
);

CREATE UNIQUE INDEX IF NOT EXISTS gpsl_club_caretaker_one_active_idx
  ON public.gpsl_club_caretaker (club_short_name)
  WHERE ended_at IS NULL;

CREATE INDEX IF NOT EXISTS gpsl_club_caretaker_primary_idx
  ON public.gpsl_club_caretaker (primary_owner_id)
  WHERE ended_at IS NULL;

ALTER TABLE public.gpsl_club_caretaker ENABLE ROW LEVEL SECURITY;

DROP POLICY IF EXISTS gpsl_club_caretaker_admin ON public.gpsl_club_caretaker;
CREATE POLICY gpsl_club_caretaker_admin ON public.gpsl_club_caretaker
  FOR ALL TO authenticated
  USING (public.is_gpsl_admin())
  WITH CHECK (public.is_gpsl_admin());

DROP POLICY IF EXISTS gpsl_club_caretaker_self_select ON public.gpsl_club_caretaker;
CREATE POLICY gpsl_club_caretaker_self_select ON public.gpsl_club_caretaker
  FOR SELECT TO authenticated
  USING (
    primary_owner_id = auth.uid()
    OR caretaker_owner_id = auth.uid()
    OR public.is_gpsl_admin()
  );


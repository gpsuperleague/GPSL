CREATE OR REPLACE FUNCTION public.admin_club_caretaker_start(
  p_club_short_name text,
  p_caretaker_email text,
  p_ends_at timestamptz DEFAULT NULL,
  p_note text DEFAULT NULL
)
RETURNS jsonb
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_club text := upper(btrim(p_club_short_name));
  v_caretaker uuid;
  v_primary uuid;
BEGIN
  IF NOT public.is_gpsl_admin() THEN
    RAISE EXCEPTION 'Admin only';
  END IF;

  SELECT c.owner_id INTO v_primary
  FROM public."Clubs" c
  WHERE c."ShortName" = v_club;

  IF v_primary IS NULL THEN
    RAISE EXCEPTION 'Club % not found or has no owner', v_club;
  END IF;

  SELECT u.id INTO v_caretaker
  FROM auth.users u
  WHERE lower(u.email) = lower(btrim(p_caretaker_email))
  LIMIT 1;

  IF v_caretaker IS NULL THEN
    RAISE EXCEPTION 'No auth user %', p_caretaker_email;
  END IF;

  UPDATE public.gpsl_club_caretaker
  SET ended_at = now(), ended_by = 'ADMIN_REPLACE'
  WHERE club_short_name = v_club AND ended_at IS NULL;

  INSERT INTO public.gpsl_club_caretaker (
    club_short_name, primary_owner_id, caretaker_owner_id, ends_at, note
  )
  VALUES (v_club, v_primary, v_caretaker, p_ends_at, nullif(btrim(p_note), ''));

  RETURN jsonb_build_object(
    'ok', true,
    'club_short_name', v_club,
    'primary_owner_id', v_primary,
    'caretaker_owner_id', v_caretaker,
    'ends_at', p_ends_at
  );
END;
$function$;

CREATE OR REPLACE FUNCTION public.admin_club_caretaker_end(p_club_short_name text)
RETURNS jsonb
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_club text := upper(btrim(p_club_short_name));
BEGIN
  IF NOT public.is_gpsl_admin() THEN
    RAISE EXCEPTION 'Admin only';
  END IF;

  UPDATE public.gpsl_club_caretaker
  SET ended_at = now(), ended_by = 'ADMIN'

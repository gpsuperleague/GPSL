CREATE OR REPLACE FUNCTION public.waiting_list_tier_rank(p_tier text)
RETURNS integer
LANGUAGE sql
IMMUTABLE
AS $$
  SELECT CASE coalesce(p_tier, 'standard')
    WHEN 'standard' THEN 1
    WHEN 'returning' THEN 2
    WHEN 'new' THEN 3
    ELSE 4
  END;
$$;

CREATE OR REPLACE FUNCTION public.waiting_list_on_list_status(p_status text)
RETURNS boolean
LANGUAGE sql
IMMUTABLE
AS $$
  SELECT coalesce(p_status, '') IN ('member', 'on_absence');
$$;

CREATE OR REPLACE FUNCTION public.waiting_list_account_created_at(p_owner_id uuid)
RETURNS timestamptz
LANGUAGE sql
STABLE
SECURITY DEFINER
SET search_path = public
AS $$
  SELECT u.created_at
  FROM auth.users u
  WHERE u.id = p_owner_id;
$$;

CREATE OR REPLACE FUNCTION public.waiting_list_clear_sort_flags()
RETURNS void
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $function$
BEGIN
  IF NOT public.is_gpsl_admin() THEN
    RAISE EXCEPTION 'Admin only';
  END IF;

  UPDATE public.gpsl_owner_registry r
  SET waiting_list_admin_sort = NULL,
      waiting_list_use_admin_sort = false
  WHERE public.waiting_list_on_list_status(r.status);
END;
$function$;

CREATE OR REPLACE FUNCTION public.waiting_list_rebuild_admin_sort()
RETURNS void
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_row record;
  v_sort int := 0;
BEGIN
  IF NOT public.is_gpsl_admin() THEN
    RAISE EXCEPTION 'Admin only';
  END IF;

  FOR v_row IN
    SELECT r.owner_id
    FROM public.gpsl_owner_registry r
    JOIN auth.users u ON u.id = r.owner_id
    WHERE public.waiting_list_on_list_status(r.status)
      AND NOT EXISTS (SELECT 1 FROM public."Clubs" c WHERE c.owner_id = r.owner_id)
    ORDER BY
      public.waiting_list_tier_rank(r.waiting_list_tier),
      CASE WHEN r.waiting_list_use_admin_sort AND r.waiting_list_admin_sort IS NOT NULL
        THEN r.waiting_list_admin_sort END NULLS LAST,
      u.created_at,
      r.owner_id
  LOOP
    v_sort := v_sort + 1000;
    UPDATE public.gpsl_owner_registry
    SET waiting_list_admin_sort = v_sort,
        waiting_list_use_admin_sort = true
    WHERE owner_id = v_row.owner_id;
  END LOOP;
END;
$function$;

CREATE OR REPLACE FUNCTION public.waiting_list_enqueue_new(p_owner_id uuid)
RETURNS void
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $function$
BEGIN
  UPDATE public.gpsl_owner_registry
  SET status = 'member',
      waiting_list_tier = 'new',
      waiting_list_admin_sort = NULL,
      waiting_list_use_admin_sort = false,
      returned_to_list_at = NULL,
      absence_note = NULL,
      status_changed_at = now()
  WHERE owner_id = p_owner_id;

  IF NOT FOUND THEN
    INSERT INTO public.gpsl_owner_registry (
      owner_id, status, waiting_list_tier, status_changed_at
    )
    VALUES (p_owner_id, 'member', 'new', now());
  END IF;
END;
$function$;

CREATE OR REPLACE FUNCTION public.waiting_list_enqueue_returning(p_owner_id uuid)
RETURNS void
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $function$
BEGIN
  -- Everyone already waiting becomes "standard" (ahead of returners, above fresh joins).
  UPDATE public.gpsl_owner_registry
  SET waiting_list_tier = 'standard'
  WHERE public.waiting_list_on_list_status(status)
    AND waiting_list_tier = 'new'
    AND owner_id <> p_owner_id;

  UPDATE public.gpsl_owner_registry
  SET status = 'member',
      waiting_list_tier = 'returning',
      waiting_list_admin_sort = NULL,
      waiting_list_use_admin_sort = false,
      returned_to_list_at = now(),
      absence_note = NULL,
      status_changed_at = now()
  WHERE owner_id = p_owner_id;

  IF NOT FOUND THEN
    INSERT INTO public.gpsl_owner_registry (
      owner_id, status, waiting_list_tier, returned_to_list_at, status_changed_at
    )
    VALUES (p_owner_id, 'member', 'returning', now(), now());
  END IF;
END;
$function$;


-- =============================================================================
-- Natter reactions — club owners can react with a fixed emoji set
-- Up to 3 distinct emojis per owner per post (tap to toggle).
-- =============================================================================

CREATE TABLE IF NOT EXISTS public.natter_reactions (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  post_id bigint NOT NULL REFERENCES public.natter_posts (id) ON DELETE CASCADE,
  owner_id uuid NOT NULL,
  emoji text NOT NULL,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now(),
  CONSTRAINT natter_reactions_emoji_check CHECK (
    emoji IN ('👍', '😂', '🔥', '⚽', '👏', '❤️')
  ),
  CONSTRAINT natter_reactions_unique UNIQUE (post_id, owner_id, emoji)
);

-- Adjust constraint for older installs (was (post_id, owner_id) previously).
ALTER TABLE public.natter_reactions DROP CONSTRAINT IF EXISTS natter_reactions_unique;
ALTER TABLE public.natter_reactions ADD CONSTRAINT natter_reactions_unique UNIQUE (post_id, owner_id, emoji);

CREATE INDEX IF NOT EXISTS natter_reactions_post_idx
  ON public.natter_reactions (post_id);

COMMENT ON TABLE public.natter_reactions IS
  'Natter: up to 3 emoji reactions per club owner per post.';

ALTER TABLE public.natter_reactions ENABLE ROW LEVEL SECURITY;

DROP POLICY IF EXISTS natter_reactions_select ON public.natter_reactions;
CREATE POLICY natter_reactions_select ON public.natter_reactions
  FOR SELECT TO authenticated USING (true);

-- Summary counts for the feed
CREATE OR REPLACE FUNCTION public.natter_reaction_counts(p_post_id bigint)
RETURNS jsonb
LANGUAGE sql
STABLE
SECURITY DEFINER
SET search_path = public
AS $$
  SELECT coalesce(jsonb_agg(
    jsonb_build_object('emoji', q.emoji, 'count', q.cnt)
    ORDER BY q.cnt DESC, q.emoji
  ), '[]'::jsonb)
  FROM (
    SELECT r.emoji, count(*)::int AS cnt
    FROM public.natter_reactions r
    WHERE r.post_id = p_post_id
    GROUP BY r.emoji
  ) q;
$$;

-- Toggle a specific emoji reaction (enforces max 3 distinct emojis per owner/post)
CREATE OR REPLACE FUNCTION public.natter_react(
  p_post_id bigint,
  p_emoji text
)
RETURNS jsonb
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_uid uuid := auth.uid();
  v_emoji text := nullif(btrim(coalesce(p_emoji, '')), '');
  v_exists boolean := false;
  v_count int;
  v_club text;
  v_my_reactions jsonb;
BEGIN
  IF v_uid IS NULL THEN
    RETURN jsonb_build_object('ok', false, 'reason', 'not_authenticated');
  END IF;

  IF p_post_id IS NULL THEN
    RETURN jsonb_build_object('ok', false, 'reason', 'bad_post');
  END IF;

  IF NOT EXISTS (SELECT 1 FROM public.natter_posts p WHERE p.id = p_post_id) THEN
    RETURN jsonb_build_object('ok', false, 'reason', 'not_found');
  END IF;

  SELECT c."ShortName"
  INTO v_club
  FROM public."Clubs" c
  WHERE c.owner_id = v_uid
  LIMIT 1;

  IF v_club IS NULL THEN
    RETURN jsonb_build_object('ok', false, 'reason', 'no_club');
  END IF;

  IF v_emoji IS NULL OR v_emoji NOT IN ('👍', '😂', '🔥', '⚽', '👏', '❤️') THEN
    RETURN jsonb_build_object('ok', false, 'reason', 'bad_emoji');
  END IF;

  SELECT EXISTS (
    SELECT 1
    FROM public.natter_reactions r
    WHERE r.post_id = p_post_id
      AND r.owner_id = v_uid
      AND r.emoji = v_emoji
  )
  INTO v_exists;

  IF v_exists THEN
    DELETE FROM public.natter_reactions
    WHERE post_id = p_post_id
      AND owner_id = v_uid
      AND emoji = v_emoji;
  ELSE
    SELECT count(*)::int
    INTO v_count
    FROM public.natter_reactions r
    WHERE r.post_id = p_post_id
      AND r.owner_id = v_uid;

    IF v_count >= 3 THEN
      RETURN jsonb_build_object(
        'ok', false,
        'reason', 'limit_reached',
        'max', 3
      );
    END IF;

    INSERT INTO public.natter_reactions (post_id, owner_id, emoji)
    VALUES (p_post_id, v_uid, v_emoji);
  END IF;

  SELECT coalesce(jsonb_agg(r.emoji ORDER BY r.emoji), '[]'::jsonb)
  INTO v_my_reactions
  FROM public.natter_reactions r
  WHERE r.post_id = p_post_id
    AND r.owner_id = v_uid;

  RETURN jsonb_build_object(
    'ok', true,
    'post_id', p_post_id,
    'my_reactions', v_my_reactions,
    'reactions', public.natter_reaction_counts(p_post_id)
  );
END;
$function$;

-- Enrich list posts with reaction summary + caller's reaction set
CREATE OR REPLACE FUNCTION public.natter_list_posts(
  p_season_id bigint DEFAULT NULL,
  p_gpsl_month text DEFAULT NULL,
  p_limit int DEFAULT 100
)
RETURNS jsonb
LANGUAGE plpgsql
STABLE
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_season_id bigint := p_season_id;
  v_month text := lower(nullif(btrim(coalesce(p_gpsl_month, '')), ''));
  v_lim int := greatest(1, least(coalesce(p_limit, 100), 200));
  v_uid uuid := auth.uid();
  v_can_react boolean := false;
BEGIN
  IF v_season_id IS NULL THEN
    SELECT id INTO v_season_id
    FROM public.competition_seasons
    WHERE is_current = true
    ORDER BY id DESC
    LIMIT 1;
  END IF;

  IF v_season_id IS NULL THEN
    RETURN jsonb_build_object('ok', false, 'reason', 'no_season', 'posts', '[]'::jsonb);
  END IF;

  IF v_uid IS NOT NULL THEN
    v_can_react := EXISTS (
      SELECT 1 FROM public."Clubs" c WHERE c.owner_id = v_uid LIMIT 1
    );
  END IF;

  RETURN jsonb_build_object(
    'ok', true,
    'season_id', v_season_id,
    'gpsl_month', v_month,
    'can_react', v_can_react,
    'reaction_emojis', '["👍","😂","🔥","⚽","👏","❤️"]'::jsonb,
    'posts', coalesce((
      SELECT jsonb_agg(row_data ORDER BY sort_at DESC)
      FROM (
        SELECT
          jsonb_build_object(
            'id', p.id,
            'season_id', p.season_id,
            'gpsl_month', p.gpsl_month,
            'month_label', public.competition_gpsl_month_label(p.gpsl_month),
            'club_short', p.club_short_name,
            'club_name', public.natter_club_display_name(p.club_short_name),
            'owner_tag', p.owner_tag,
            'body', p.body,
            'image_path', p.image_path,
            'created_at', p.created_at,
            'reactions', public.natter_reaction_counts(p.id),
            'my_reactions', coalesce((
              SELECT jsonb_agg(r.emoji ORDER BY r.emoji)
              FROM public.natter_reactions r
              WHERE r.post_id = p.id
                AND r.owner_id = v_uid
            ), '[]'::jsonb)
          ) AS row_data,
          p.created_at AS sort_at
        FROM public.natter_posts p
        WHERE p.season_id = v_season_id
          AND (v_month IS NULL OR p.gpsl_month = v_month)
        ORDER BY p.created_at DESC
        LIMIT v_lim
      ) q
    ), '[]'::jsonb)
  );
END;
$function$;


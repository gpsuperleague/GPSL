CREATE TABLE IF NOT EXISTS public.gpsl_sport_editions (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  season_id bigint NOT NULL REFERENCES public.competition_seasons (id) ON DELETE CASCADE,
  gpsl_month text NOT NULL,
  edition_label text NOT NULL,
  published_at timestamptz NOT NULL DEFAULT now(),
  story_type text NOT NULL,
  front_page jsonb NOT NULL,
  back_page jsonb NOT NULL DEFAULT '{}'::jsonb,
  detail jsonb NOT NULL DEFAULT '{}'::jsonb,
  CONSTRAINT gpsl_sport_editions_season_month_unique UNIQUE (season_id, gpsl_month)
);

CREATE INDEX IF NOT EXISTS gpsl_sport_editions_season_published_idx
  ON public.gpsl_sport_editions (season_id, published_at DESC);

CREATE TABLE IF NOT EXISTS public.gpsl_sport_reads (
  owner_id uuid NOT NULL REFERENCES auth.users (id) ON DELETE CASCADE,
  edition_id bigint NOT NULL REFERENCES public.gpsl_sport_editions (id) ON DELETE CASCADE,
  read_at timestamptz NOT NULL DEFAULT now(),
  PRIMARY KEY (owner_id, edition_id)
);

COMMENT ON TABLE public.gpsl_sport_editions IS
  'League newspaper edition per GPSL month (generated when month locks).';
COMMENT ON TABLE public.gpsl_sport_reads IS
  'Per-owner read state for GPSL Sport editions.';

ALTER TABLE public.gpsl_sport_editions ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.gpsl_sport_reads ENABLE ROW LEVEL SECURITY;

DROP POLICY IF EXISTS "Authenticated read sport editions" ON public.gpsl_sport_editions;
CREATE POLICY "Authenticated read sport editions"
  ON public.gpsl_sport_editions FOR SELECT TO authenticated USING (true);

DROP POLICY IF EXISTS "Owners read own sport reads" ON public.gpsl_sport_reads;
CREATE POLICY "Owners read own sport reads"
  ON public.gpsl_sport_reads FOR SELECT TO authenticated USING (owner_id = auth.uid());

DROP POLICY IF EXISTS "Owners insert own sport reads" ON public.gpsl_sport_reads;
CREATE POLICY "Owners insert own sport reads"
  ON public.gpsl_sport_reads FOR INSERT TO authenticated WITH CHECK (owner_id = auth.uid());

DROP POLICY IF EXISTS "Owners update own sport reads" ON public.gpsl_sport_reads;
CREATE POLICY "Owners update own sport reads"
  ON public.gpsl_sport_reads FOR UPDATE TO authenticated
  USING (owner_id = auth.uid()) WITH CHECK (owner_id = auth.uid());

GRANT SELECT ON public.gpsl_sport_editions TO authenticated;
GRANT SELECT, INSERT, UPDATE ON public.gpsl_sport_reads TO authenticated;


CREATE OR REPLACE FUNCTION public.match_schedule_agreed_kickoff(p_fixture_id bigint)
RETURNS timestamptz
LANGUAGE sql
STABLE
SECURITY DEFINER
SET search_path = public
AS $$
  SELECT sch.agreed_kickoff_at
  FROM public.competition_fixture_schedule sch
  WHERE sch.fixture_id = p_fixture_id
    AND sch.status = 'agreed';
$$;

CREATE OR REPLACE FUNCTION public.match_schedule_fixture_checkins(p_fixture_id bigint)
RETURNS TABLE (club_short_name text, checked_in_at timestamptz)
LANGUAGE sql
STABLE
SECURITY DEFINER
SET search_path = public
AS $$
  SELECT c.club_short_name, c.checked_in_at
  FROM public.competition_fixture_checkin c
  WHERE c.fixture_id = p_fixture_id;
$$;

CREATE OR REPLACE FUNCTION public.match_schedule_emergency_drops_used(
  p_season_id bigint,
  p_club_short_name text
)
RETURNS integer
LANGUAGE sql
STABLE
SECURITY DEFINER
SET search_path = public
AS $$
  SELECT count(*)::integer
  FROM public.competition_club_emergency_drop_use e
  WHERE e.season_id = p_season_id
    AND e.club_short_name = p_club_short_name;
$$;

CREATE OR REPLACE FUNCTION public.match_schedule_reschedule_used_this_month(
  p_season_id bigint,
  p_club_short_name text,
  p_gpsl_month text
)
RETURNS boolean
LANGUAGE sql
STABLE
SECURITY DEFINER
SET search_path = public
AS $$
  SELECT EXISTS (
    SELECT 1
    FROM public.competition_club_month_reschedule_use r
    WHERE r.season_id = p_season_id
      AND r.club_short_name = p_club_short_name
      AND r.gpsl_month = p_gpsl_month
  );
$$;

CREATE OR REPLACE FUNCTION public.match_schedule_reset_to_unscheduled(p_fixture_id bigint)
RETURNS void
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $function$
BEGIN
  UPDATE public.competition_fixture_schedule
  SET
    status = 'unscheduled',
    agreed_kickoff_at = NULL,
    pending_proposal_id = NULL,
    updated_at = now()
  WHERE fixture_id = p_fixture_id;

  DELETE FROM public.competition_fixture_checkin
  WHERE fixture_id = p_fixture_id;
END;
$function$;

-- ---------------------------------------------------------------------------
-- Forfeit (3–0, no stats, cup advance, fine)
-- ---------------------------------------------------------------------------

CREATE OR REPLACE FUNCTION public.fixture_apply_forfeit(
  p_fixture_id bigint,
  p_loser_club text,
  p_tariff_code text,
  p_reason text
)
RETURNS void
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_fixture public.competition_fixtures;
  v_home_goals smallint;
  v_away_goals smallint;
  v_winner text;
  v_title text;
  v_body text;
BEGIN
  SELECT * INTO v_fixture
  FROM public.competition_fixtures
  WHERE id = p_fixture_id
  FOR UPDATE;

  IF NOT FOUND THEN
    RAISE EXCEPTION 'Fixture not found';
  END IF;

  IF v_fixture.status = 'played' THEN
    RETURN;
  END IF;

  IF p_loser_club NOT IN (v_fixture.home_club_short_name, v_fixture.away_club_short_name) THEN
    RAISE EXCEPTION 'Loser must be a club in this fixture';
  END IF;

  v_winner := CASE
    WHEN p_loser_club = v_fixture.home_club_short_name THEN v_fixture.away_club_short_name
    ELSE v_fixture.home_club_short_name
  END;

  IF v_winner = v_fixture.home_club_short_name THEN
    v_home_goals := 3;
    v_away_goals := 0;
  ELSE
    v_home_goals := 0;
    v_away_goals := 3;
  END IF;

  UPDATE public.competition_result_submissions
  SET status = 'rejected',
      reject_reason = 'Superseded by forfeit',
      responded_at = now()
  WHERE fixture_id = p_fixture_id
    AND status = 'pending';

  UPDATE public.competition_fixtures
  SET
    home_goals = v_home_goals,
    away_goals = v_away_goals,
    status = 'played',
    is_forfeit = true,
    forfeit_loser_club = p_loser_club
  WHERE id = p_fixture_id;

  PERFORM public.competition_apply_club_fine_tariff(
    p_loser_club,
    p_tariff_code,
    NULL,
    coalesce(p_reason, 'Match forfeit'),
    p_fixture_id,
    v_fixture.season_id
  );

  IF v_fixture.competition_type = 'cup' THEN
    PERFORM public.competition_cup_on_fixture_played(p_fixture_id);
  ELSIF v_fixture.competition_type = 'league' THEN
    PERFORM public.competition_try_pay_league_division_prizes(
      v_fixture.season_id,
      v_fixture.division
    );
  END IF;

  v_title := 'Match forfeited';
  v_body := format(
    '%s forfeited 3–0. %s',
    p_loser_club,
    coalesce(p_reason, '')
  );

  PERFORM public.owner_inbox_send(
    'match_forfeit_applied', v_title, v_body,
    v_fixture.home_club_short_name, NULL, p_fixture_id,
    NULL, NULL, NULL,
    'fixture_schedule.html?fixture=' || p_fixture_id::text,
    'forfeit:' || p_fixture_id::text || ':home',
    v_fixture.gpsl_month, v_fixture.season_id, NULL
  );
  PERFORM public.owner_inbox_send(
    'match_forfeit_applied', v_title, v_body,
    v_fixture.away_club_short_name, NULL, p_fixture_id,
    NULL, NULL, NULL,
    'fixture_schedule.html?fixture=' || p_fixture_id::text,
    'forfeit:' || p_fixture_id::text || ':away',
    v_fixture.gpsl_month, v_fixture.season_id, NULL
  );
END;
$function$;

-- ---------------------------------------------------------------------------
-- Check-in resolution (no-show after window)
-- ---------------------------------------------------------------------------

CREATE OR REPLACE FUNCTION public.fixture_try_checkin_forfeit(p_fixture_id bigint)
RETURNS boolean
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_fixture public.competition_fixtures;
  v_kickoff timestamptz;
  v_window_end timestamptz;
  v_home_in boolean;
  v_away_in boolean;
  v_loser text;
BEGIN
  SELECT * INTO v_fixture FROM public.competition_fixtures WHERE id = p_fixture_id;
  IF NOT FOUND OR v_fixture.status <> 'scheduled' THEN
    RETURN false;
  END IF;

  v_kickoff := public.match_schedule_agreed_kickoff(p_fixture_id);
  IF v_kickoff IS NULL THEN
    RETURN false;
  END IF;

  v_window_end := v_kickoff + (public.match_schedule_checkin_minutes() || ' minutes')::interval;
  IF now() < v_window_end THEN
    RETURN false;
  END IF;

  SELECT EXISTS (
    SELECT 1 FROM public.competition_fixture_checkin c
    WHERE c.fixture_id = p_fixture_id
      AND c.club_short_name = v_fixture.home_club_short_name
  ) INTO v_home_in;

  SELECT EXISTS (
    SELECT 1 FROM public.competition_fixture_checkin c
    WHERE c.fixture_id = p_fixture_id
      AND c.club_short_name = v_fixture.away_club_short_name
  ) INTO v_away_in;

  IF v_home_in AND v_away_in THEN
    RETURN false;
  END IF;

  IF v_home_in AND NOT v_away_in THEN
    v_loser := v_fixture.away_club_short_name;
  ELSIF v_away_in AND NOT v_home_in THEN
    v_loser := v_fixture.home_club_short_name;
  ELSE
    RETURN false;
  END IF;

  PERFORM public.fixture_apply_forfeit(
    p_fixture_id,
    v_loser,
    'match_agreed_no_show',
    'No check-in during the agreed kick-off window'
  );

  RETURN true;
END;
$function$;

CREATE OR REPLACE FUNCTION public.match_schedule_assert_ready_to_play(p_fixture_id bigint)
RETURNS void
LANGUAGE plpgsql
STABLE
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_fixture public.competition_fixtures;
  v_kickoff timestamptz;
  v_block_end timestamptz;
  v_home_in boolean;
  v_away_in boolean;
BEGIN
  IF public.is_gpsl_admin() THEN
    RETURN;
  END IF;

  PERFORM public.fixture_try_checkin_forfeit(p_fixture_id);

  SELECT * INTO v_fixture FROM public.competition_fixtures WHERE id = p_fixture_id;
  IF v_fixture.status = 'played' THEN
    RAISE EXCEPTION 'This fixture has already been played';
  END IF;

  v_kickoff := public.match_schedule_agreed_kickoff(p_fixture_id);
  IF v_kickoff IS NULL THEN
    RAISE EXCEPTION 'Kick-off time must be agreed before entering a result';
  END IF;

  v_block_end := v_kickoff + (public.match_schedule_block_minutes() || ' minutes')::interval;

  IF now() < v_kickoff THEN
    RAISE EXCEPTION 'Result entry opens at the agreed kick-off (%)',
      public.match_schedule_format_kickoff_uk(v_kickoff);
  END IF;

  IF now() >= v_block_end THEN
    RAISE EXCEPTION 'The 30-minute match block has ended for this kick-off';
  END IF;

  SELECT EXISTS (
    SELECT 1 FROM public.competition_fixture_checkin c
    WHERE c.fixture_id = p_fixture_id
      AND c.club_short_name = v_fixture.home_club_short_name
  ) INTO v_home_in;

  SELECT EXISTS (
    SELECT 1 FROM public.competition_fixture_checkin c
    WHERE c.fixture_id = p_fixture_id
      AND c.club_short_name = v_fixture.away_club_short_name
  ) INTO v_away_in;

  IF NOT v_home_in OR NOT v_away_in THEN
    RAISE EXCEPTION 'Both clubs must check in before entering a result';
  END IF;
END;
$function$;

-- Guard result submission
CREATE OR REPLACE FUNCTION public.match_schedule_result_submission_guard()
RETURNS trigger
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $function$
BEGIN
  PERFORM public.match_schedule_assert_ready_to_play(NEW.fixture_id);
  RETURN NEW;
END;
$function$;

DROP TRIGGER IF EXISTS match_schedule_result_submission_guard_trg
  ON public.competition_result_submissions;

CREATE TRIGGER match_schedule_result_submission_guard_trg
  BEFORE INSERT ON public.competition_result_submissions
  FOR EACH ROW
  EXECUTE FUNCTION public.match_schedule_result_submission_guard();


  kickoff_at timestamptz NOT NULL,
  status text NOT NULL DEFAULT 'pending' CHECK (
    status IN ('pending', 'accepted', 'superseded', 'withdrawn')
  ),
  created_at timestamptz NOT NULL DEFAULT now()
);

CREATE INDEX IF NOT EXISTS competition_fixture_schedule_proposal_fixture_idx
  ON public.competition_fixture_schedule_proposal (fixture_id, created_at DESC);


-- ---------------------------------------------------------------------------
-- Weekly availability (30-minute blocks, UK wall-clock iso_dow + minute)
-- ---------------------------------------------------------------------------

CREATE TABLE IF NOT EXISTS public.club_owner_availability_slot (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  season_id bigint NOT NULL REFERENCES public.competition_seasons (id) ON DELETE CASCADE,
  club_short_name text NOT NULL REFERENCES public."Clubs" ("ShortName"),
  owner_id uuid NOT NULL REFERENCES auth.users (id) ON DELETE CASCADE,
  iso_dow smallint NOT NULL CHECK (iso_dow BETWEEN 1 AND 7),
  slot_minute smallint NOT NULL CHECK (
    slot_minute >= 0
    AND slot_minute <= 1410
    AND slot_minute % 30 = 0
  ),
  created_at timestamptz NOT NULL DEFAULT now(),
  CONSTRAINT club_owner_availability_slot_unique
    UNIQUE (season_id, club_short_name, iso_dow, slot_minute)
);

CREATE INDEX IF NOT EXISTS club_owner_availability_slot_club_idx
  ON public.club_owner_availability_slot (season_id, club_short_name);

-- One-off unavailable blocks (absences) within the season
CREATE TABLE IF NOT EXISTS public.club_owner_availability_unavailable (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  season_id bigint NOT NULL REFERENCES public.competition_seasons (id) ON DELETE CASCADE,
  club_short_name text NOT NULL REFERENCES public."Clubs" ("ShortName"),
  owner_id uuid NOT NULL REFERENCES auth.users (id) ON DELETE CASCADE,
  starts_at timestamptz NOT NULL,
  ends_at timestamptz NOT NULL,
  note text,
  created_at timestamptz NOT NULL DEFAULT now(),
  CONSTRAINT club_owner_availability_unavailable_window CHECK (ends_at > starts_at)
);

CREATE INDEX IF NOT EXISTS club_owner_availability_unavail_club_idx
  ON public.club_owner_availability_unavailable (season_id, club_short_name);

-- ---------------------------------------------------------------------------
-- Fixture schedule state
-- ---------------------------------------------------------------------------

CREATE TABLE IF NOT EXISTS public.competition_fixture_schedule (
  fixture_id bigint PRIMARY KEY REFERENCES public.competition_fixtures (id) ON DELETE CASCADE,
  season_id bigint NOT NULL REFERENCES public.competition_seasons (id) ON DELETE CASCADE,
  status text NOT NULL DEFAULT 'unscheduled' CHECK (
    status IN ('unscheduled', 'negotiating', 'agreed')
  ),
  home_proposal_count smallint NOT NULL DEFAULT 0 CHECK (home_proposal_count >= 0),
  away_proposal_count smallint NOT NULL DEFAULT 0 CHECK (away_proposal_count >= 0),
  agreed_kickoff_at timestamptz,
  pending_proposal_id bigint,
  discord_hint_shown boolean NOT NULL DEFAULT false,
  updated_at timestamptz NOT NULL DEFAULT now()
);

CREATE TABLE IF NOT EXISTS public.competition_fixture_schedule_proposal (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  fixture_id bigint NOT NULL REFERENCES public.competition_fixtures (id) ON DELETE CASCADE,

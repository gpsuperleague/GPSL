CREATE OR REPLACE FUNCTION public.match_schedule_format_kickoff_uk(p_kickoff timestamptz)
RETURNS text
LANGUAGE sql
IMMUTABLE
AS $$
  SELECT to_char(p_kickoff AT TIME ZONE 'Europe/London', 'Dy DD Mon YYYY HH24:MI') || ' UK';
$$;

CREATE OR REPLACE FUNCTION public.match_schedule_kickoff_is_slot(p_kickoff timestamptz)
RETURNS boolean
LANGUAGE sql
IMMUTABLE
AS $$
  SELECT
    EXTRACT(MINUTE FROM p_kickoff AT TIME ZONE 'Europe/London')::integer % 30 = 0
    AND EXTRACT(SECOND FROM p_kickoff) = 0;
$$;

CREATE OR REPLACE FUNCTION public.match_schedule_club_timezone(p_club text)
RETURNS text
LANGUAGE sql
STABLE
SECURITY DEFINER
SET search_path = public
AS $$
  SELECT COALESCE(NULLIF(btrim(c.owner_timezone), ''), 'Europe/London')
  FROM public."Clubs" c
  WHERE c."ShortName" = p_club;
$$;

CREATE OR REPLACE FUNCTION public.match_schedule_fixture_month_window(p_fixture_id bigint)
RETURNS TABLE (unlock_at timestamptz, lock_at timestamptz, gpsl_month text)
LANGUAGE plpgsql
STABLE
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_fixture public.competition_fixtures;
BEGIN
  SELECT * INTO v_fixture
  FROM public.competition_fixtures f
  WHERE f.id = p_fixture_id;

  IF NOT FOUND THEN
    RETURN;
  END IF;

  RETURN QUERY
  SELECT m.unlock_at, m.lock_at, m.gpsl_month
  FROM public.competition_season_calendar m
  WHERE m.season_id = v_fixture.season_id
    AND m.gpsl_month = v_fixture.gpsl_month;
END;
$function$;

CREATE OR REPLACE FUNCTION public.match_schedule_club_available_at(
  p_season_id bigint,
  p_club_short_name text,
  p_kickoff timestamptz
)
RETURNS boolean
LANGUAGE plpgsql
STABLE
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_local timestamp;
  v_isodow smallint;
  v_slot_minute smallint;
BEGIN
  IF p_club_short_name IS NULL OR btrim(p_club_short_name) = '' THEN
    RETURN false;
  END IF;

  IF NOT public.match_schedule_kickoff_is_slot(p_kickoff) THEN
    RETURN false;
  END IF;

  v_local := p_kickoff AT TIME ZONE 'Europe/London';
  v_isodow := EXTRACT(ISODOW FROM v_local)::smallint;
  v_slot_minute := (
    EXTRACT(HOUR FROM v_local) * 60 + EXTRACT(MINUTE FROM v_local)
  )::smallint;

  IF NOT EXISTS (
    SELECT 1
    FROM public.club_owner_availability_slot s
    WHERE s.season_id = p_season_id
      AND s.club_short_name = p_club_short_name
      AND s.iso_dow = v_isodow
      AND s.slot_minute = v_slot_minute
  ) THEN
    RETURN false;
  END IF;

  IF EXISTS (
    SELECT 1
    FROM public.club_owner_holidays h
    WHERE h.season_id = p_season_id
      AND h.club_short_name = p_club_short_name
      AND p_kickoff >= h.starts_at
      AND p_kickoff < h.ends_at
  ) THEN
    RETURN false;
  END IF;

  IF EXISTS (
    SELECT 1
    FROM public.club_owner_availability_unavailable u
    WHERE u.season_id = p_season_id
      AND u.club_short_name = p_club_short_name
      AND p_kickoff >= u.starts_at
      AND p_kickoff < u.ends_at
  ) THEN
    RETURN false;
  END IF;

  RETURN true;
END;
$function$;

CREATE OR REPLACE FUNCTION public.match_schedule_assert_kickoff_valid(
  p_fixture_id bigint,
  p_kickoff timestamptz
)
RETURNS public.competition_fixtures
LANGUAGE plpgsql
STABLE
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_fixture public.competition_fixtures;
  v_unlock timestamptz;
  v_lock timestamptz;
BEGIN
  SELECT * INTO v_fixture
  FROM public.competition_fixtures f
  WHERE f.id = p_fixture_id;

  IF NOT FOUND THEN
    RAISE EXCEPTION 'Fixture not found';
  END IF;

  IF v_fixture.status <> 'scheduled' THEN
    RAISE EXCEPTION 'Fixture is not open for scheduling';
  END IF;

  IF NOT public.match_schedule_kickoff_is_slot(p_kickoff) THEN
    RAISE EXCEPTION 'Kick-off must be on a 30-minute boundary (UK time)';
  END IF;

  SELECT w.unlock_at, w.lock_at
  INTO v_unlock, v_lock
  FROM public.match_schedule_fixture_month_window(p_fixture_id) w;

  IF v_unlock IS NULL THEN
    RAISE EXCEPTION 'No GPSL month window for this fixture';
  END IF;

  IF p_kickoff < v_unlock OR p_kickoff + interval '30 minutes' > v_lock THEN
    RAISE EXCEPTION 'Kick-off must fall within the GPSL month window (% – % UK)',
      public.match_schedule_format_kickoff_uk(v_unlock),
      public.match_schedule_format_kickoff_uk(v_lock);
  END IF;

  IF NOT public.match_schedule_club_available_at(v_fixture.season_id, v_fixture.home_club_short_name, p_kickoff) THEN
    RAISE EXCEPTION 'Home club is not available at that time';
  END IF;

  IF NOT public.match_schedule_club_available_at(v_fixture.season_id, v_fixture.away_club_short_name, p_kickoff) THEN
    RAISE EXCEPTION 'Away club is not available at that time';
  END IF;

  RETURN v_fixture;
END;
$function$;

CREATE OR REPLACE FUNCTION public.match_schedule_ensure_row(p_fixture_id bigint)
RETURNS public.competition_fixture_schedule
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_fixture public.competition_fixtures;
  v_row public.competition_fixture_schedule;
BEGIN
  SELECT * INTO v_fixture FROM public.competition_fixtures WHERE id = p_fixture_id;
  IF NOT FOUND THEN
    RAISE EXCEPTION 'Fixture not found';
  END IF;

  INSERT INTO public.competition_fixture_schedule (fixture_id, season_id, status)
  VALUES (p_fixture_id, v_fixture.season_id, 'unscheduled')
  ON CONFLICT (fixture_id) DO NOTHING;

  SELECT * INTO v_row
  FROM public.competition_fixture_schedule
  WHERE fixture_id = p_fixture_id;

  RETURN v_row;
END;
$function$;

CREATE OR REPLACE FUNCTION public.match_schedule_intersection_slots(p_fixture_id bigint)
RETURNS TABLE (kickoff_at timestamptz)
LANGUAGE plpgsql
STABLE
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_fixture public.competition_fixtures;
  v_unlock timestamptz;
  v_lock timestamptz;
  v_cursor timestamptz;
BEGIN
  SELECT * INTO v_fixture FROM public.competition_fixtures WHERE id = p_fixture_id;
  IF NOT FOUND THEN
    RETURN;
  END IF;

  SELECT w.unlock_at, w.lock_at INTO v_unlock, v_lock
  FROM public.match_schedule_fixture_month_window(p_fixture_id) w;

  IF v_unlock IS NULL THEN
    RETURN;
  END IF;

  v_cursor := v_unlock;
  WHILE v_cursor + interval '30 minutes' <= v_lock LOOP
    IF public.match_schedule_club_available_at(v_fixture.season_id, v_fixture.home_club_short_name, v_cursor)
       AND public.match_schedule_club_available_at(v_fixture.season_id, v_fixture.away_club_short_name, v_cursor)
    THEN
      kickoff_at := v_cursor;
      RETURN NEXT;
    END IF;
    v_cursor := v_cursor + interval '30 minutes';
  END LOOP;
END;
$function$;


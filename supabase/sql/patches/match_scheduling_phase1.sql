CREATE OR REPLACE FUNCTION public.club_availability_save_weekly(p_slots jsonb)
RETURNS void
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_club text;
  v_season_id bigint;
  v_slot jsonb;
  v_isodow smallint;
  v_minute smallint;
BEGIN
  v_club := public.my_club_shortname();
  IF v_club IS NULL OR v_club = '' THEN
    RAISE EXCEPTION 'No club linked to this account';
  END IF;

  SELECT s.id INTO v_season_id
  FROM public.competition_seasons s
  WHERE s.is_current = true AND s.status = 'active'
  LIMIT 1;

  IF v_season_id IS NULL THEN
    RAISE EXCEPTION 'No active competition season';
  END IF;

  IF p_slots IS NULL OR jsonb_typeof(p_slots) <> 'array' THEN
    RAISE EXCEPTION 'Slots must be a JSON array';
  END IF;

  DELETE FROM public.club_owner_availability_slot
  WHERE season_id = v_season_id
    AND club_short_name = v_club;

  FOR v_slot IN SELECT * FROM jsonb_array_elements(p_slots)
  LOOP
    v_isodow := (v_slot->>'iso_dow')::smallint;
    v_minute := (
      COALESCE((v_slot->>'hour')::integer, 0) * 60
      + COALESCE((v_slot->>'minute')::integer, 0)
    )::smallint;

    IF v_isodow IS NULL OR v_isodow < 1 OR v_isodow > 7 THEN
      RAISE EXCEPTION 'Invalid iso_dow in slot';
    END IF;

    IF v_minute % 30 <> 0 OR v_minute < 0 OR v_minute > 1410 THEN
      RAISE EXCEPTION 'Invalid time in slot (30-minute blocks only)';
    END IF;

    INSERT INTO public.club_owner_availability_slot (
      season_id, club_short_name, owner_id, iso_dow, slot_minute
    )
    VALUES (v_season_id, v_club, auth.uid(), v_isodow, v_minute);
  END LOOP;
END;
$function$;

CREATE OR REPLACE FUNCTION public.club_availability_add_unavailable(
  p_starts_at timestamptz,
  p_ends_at timestamptz,
  p_note text DEFAULT NULL
)
RETURNS bigint
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_club text;
  v_season_id bigint;
  v_id bigint;
BEGIN
  v_club := public.my_club_shortname();
  IF v_club IS NULL OR v_club = '' THEN
    RAISE EXCEPTION 'No club linked to this account';
  END IF;

  IF p_starts_at IS NULL OR p_ends_at IS NULL OR p_ends_at <= p_starts_at THEN
    RAISE EXCEPTION 'Invalid unavailable window';
  END IF;

  SELECT s.id INTO v_season_id
  FROM public.competition_seasons s
  WHERE s.is_current = true AND s.status = 'active'
  LIMIT 1;

  IF v_season_id IS NULL THEN
    RAISE EXCEPTION 'No active competition season';
  END IF;

  INSERT INTO public.club_owner_availability_unavailable (
    season_id, club_short_name, owner_id, starts_at, ends_at, note
  )
  VALUES (v_season_id, v_club, auth.uid(), p_starts_at, p_ends_at, nullif(btrim(p_note), ''))
  RETURNING id INTO v_id;

  RETURN v_id;
END;
$function$;


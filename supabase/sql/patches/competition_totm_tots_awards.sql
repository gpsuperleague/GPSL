-- Pick best XI from candidate stats (month or season rows in a temp-like query)
CREATE OR REPLACE FUNCTION public.competition_pick_period_team(
  p_candidates jsonb,
  p_formation_ids text[] DEFAULT ARRAY['4-4-2', '4-3-3', '4-2-3-1']
)
RETURNS jsonb
LANGUAGE plpgsql
IMMUTABLE
AS $function$
DECLARE
  v_form text;
  v_slot record;
  v_pick record;
  v_used text[] := ARRAY[]::text[];
  v_lineup jsonb := '[]'::jsonb;
  v_all_filled boolean;
BEGIN
  IF p_candidates IS NULL OR jsonb_array_length(p_candidates) = 0 THEN
    RETURN NULL;
  END IF;

  FOREACH v_form IN ARRAY p_formation_ids LOOP
    v_used := ARRAY[]::text[];
    v_lineup := '[]'::jsonb;
    v_all_filled := true;

    FOR v_slot IN
      SELECT * FROM public.competition_formation_slot_defs(v_form)
      ORDER BY sort_order
    LOOP
      SELECT c.*
      INTO v_pick
      FROM jsonb_to_recordset(p_candidates) AS c(
        player_id text,
        club_short_name text,
        player_position text,
        stat_role text,
        appearances int,
        goals int,
        assists int,
        avg_rating numeric,
        clean_sheets int,
        selection_score numeric
      )
      WHERE c.appearances >= 2
        AND c.selection_score > 0
        AND public.competition_position_fits_slot_label(c.player_position, v_slot.slot_label)
        AND NOT (c.player_id = ANY (v_used))
      ORDER BY c.selection_score DESC, c.goals DESC, c.assists DESC, c.avg_rating DESC NULLS LAST
      LIMIT 1;

      IF NOT FOUND THEN
        v_all_filled := false;
        EXIT;
      END IF;

      v_used := array_append(v_used, v_pick.player_id);
      v_lineup := v_lineup || jsonb_build_array(
        jsonb_build_object(
          'pitch_slot', v_slot.pitch_slot,
          'slot_label', v_slot.slot_label,
          'player_id', v_pick.player_id,
          'club_short_name', v_pick.club_short_name,
          'player_position', v_pick.player_position,
          'appearances', v_pick.appearances,
          'goals', v_pick.goals,
          'assists', v_pick.assists,
          'avg_rating', v_pick.avg_rating,
          'clean_sheets', v_pick.clean_sheets,
          'selection_score', v_pick.selection_score
        )
      );
    END LOOP;

    IF v_all_filled AND jsonb_array_length(v_lineup) = 11 THEN
      RETURN jsonb_build_object('formation_id', v_form, 'lineup', v_lineup);
    END IF;
  END LOOP;

  RETURN NULL;
END;
$function$;

-- ---------------------------------------------------------------------------
-- Compute & persist team of month / season
-- ---------------------------------------------------------------------------

CREATE OR REPLACE FUNCTION public.competition_store_period_team(
  p_season_id bigint,
  p_season_label text,
  p_gpsl_month text,
  p_period_kind text,
  p_division_scope text,
  p_team jsonb
)
RETURNS bigint
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_team_id bigint;
  v_member jsonb;
  v_award_type text;
BEGIN
  IF p_team IS NULL OR NOT (p_team ? 'lineup') THEN
    RETURN NULL;
  END IF;

  v_award_type := CASE p_period_kind
    WHEN 'month' THEN 'team_of_month'
    WHEN 'season' THEN 'team_of_season'
    ELSE NULL
  END;

  DELETE FROM public.competition_period_team_member m
  USING public.competition_period_team t
  WHERE m.team_id = t.id
    AND t.season_id = p_season_id
    AND t.period_kind = p_period_kind
    AND t.division_scope = p_division_scope
    AND coalesce(t.gpsl_month, '') = coalesce(p_gpsl_month, '');

  DELETE FROM public.competition_period_team
  WHERE season_id = p_season_id
    AND period_kind = p_period_kind
    AND division_scope = p_division_scope
    AND coalesce(gpsl_month, '') = coalesce(p_gpsl_month, '');

  IF p_period_kind = 'month' THEN
    DELETE FROM public.competition_season_award
    WHERE season_id = p_season_id
      AND award_type = 'team_of_month'
      AND gpsl_month = p_gpsl_month;
  ELSIF p_period_kind = 'season' THEN
    DELETE FROM public.competition_season_award
    WHERE season_id = p_season_id
      AND award_type = 'team_of_season';
  END IF;

  INSERT INTO public.competition_period_team (
    season_id, season_label, gpsl_month, period_kind, division_scope, formation_id, detail
  )
  VALUES (
    p_season_id,
    p_season_label,
    p_gpsl_month,
    p_period_kind,
    p_division_scope,
    p_team ->> 'formation_id',
    jsonb_build_object('formation_id', p_team ->> 'formation_id')
  )
  RETURNING id INTO v_team_id;

  FOR v_member IN
    SELECT value FROM jsonb_array_elements(p_team -> 'lineup')
  LOOP
    INSERT INTO public.competition_period_team_member (
      team_id, pitch_slot, slot_label, player_id, club_short_name,
      selection_score, appearances, goals, assists, avg_rating, clean_sheets
    )
    VALUES (
      v_team_id,
      v_member ->> 'pitch_slot',
      v_member ->> 'slot_label',
      v_member ->> 'player_id',
      v_member ->> 'club_short_name',
      coalesce((v_member ->> 'selection_score')::numeric, 0),
      coalesce((v_member ->> 'appearances')::int, 0),
      coalesce((v_member ->> 'goals')::int, 0),
      coalesce((v_member ->> 'assists')::int, 0),
      nullif(v_member ->> 'avg_rating', '')::numeric,
      coalesce((v_member ->> 'clean_sheets')::int, 0)
    );

    IF v_award_type IS NOT NULL THEN
      INSERT INTO public.competition_season_award (
        season_id, season_label, award_type, player_id, club_short_name,
        stat_value, gpsl_month, detail
      )
      VALUES (
        p_season_id,
        p_season_label,
        v_award_type,
        v_member ->> 'player_id',
        v_member ->> 'club_short_name',
        coalesce((v_member ->> 'selection_score')::numeric, 0),
        p_gpsl_month,
        jsonb_build_object(
          'formation_id', p_team ->> 'formation_id',
          'pitch_slot', v_member ->> 'pitch_slot',
          'slot_label', v_member ->> 'slot_label',
          'gpsl_month', p_gpsl_month
        )
      )
      ON CONFLICT DO NOTHING;
    END IF;
  END LOOP;

  RETURN v_team_id;
END;
$function$;

CREATE OR REPLACE FUNCTION public.competition_compute_team_of_month(
  p_season_id bigint,
  p_gpsl_month text
)
RETURNS jsonb
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_label text;
  v_candidates jsonb;
  v_team jsonb;
  v_team_id bigint;
BEGIN
  SELECT label INTO v_label FROM public.competition_seasons WHERE id = p_season_id;

  SELECT coalesce(jsonb_agg(to_jsonb(s)), '[]'::jsonb)
  INTO v_candidates
  FROM public.competition_player_month_stats_public s
  WHERE s.season_id = p_season_id
    AND s.gpsl_month = p_gpsl_month
    AND s.division = 'superleague'
    AND s.appearances >= 2;

  v_team := public.competition_pick_period_team(v_candidates);
  IF v_team IS NULL THEN
    RETURN jsonb_build_object('ok', false, 'reason', 'no_valid_xi', 'gpsl_month', p_gpsl_month);
  END IF;

  v_team_id := public.competition_store_period_team(
    p_season_id, v_label, p_gpsl_month, 'month', 'superleague', v_team
  );

  RETURN jsonb_build_object(
    'ok', true,
    'gpsl_month', p_gpsl_month,
    'team_id', v_team_id,
    'formation_id', v_team ->> 'formation_id'
  );
END;
$function$;

CREATE OR REPLACE FUNCTION public.competition_compute_team_of_season(p_season_id bigint)
RETURNS jsonb
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_label text;
  v_candidates jsonb;
  v_team jsonb;
  v_team_id bigint;
BEGIN
  SELECT label INTO v_label FROM public.competition_seasons WHERE id = p_season_id;

  SELECT coalesce(jsonb_agg(to_jsonb(x)), '[]'::jsonb)
  INTO v_candidates
  FROM (
    SELECT
      a.player_id,
      a.club_short_name,
      a.player_position,
      a.stat_role,
      a.appearances,
      a.goals,
      a.assists,
      a.avg_rating,
      a.clean_sheets,
      public.competition_player_selection_score(
        a.stat_role,
        a.player_position,
        a.appearances,
        a.goals,
        a.assists,
        a.avg_rating,
        a.clean_sheets
      ) AS selection_score
    FROM public.competition_player_season_archive a
    WHERE a.season_id = p_season_id
      AND a.division = 'superleague'
      AND a.appearances >= 5
  ) x;

  v_team := public.competition_pick_period_team(v_candidates);
  IF v_team IS NULL THEN
    RETURN jsonb_build_object('ok', false, 'reason', 'no_valid_xi');
  END IF;

  v_team_id := public.competition_store_period_team(
    p_season_id, v_label, NULL, 'season', 'superleague', v_team
  );

  RETURN jsonb_build_object(
    'ok', true,
    'team_id', v_team_id,
    'formation_id', v_team ->> 'formation_id'
  );
END;
$function$;

CREATE OR REPLACE FUNCTION public.competition_compute_championship_player_of_season(p_season_id bigint)
RETURNS jsonb
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_label text;
  v_winner record;
BEGIN
  SELECT label INTO v_label FROM public.competition_seasons WHERE id = p_season_id;

  DELETE FROM public.competition_season_award
  WHERE season_id = p_season_id
    AND award_type = 'championship_player_of_season';

  SELECT
    a.player_id,
    a.club_short_name,
    a.division,
    public.competition_player_selection_score(
      a.stat_role,
      a.player_position,
      a.appearances,
      a.goals,
      a.assists,
      a.avg_rating,
      a.clean_sheets
    ) AS selection_score,
    a.goals,
    a.assists,
    a.avg_rating,
    a.appearances
  INTO v_winner
  FROM public.competition_player_season_archive a
  WHERE a.season_id = p_season_id
    AND a.division IN ('championship_a', 'championship_b')
    AND a.appearances >= 5
  ORDER BY
    public.competition_player_selection_score(
      a.stat_role, a.player_position, a.appearances, a.goals, a.assists, a.avg_rating, a.clean_sheets
    ) DESC,
    a.goals DESC,
    a.assists DESC
  LIMIT 1;

  IF NOT FOUND OR coalesce(v_winner.selection_score, 0) <= 0 THEN
    RETURN jsonb_build_object('ok', false, 'reason', 'no_eligible_player');
  END IF;

  INSERT INTO public.competition_season_award (
    season_id, season_label, award_type, player_id, club_short_name, stat_value, detail
  )
  VALUES (
    p_season_id,
    v_label,
    'championship_player_of_season',
    v_winner.player_id,
    v_winner.club_short_name,
    v_winner.selection_score,
    jsonb_build_object(
      'division', v_winner.division,
      'goals', v_winner.goals,
      'assists', v_winner.assists,
      'avg_rating', v_winner.avg_rating,
      'appearances', v_winner.appearances
    )
  );

  RETURN jsonb_build_object(
    'ok', true,
    'player_id', v_winner.player_id,
    'club_short_name', v_winner.club_short_name,
    'division', v_winner.division
  );
END;
$function$;

-- Process all locked months without a TOTM job
CREATE OR REPLACE FUNCTION public.competition_process_month_team_awards(p_season_id bigint)
RETURNS jsonb
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_cal record;
  v_results jsonb := '[]'::jsonb;
  v_res jsonb;
BEGIN
  FOR v_cal IN
    SELECT c.gpsl_month
    FROM public.competition_season_calendar c
    WHERE c.season_id = p_season_id
      AND c.gpsl_month IS NOT NULL
      AND c.lock_at IS NOT NULL
      AND c.lock_at <= now()
      AND NOT EXISTS (
        SELECT 1
        FROM public.competition_season_calendar_jobs j
        WHERE j.season_id = p_season_id
          AND j.job_key = 'team_of_month:' || c.gpsl_month
      )
    ORDER BY public.competition_gpsl_month_sort(c.gpsl_month)
  LOOP
    v_res := public.competition_compute_team_of_month(p_season_id, v_cal.gpsl_month);

    INSERT INTO public.competition_season_calendar_jobs (
      season_id, job_key, gpsl_month, result
    )
    VALUES (
      p_season_id,
      'team_of_month:' || v_cal.gpsl_month,
      v_cal.gpsl_month,
      coalesce(v_res, '{}'::jsonb)
    )
    ON CONFLICT (season_id, job_key) DO UPDATE
      SET result = excluded.result,
          gpsl_month = excluded.gpsl_month,
          ran_at = now();

    v_results := v_results || jsonb_build_array(
      jsonb_build_object('gpsl_month', v_cal.gpsl_month, 'result', v_res)
    );
  END LOOP;

  RETURN jsonb_build_object('ok', true, 'processed', v_results);
END;
$function$;


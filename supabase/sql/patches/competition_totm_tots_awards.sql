CREATE OR REPLACE FUNCTION public.competition_position_fits_slot_label(
  p_position text,
  p_slot_label text
)
RETURNS boolean
LANGUAGE sql
IMMUTABLE
AS $$
  SELECT CASE upper(btrim(coalesce(p_slot_label, '')))
    WHEN 'GK' THEN upper(btrim(coalesce(p_position, ''))) = 'GK'
    WHEN 'LB' THEN upper(btrim(p_position)) IN ('LB', 'LWB')
    WHEN 'RB' THEN upper(btrim(p_position)) IN ('RB', 'RWB')
    WHEN 'CB' THEN upper(btrim(p_position)) = 'CB'
    WHEN 'DMF' THEN upper(btrim(p_position)) IN ('DMF', 'CMF')
    WHEN 'LMF' THEN upper(btrim(p_position)) IN ('LMF', 'LW', 'WG')
    WHEN 'RMF' THEN upper(btrim(p_position)) IN ('RMF', 'RW', 'WG')
    WHEN 'CMF' THEN upper(btrim(p_position)) IN ('CMF', 'DMF', 'AMF')
    WHEN 'AMF' THEN upper(btrim(p_position)) IN ('AMF', 'SS', 'CMF')
    WHEN 'LWF' THEN upper(btrim(p_position)) IN ('LWF', 'LW', 'SS', 'CF')
    WHEN 'RWF' THEN upper(btrim(p_position)) IN ('RWF', 'RW', 'SS', 'CF')
    WHEN 'CF' THEN upper(btrim(p_position)) IN ('CF', 'SS')
    ELSE false
  END;
$$;

CREATE OR REPLACE FUNCTION public.competition_player_clean_sheets_month(
  p_season_id bigint,
  p_gpsl_month text,
  p_player_id text,
  p_club_short_name text DEFAULT NULL
)
RETURNS int
LANGUAGE sql
STABLE
SECURITY DEFINER
SET search_path = public
AS $$
  SELECT count(*)::int
  FROM public.competition_match_player_stats m
  JOIN public.competition_fixtures f ON f.id = m.fixture_id
  JOIN public."Players" p ON p."Konami_ID"::text = m.player_id
  WHERE m.season_id = p_season_id
    AND f.gpsl_month = p_gpsl_month
    AND f.competition_type = 'league'
    AND m.player_id = btrim(p_player_id)
    AND m.started = true
    AND f.status = 'played'
    AND (
      p_club_short_name IS NULL
      OR m.club_short_name = btrim(p_club_short_name)
    )
    AND public.competition_player_stat_role(p."Position") IN ('goalkeeper', 'defender')
    AND public.competition_player_conceded_in_fixture(f.id, m.club_short_name) = 0;
$$;

CREATE OR REPLACE FUNCTION public.competition_player_selection_score(
  p_stat_role text,
  p_position text,
  p_appearances int,
  p_goals int,
  p_assists int,
  p_avg_rating numeric,
  p_clean_sheets int
)
RETURNS numeric
LANGUAGE plpgsql
IMMUTABLE
AS $function$
DECLARE
  v_pos text := upper(btrim(coalesce(p_position, '')));
  v_role text := coalesce(p_stat_role, 'outfield');
  v_rating numeric := coalesce(p_avg_rating, 0);
  v_apps int := greatest(coalesce(p_appearances, 0), 0);
BEGIN
  IF v_apps < 1 THEN
    RETURN 0;
  END IF;

  IF v_role = 'goalkeeper' THEN
    RETURN round(v_rating * 10 + greatest(coalesce(p_clean_sheets, 0), 0) * 15 + v_apps * 0.5, 2);
  ELSIF v_role = 'defender' AND v_pos IN ('LB', 'RB', 'LWB', 'RWB') THEN
    RETURN round(
      v_rating * 10
      + greatest(coalesce(p_assists, 0), 0) * 8
      + greatest(coalesce(p_goals, 0), 0) * 4
      + v_apps * 0.3,
      2
    );
  ELSIF v_role = 'defender' THEN
    RETURN round(
      v_rating * 10
      + greatest(coalesce(p_clean_sheets, 0), 0) * 12
      + greatest(coalesce(p_assists, 0), 0) * 3
      + v_apps * 0.3,
      2
    );
  ELSIF v_role IN ('midfielder', 'forward') THEN
    RETURN round(
      v_rating * 10
      + greatest(coalesce(p_goals, 0), 0) * 8
      + greatest(coalesce(p_assists, 0), 0) * 6
      + v_apps * 0.2,
      2
    );
  END IF;

  RETURN round(
    v_rating * 10
    + greatest(coalesce(p_goals, 0), 0) * 7
    + greatest(coalesce(p_assists, 0), 0) * 5
    + v_apps * 0.2,
    2
  );
END;
$function$;

-- ---------------------------------------------------------------------------
-- Monthly stats (league)
-- ---------------------------------------------------------------------------

CREATE OR REPLACE VIEW public.competition_player_month_stats_public
WITH (security_invoker = false)
AS
SELECT
  m.season_id,
  s.label AS season_label,
  f.gpsl_month,
  m.player_id,
  p."Name" AS player_name,
  m.club_short_name,
  c."Club" AS club_name,
  ccs.division,
  p."Position" AS player_position,
  public.competition_player_stat_role(p."Position") AS stat_role,
  count(*) FILTER (WHERE m.appeared)::int AS appearances,
  count(*) FILTER (WHERE m.started)::int AS starts,
  coalesce(sum(m.goals), 0)::int AS goals,
  coalesce(sum(m.assists), 0)::int AS assists,
  round(avg(m.rating) FILTER (WHERE m.rating IS NOT NULL), 2) AS avg_rating,
  count(*) FILTER (WHERE m.is_player_of_match)::int AS potm_awards,
  public.competition_player_clean_sheets_month(
    m.season_id,
    f.gpsl_month,
    m.player_id,
    m.club_short_name
  ) AS clean_sheets,
  public.competition_player_selection_score(
    public.competition_player_stat_role(p."Position"),
    p."Position",
    count(*) FILTER (WHERE m.appeared)::int,
    coalesce(sum(m.goals), 0)::int,
    coalesce(sum(m.assists), 0)::int,
    round(avg(m.rating) FILTER (WHERE m.rating IS NOT NULL), 2),
    public.competition_player_clean_sheets_month(
      m.season_id,
      f.gpsl_month,
      m.player_id,
      m.club_short_name
    )
  ) AS selection_score
FROM public.competition_match_player_stats m
JOIN public.competition_fixtures f ON f.id = m.fixture_id
JOIN public.competition_seasons s ON s.id = m.season_id
JOIN public."Players" p ON p."Konami_ID"::text = m.player_id
JOIN public."Clubs" c ON c."ShortName" = m.club_short_name
LEFT JOIN public.competition_club_seasons ccs
  ON ccs.season_id = m.season_id AND ccs.club_short_name = m.club_short_name
WHERE f.status = 'played'
  AND f.competition_type = 'league'
  AND f.gpsl_month IS NOT NULL
GROUP BY
  m.season_id, s.label, f.gpsl_month, m.player_id, p."Name",
  m.club_short_name, c."Club", ccs.division, p."Position";

GRANT SELECT ON public.competition_player_month_stats_public TO authenticated, anon;


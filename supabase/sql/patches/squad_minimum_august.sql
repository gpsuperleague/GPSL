-- =============================================================================
-- Squad minimum (24) — August enforcement, fines, and season loans
-- Run once after squad_composition_rules.sql + competition_fines.sql
--       (+ central_bank_model_a_flows.sql if using bank legs).
-- =============================================================================
-- Rules:
--   * Minimum 24 registered players by start of GPSL August.
--   * No penalties before August.
--   * Per missing player at August enforcement: ₿5m fine + random HG free agent
--     (rating ≤ 72) on season loan + ₿5m loan fee.
--   * Season loans terminable when squad > 24; 50% loan fee refunded (not fines).
-- =============================================================================

CREATE TABLE IF NOT EXISTS public.club_squad_minimum_enforcement (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  season_id bigint NOT NULL REFERENCES public.competition_seasons (id) ON DELETE CASCADE,
  club_short_name text NOT NULL REFERENCES public."Clubs" ("ShortName") ON DELETE CASCADE,
  squad_count int NOT NULL,
  shortfall int NOT NULL CHECK (shortfall > 0),
  fine_per_player numeric NOT NULL DEFAULT 5000000,
  loan_fee_per_player numeric NOT NULL DEFAULT 5000000,
  total_fine numeric NOT NULL,
  total_loan_fee numeric NOT NULL DEFAULT 0,
  loans_granted int NOT NULL DEFAULT 0,
  enforced_at timestamptz NOT NULL DEFAULT now(),
  metadata jsonb NOT NULL DEFAULT '{}'::jsonb,
  UNIQUE (season_id, club_short_name)
);

CREATE TABLE IF NOT EXISTS public.club_season_loans (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  season_id bigint NOT NULL REFERENCES public.competition_seasons (id) ON DELETE CASCADE,
  club_short_name text NOT NULL REFERENCES public."Clubs" ("ShortName") ON DELETE CASCADE,
  player_id text NOT NULL,
  loan_fee numeric NOT NULL DEFAULT 5000000,
  fine_amount numeric NOT NULL DEFAULT 5000000,
  status text NOT NULL DEFAULT 'active'
    CHECK (status IN ('active', 'terminated')),
  loan_ledger_id bigint,
  fine_ledger_id bigint,
  refund_ledger_id bigint,
  refund_amount numeric,
  terminated_at timestamptz,
  created_at timestamptz NOT NULL DEFAULT now(),
  UNIQUE (season_id, player_id),
  UNIQUE (season_id, club_short_name, player_id)
);

CREATE INDEX IF NOT EXISTS club_season_loans_club_active_idx
  ON public.club_season_loans (club_short_name, season_id)
  WHERE status = 'active';

ALTER TABLE public.club_squad_minimum_enforcement ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.club_season_loans ENABLE ROW LEVEL SECURITY;

DROP POLICY IF EXISTS club_squad_minimum_enforcement_select ON public.club_squad_minimum_enforcement;
CREATE POLICY club_squad_minimum_enforcement_select ON public.club_squad_minimum_enforcement
  FOR SELECT TO authenticated USING (true);

DROP POLICY IF EXISTS club_season_loans_select ON public.club_season_loans;
CREATE POLICY club_season_loans_select ON public.club_season_loans
  FOR SELECT TO authenticated USING (true);

DROP POLICY IF EXISTS club_squad_minimum_enforcement_admin ON public.club_squad_minimum_enforcement;
CREATE POLICY club_squad_minimum_enforcement_admin ON public.club_squad_minimum_enforcement
  FOR ALL TO authenticated
  USING (public.is_gpsl_admin())
  WITH CHECK (public.is_gpsl_admin());

DROP POLICY IF EXISTS club_season_loans_admin ON public.club_season_loans;
CREATE POLICY club_season_loans_admin ON public.club_season_loans
  FOR ALL TO authenticated
  USING (public.is_gpsl_admin())
  WITH CHECK (public.is_gpsl_admin());

GRANT SELECT ON public.club_squad_minimum_enforcement TO authenticated;
GRANT SELECT ON public.club_season_loans TO authenticated;


CREATE OR REPLACE FUNCTION public.club_transfer_buy_embargo_ensure(
  p_club_short_name text,
  p_season_id bigint,
  p_reason text DEFAULT 'ffp_eos'
)
RETURNS text
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_club text;
  v_open boolean := false;
  v_phase text;
BEGIN
  SELECT c."ShortName" INTO v_club
  FROM public."Clubs" c
  WHERE upper(c."ShortName") = upper(btrim(coalesce(p_club_short_name, '')))
  LIMIT 1;

  IF v_club IS NULL THEN
    v_club := btrim(p_club_short_name);
  END IF;

  IF v_club IS NULL OR v_club = '' OR p_season_id IS NULL THEN
    RETURN NULL;
  END IF;

  -- Already have an unfinished embargo for this club — keep it
  IF EXISTS (
    SELECT 1 FROM public.club_transfer_buy_embargoes e
    WHERE upper(e.club_short_name) = upper(v_club)
      AND e.phase IN ('skip_current', 'armed', 'active')
  ) THEN
    SELECT e.phase INTO v_phase
    FROM public.club_transfer_buy_embargoes e
    WHERE upper(e.club_short_name) = upper(v_club)
      AND e.phase IN ('skip_current', 'armed', 'active')
    LIMIT 1;
    RETURN v_phase;
  END IF;

  SELECT coalesce(g.transfer_window_open, false) INTO v_open
  FROM public.global_settings g
  WHERE g.id = 1;

  v_phase := CASE WHEN v_open THEN 'skip_current' ELSE 'armed' END;

  INSERT INTO public.club_transfer_buy_embargoes (
    club_short_name, source_season_id, phase, reason, metadata
  )
  VALUES (
    v_club,
    p_season_id,
    v_phase,
    coalesce(nullif(btrim(p_reason), ''), 'ffp_eos'),
    jsonb_build_object(
      'window_open_at_create', v_open,
      'created_by', 'competition_post_eos_ffp_charges'
    )
  )
  ON CONFLICT (club_short_name) DO UPDATE
  SET source_season_id = CASE
        WHEN club_transfer_buy_embargoes.phase = 'done'
             AND club_transfer_buy_embargoes.source_season_id = excluded.source_season_id
          THEN club_transfer_buy_embargoes.source_season_id
        WHEN club_transfer_buy_embargoes.phase IN ('skip_current', 'armed', 'active')
          THEN club_transfer_buy_embargoes.source_season_id
        ELSE excluded.source_season_id
      END,
      phase = CASE
        WHEN club_transfer_buy_embargoes.phase IN ('skip_current', 'armed', 'active')
          THEN club_transfer_buy_embargoes.phase
        WHEN club_transfer_buy_embargoes.phase = 'done'
             AND club_transfer_buy_embargoes.source_season_id = excluded.source_season_id
          THEN 'done'
        ELSE excluded.phase
      END,
      reason = excluded.reason,
      completed_at = CASE
        WHEN club_transfer_buy_embargoes.phase = 'done'
             AND club_transfer_buy_embargoes.source_season_id = excluded.source_season_id
          THEN club_transfer_buy_embargoes.completed_at
        WHEN club_transfer_buy_embargoes.phase IN ('skip_current', 'armed', 'active')
          THEN club_transfer_buy_embargoes.completed_at
        ELSE NULL
      END,
      activated_at = CASE
        WHEN club_transfer_buy_embargoes.phase = 'active'
          THEN club_transfer_buy_embargoes.activated_at
        ELSE club_transfer_buy_embargoes.activated_at
      END,
      metadata = club_transfer_buy_embargoes.metadata || excluded.metadata;

  RETURN v_phase;
END;
$function$;

CREATE OR REPLACE FUNCTION public.club_transfer_buy_embargo_on_window_change()
RETURNS trigger
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $function$
BEGIN
  IF TG_OP <> 'UPDATE'
     OR NEW.transfer_window_open IS NOT DISTINCT FROM OLD.transfer_window_open THEN
    RETURN NEW;
  END IF;

  -- Window opens: armed → active (this is the banned window)
  IF NEW.transfer_window_open IS TRUE AND OLD.transfer_window_open IS NOT TRUE THEN
    UPDATE public.club_transfer_buy_embargoes
    SET phase = 'active',
        activated_at = now()
    WHERE phase = 'armed';
  END IF;

  -- Window closes:
  --   skip_current → armed (current open was skipped; next open is banned)
  --   active → done (banned window finished)
  IF NEW.transfer_window_open IS NOT TRUE AND OLD.transfer_window_open IS TRUE THEN
    UPDATE public.club_transfer_buy_embargoes
    SET phase = 'armed'
    WHERE phase = 'skip_current';

    UPDATE public.club_transfer_buy_embargoes
    SET phase = 'done',
        completed_at = now()
    WHERE phase = 'active';
  END IF;

  RETURN NEW;
END;
$function$;

DROP TRIGGER IF EXISTS trg_club_transfer_buy_embargo_window ON public.global_settings;
CREATE TRIGGER trg_club_transfer_buy_embargo_window
  AFTER UPDATE OF transfer_window_open ON public.global_settings
  FOR EACH ROW
  EXECUTE FUNCTION public.club_transfer_buy_embargo_on_window_change();

CREATE OR REPLACE FUNCTION public.trg_player_transfer_bids_ffp_buy_embargo()
RETURNS trigger
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_bidder text := btrim(coalesce(NEW.bidder_club_id::text, ''));
BEGIN
  IF v_bidder = '' THEN
    RETURN NEW;
  END IF;

  IF public.club_transfer_buy_embargo_active(v_bidder) THEN
    RAISE EXCEPTION
      '% is under a transfer buy embargo (FFP) and cannot bid or sign this window',
      v_bidder;
  END IF;

  -- Same-club FFP rejoin lock (draft / direct bids on free agents)
  IF NEW.player_id IS NOT NULL
     AND public.player_club_rejoin_is_blocked(NEW.player_id::text, v_bidder) THEN
    RAISE EXCEPTION
      'Cannot bid: player cannot rejoin % this season (FFP release lock)',
      v_bidder;
  END IF;

  IF NEW.direct_bid_id IS NOT NULL
     AND public.player_club_rejoin_is_blocked(NEW.direct_bid_id::text, v_bidder) THEN
    RAISE EXCEPTION
      'Cannot bid: player cannot rejoin % this season (FFP release lock)',
      v_bidder;
  END IF;

  RETURN NEW;
END;
$function$;


CREATE TABLE IF NOT EXISTS public.club_transfer_buy_embargoes (
  club_short_name text PRIMARY KEY,
  source_season_id bigint NOT NULL REFERENCES public.competition_seasons (id) ON DELETE CASCADE,
  phase text NOT NULL
    CHECK (phase IN ('skip_current', 'armed', 'active', 'done')),
  reason text NOT NULL DEFAULT 'ffp_eos',
  created_at timestamptz NOT NULL DEFAULT now(),
  activated_at timestamptz,
  completed_at timestamptz,
  metadata jsonb NOT NULL DEFAULT '{}'::jsonb
);

CREATE INDEX IF NOT EXISTS club_transfer_buy_embargoes_phase_idx
  ON public.club_transfer_buy_embargoes (phase)
  WHERE phase IN ('skip_current', 'armed', 'active');

COMMENT ON TABLE public.club_transfer_buy_embargoes IS
  'FFP buy ban: skip_current (window was open) → armed → active for one open window → done.';

ALTER TABLE public.club_transfer_buy_embargoes ENABLE ROW LEVEL SECURITY;

CREATE OR REPLACE FUNCTION public.club_transfer_buy_embargo_active(p_club_short_name text)
RETURNS boolean
LANGUAGE sql
STABLE
SECURITY DEFINER
SET search_path = public
AS $$
  SELECT EXISTS (
    SELECT 1
    FROM public.club_transfer_buy_embargoes e
    WHERE upper(e.club_short_name) = upper(btrim(coalesce(p_club_short_name, '')))
      AND e.phase = 'active'
  );
$$;

CREATE OR REPLACE FUNCTION public.assert_club_may_sign_player(
  p_club_short_name text,
  p_player_id text
)
RETURNS void
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $function$
BEGIN
  IF public.player_club_rejoin_is_blocked(p_player_id, p_club_short_name) THEN
    RAISE EXCEPTION
      'Player cannot rejoin % this season (FFP release lock)',
      btrim(p_club_short_name);
  END IF;

  IF public.club_transfer_buy_embargo_active(p_club_short_name) THEN
    RAISE EXCEPTION
      '% is under a transfer buy embargo (FFP) and cannot sign players this window',
      btrim(p_club_short_name);
  END IF;
END;
$function$;


-- =============================================================================
-- FFP EOS: ₿50M fine + MV releases + same-club season lock + buy embargo
--
-- Rules (Close Finances, after wages / maintenance / debt interest):
--   1) Trigger when Club_Finances.balance <= −₿100,000,000
--   2) Flat ₿50M eos_ffp_charge to central bank
--   3) While balance still <= −₿99,999,999, release highest-MV players @ MV
--      (free agent / draft) until balance > −₿99,999,999
--   4) Released players cannot rejoin that club for the following GPSL season
--   5) Buy embargo for one transfer window — the next window after Close Finances;
--      if the window is already open, skip that one and ban the following.
--      Selling / outbound listings remain allowed.
--
-- Run after stadium_maintenance_eos_and_season1_backfill.sql (or any Close Finances
-- that calls competition_post_eos_ffp_charges). Safe re-run.
-- =============================================================================

-- ---------------------------------------------------------------------------
-- Bank defaults: ₿50M fine, −₿100M trigger
-- ---------------------------------------------------------------------------

ALTER TABLE public.gpsl_bank_account
  ADD COLUMN IF NOT EXISTS eos_ffp_debt_threshold numeric(14, 2) NOT NULL DEFAULT 100000000,
  ADD COLUMN IF NOT EXISTS eos_ffp_flat_fine numeric(14, 2) NOT NULL DEFAULT 50000000,
  ADD COLUMN IF NOT EXISTS eos_ffp_clear_threshold numeric(14, 2) NOT NULL DEFAULT 99999999;

UPDATE public.gpsl_bank_account
SET eos_ffp_flat_fine = 50000000,
    eos_ffp_debt_threshold = coalesce(eos_ffp_debt_threshold, 100000000),
    eos_ffp_clear_threshold = coalesce(eos_ffp_clear_threshold, 99999999)
WHERE id = 1;

COMMENT ON COLUMN public.gpsl_bank_account.eos_ffp_debt_threshold IS
  'FFP triggers when balance <= -threshold (default ₿100M).';
COMMENT ON COLUMN public.gpsl_bank_account.eos_ffp_flat_fine IS
  'Flat FFP fine charged to central bank (default ₿50M).';
COMMENT ON COLUMN public.gpsl_bank_account.eos_ffp_clear_threshold IS
  'After fine, release MV until balance > -clear_threshold (default ₿99,999,999).';

-- ---------------------------------------------------------------------------
-- Same-club rejoin ban (following season only)
-- ---------------------------------------------------------------------------

CREATE TABLE IF NOT EXISTS public.player_club_rejoin_blocks (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  player_id text NOT NULL,
  club_short_name text NOT NULL,
  released_season_id bigint NOT NULL REFERENCES public.competition_seasons (id) ON DELETE CASCADE,
  blocked_season_id bigint REFERENCES public.competition_seasons (id) ON DELETE CASCADE,
  reason text NOT NULL DEFAULT 'ffp_eos_release',
  created_at timestamptz NOT NULL DEFAULT now(),
  CONSTRAINT player_club_rejoin_blocks_unique
    UNIQUE (player_id, club_short_name, released_season_id)
);

CREATE INDEX IF NOT EXISTS player_club_rejoin_blocks_lookup_idx
  ON public.player_club_rejoin_blocks (player_id, club_short_name);

COMMENT ON TABLE public.player_club_rejoin_blocks IS
  'Player cannot rejoin this club during blocked_season_id (following season after FFP release).';

ALTER TABLE public.player_club_rejoin_blocks ENABLE ROW LEVEL SECURITY;

CREATE OR REPLACE FUNCTION public.player_club_rejoin_resolve_blocked_season(
  p_released_season_id bigint
)
RETURNS bigint
LANGUAGE sql
STABLE
SECURITY DEFINER
SET search_path = public
AS $$
  SELECT s.id
  FROM public.competition_seasons s
  WHERE s.id > p_released_season_id
  ORDER BY s.id ASC
  LIMIT 1;
$$;

CREATE OR REPLACE FUNCTION public.player_club_rejoin_block_record(
  p_player_id text,
  p_club_short_name text,
  p_released_season_id bigint,
  p_reason text DEFAULT 'ffp_eos_release'
)
RETURNS void
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_pid text := btrim(coalesce(p_player_id, ''));
  v_club text;
  v_blocked bigint;
BEGIN
  IF v_pid = '' OR p_released_season_id IS NULL THEN
    RETURN;
  END IF;

  SELECT c."ShortName" INTO v_club
  FROM public."Clubs" c
  WHERE upper(c."ShortName") = upper(btrim(coalesce(p_club_short_name, '')))
  LIMIT 1;

  IF v_club IS NULL THEN
    v_club := btrim(p_club_short_name);
  END IF;

  IF v_club IS NULL OR v_club = '' THEN
    RETURN;
  END IF;

  v_blocked := public.player_club_rejoin_resolve_blocked_season(p_released_season_id);

  INSERT INTO public.player_club_rejoin_blocks (
    player_id, club_short_name, released_season_id, blocked_season_id, reason
  )
  VALUES (
    v_pid, v_club, p_released_season_id, v_blocked, coalesce(nullif(btrim(p_reason), ''), 'ffp_eos_release')
  )
  ON CONFLICT (player_id, club_short_name, released_season_id)
  DO UPDATE SET
    blocked_season_id = coalesce(
      excluded.blocked_season_id,
      public.player_club_rejoin_blocks.blocked_season_id
    ),
    reason = excluded.reason;
END;
$function$;

CREATE OR REPLACE FUNCTION public.player_club_rejoin_is_blocked(
  p_player_id text,
  p_club_short_name text,
  p_season_id bigint DEFAULT NULL
)
RETURNS boolean
LANGUAGE plpgsql
VOLATILE
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_pid text := btrim(coalesce(p_player_id, ''));
  v_club text := upper(btrim(coalesce(p_club_short_name, '')));
  v_season bigint := p_season_id;
  v_row record;
  v_blocked bigint;
BEGIN
  IF v_pid = '' OR v_club = '' THEN
    RETURN false;
  END IF;

  IF v_season IS NULL THEN
    SELECT id INTO v_season
    FROM public.competition_seasons
    WHERE is_current = true
    ORDER BY id DESC
    LIMIT 1;
  END IF;

  IF v_season IS NULL THEN
    RETURN false;
  END IF;

  FOR v_row IN
    SELECT *
    FROM public.player_club_rejoin_blocks b
    WHERE b.player_id = v_pid
      AND upper(b.club_short_name) = v_club
  LOOP
    v_blocked := v_row.blocked_season_id;
    IF v_blocked IS NULL THEN
      v_blocked := public.player_club_rejoin_resolve_blocked_season(v_row.released_season_id);
      IF v_blocked IS NOT NULL THEN
        UPDATE public.player_club_rejoin_blocks
        SET blocked_season_id = v_blocked
        WHERE id = v_row.id;
      END IF;
    END IF;

    IF v_blocked IS NOT NULL AND v_blocked = v_season THEN
      RETURN true;
    END IF;
  END LOOP;

  RETURN false;
END;
$function$;


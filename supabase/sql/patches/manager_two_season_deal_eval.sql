CREATE OR REPLACE FUNCTION public.owner_inbox_notify_manager_season_end(p_results jsonb)
RETURNS void
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_row jsonb;
  v_club text;
  v_action text;
  v_body text;
  v_title text;
BEGIN
  IF p_results IS NULL OR jsonb_typeof(p_results) <> 'array' THEN
    RETURN;
  END IF;

  FOR v_row IN SELECT * FROM jsonb_array_elements(p_results)
  LOOP
    v_club := v_row ->> 'club';
    v_action := v_row ->> 'action';
    IF v_club IS NULL OR v_club = '' THEN
      CONTINUE;
    END IF;

    v_title := CASE v_action
      WHEN 'renewal_available' THEN 'Manager renewal available'
      WHEN 'released_failed_deal' THEN 'Manager released'
      WHEN 'released' THEN 'Manager released'
      WHEN 'season_tick' THEN 'Manager season reviewed'
      WHEN 'awaiting_renewal' THEN 'Manager renewal pending'
      ELSE 'Manager update'
    END;

    v_body := CASE v_action
      WHEN 'season_tick' THEN format(
        'Your manager finished %s (%s). Contract continues — %s season(s) remaining on this deal.',
        coalesce(v_row ->> 'position', '?'),
        CASE WHEN (v_row ->> 'target_met') = 'true' THEN 'target met' WHEN (v_row ->> 'target_met') = 'false' THEN 'target missed' ELSE 'no evaluation' END,
        coalesce(v_row ->> 'seasons_remaining', '?')
      )
      WHEN 'renewal_available' THEN format(
        'Your manager completed their 2-season deal (finished %s this season). They hit their target in at least one season — renew them from Club Details or Squad.',
        coalesce(v_row ->> 'position', '?')
      )
      WHEN 'released_failed_deal' THEN format(
        'Your manager missed their target in both seasons of the deal (finished %s this season). They have been released for market value; your club cannot re-sign them for two seasons.',
        coalesce(v_row ->> 'position', '?')
      )
      WHEN 'released' THEN format(
        'Your manager missed the league target (finished %s). They have been released; your club received market-value compensation.',
        coalesce(v_row ->> 'position', '?')
      )
      WHEN 'awaiting_renewal' THEN
        'Your manager is still awaiting a renewal decision on Club Details / Squad.'
      ELSE format('Manager review: %s', v_action)
    END;

    PERFORM public.owner_inbox_send(
      'season_overview',
      v_title,
      v_body,
      v_club,
      NULL, NULL, NULL, NULL, NULL,
      'club_details.html',
      format('mgr_end:%s:%s:%s', v_club, v_action, coalesce(v_row ->> 'manager_id', '')),
      NULL, NULL
    );
  END LOOP;
END;
$function$;

DROP VIEW IF EXISTS public.manager_club_status_public;

CREATE VIEW public.manager_club_status_public
WITH (security_invoker = true)
AS
SELECT
  c."ShortName" AS club_short_name,
  m.id AS manager_id,
  m.name AS manager_name,
  m.rating AS manager_rating,
  m.market_value,
  m.contract_seasons_remaining,
  m.weekly_wage,
  m.pending_owner_renewal,
  m.deal_start_season_id,
  c.manager_sacks_remaining,
  coalesce(pos.division, ccs.division) AS division,
  pos.season_position,
  t.target_kind,
  t.target_value,
  t.label AS target_label,
  public.manager_target_met(
    t,
    pos.season_position,
    coalesce(pos.division, ccs.division)
  ) AS target_met,
  public.manager_boost_band_label(1, e.boost1_min, e.boost1_max) AS boost1_label,
  public.manager_boost_band_label(2, e.boost2_min, e.boost2_max) AS boost2_label,
  public.manager_boost_band_label(3, e.boost3_min, e.boost3_max) AS boost3_label,
  (
    SELECT count(*)::int
    FROM public.manager_deal_season_results r
    WHERE r.manager_id = m.id
      AND r.club_short_name = c."ShortName"
      AND r.deal_start_season_id = coalesce(m.deal_start_season_id, m.signed_season_id)
      AND r.target_met IS TRUE
  ) AS deal_target_hits,
  (
    SELECT count(*)::int
    FROM public.manager_deal_season_results r
    WHERE r.manager_id = m.id
      AND r.club_short_name = c."ShortName"
      AND r.deal_start_season_id = coalesce(m.deal_start_season_id, m.signed_season_id)
      AND r.target_met IS FALSE
  ) AS deal_target_misses
FROM public."Clubs" c
LEFT JOIN public."Managers" m ON m.id = c.manager_id
LEFT JOIN public.competition_seasons s ON s.is_current = true
LEFT JOIN public.competition_club_seasons ccs
  ON ccs.club_short_name = c."ShortName" AND ccs.season_id = s.id
LEFT JOIN LATERAL public.manager_club_season_position(s.id, c."ShortName") pos ON s.id IS NOT NULL
LEFT JOIN public.manager_rating_targets t
  ON m.id IS NOT NULL
  AND coalesce(pos.division, ccs.division) IS NOT NULL
  AND m.rating BETWEEN t.min_rating AND t.max_rating
  AND t.division = coalesce(pos.division, ccs.division)
  AND t.id = (
    SELECT t2.id
    FROM public.manager_rating_targets t2
    WHERE t2.division = coalesce(pos.division, ccs.division)
      AND m.rating BETWEEN t2.min_rating AND t2.max_rating
    ORDER BY t2.sort_order, t2.id
    LIMIT 1
  )
LEFT JOIN public.manager_proficiency_expectancy e
  ON m.id IS NOT NULL
  AND e.proficiency = public.manager_proficiency_clamp(m.rating);

GRANT SELECT ON public.manager_club_status_public TO authenticated;


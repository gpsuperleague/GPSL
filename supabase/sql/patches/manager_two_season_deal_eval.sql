-- =============================================================================
-- Manager 2-season deals
--
-- • Stay for the full contract (default 2 seasons) — no mid-deal auto fire/renew
-- • Each season records target hit/miss
-- • At deal end: failed BOTH seasons → released for MV + 2-season rehire ban
--               hit at least ONE → pending owner renewal (club_details / squad)
-- Safe re-run.
-- =============================================================================

ALTER TABLE public."Managers"
  ADD COLUMN IF NOT EXISTS deal_start_season_id bigint
    REFERENCES public.competition_seasons (id) ON DELETE SET NULL,
  ADD COLUMN IF NOT EXISTS pending_owner_renewal boolean NOT NULL DEFAULT false;

CREATE TABLE IF NOT EXISTS public.manager_deal_season_results (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  manager_id bigint NOT NULL REFERENCES public."Managers" (id) ON DELETE CASCADE,
  club_short_name text NOT NULL,
  deal_start_season_id bigint NOT NULL REFERENCES public.competition_seasons (id) ON DELETE CASCADE,
  season_id bigint NOT NULL REFERENCES public.competition_seasons (id) ON DELETE CASCADE,
  division text,
  final_position smallint,
  target_kind text,
  target_value smallint,
  target_label text,
  target_met boolean,
  recorded_at timestamptz NOT NULL DEFAULT now(),
  CONSTRAINT manager_deal_season_results_uidx
    UNIQUE (manager_id, club_short_name, deal_start_season_id, season_id)
);

CREATE INDEX IF NOT EXISTS manager_deal_season_results_club_idx
  ON public.manager_deal_season_results (club_short_name, season_id DESC);

ALTER TABLE public.manager_deal_season_results ENABLE ROW LEVEL SECURITY;

DROP POLICY IF EXISTS manager_deal_season_results_select ON public.manager_deal_season_results;
CREATE POLICY manager_deal_season_results_select
  ON public.manager_deal_season_results FOR SELECT TO authenticated
  USING (true);

GRANT SELECT ON public.manager_deal_season_results TO authenticated;
GRANT ALL ON public.manager_deal_season_results TO service_role;

-- Multi-season rehire ban (failed both targets, or extendable for sacks)
CREATE TABLE IF NOT EXISTS public.manager_club_rehire_blocks (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  club_short_name text NOT NULL,
  manager_id bigint NOT NULL REFERENCES public."Managers" (id) ON DELETE CASCADE,
  reason text NOT NULL DEFAULT 'failed_targets',
  blocked_from_season_id bigint NOT NULL REFERENCES public.competition_seasons (id) ON DELETE CASCADE,
  blocked_through_season_id bigint NOT NULL REFERENCES public.competition_seasons (id) ON DELETE CASCADE,
  created_at timestamptz NOT NULL DEFAULT now(),
  CONSTRAINT manager_club_rehire_blocks_uidx UNIQUE (club_short_name, manager_id)
);

CREATE INDEX IF NOT EXISTS manager_club_rehire_blocks_club_idx
  ON public.manager_club_rehire_blocks (club_short_name);

ALTER TABLE public.manager_club_rehire_blocks ENABLE ROW LEVEL SECURITY;

DROP POLICY IF EXISTS manager_club_rehire_blocks_select ON public.manager_club_rehire_blocks;
CREATE POLICY manager_club_rehire_blocks_select
  ON public.manager_club_rehire_blocks FOR SELECT TO authenticated
  USING (true);

GRANT SELECT ON public.manager_club_rehire_blocks TO authenticated;
GRANT ALL ON public.manager_club_rehire_blocks TO service_role;

-- Backfill deal start for current contracts
UPDATE public."Managers" m
SET deal_start_season_id = coalesce(m.deal_start_season_id, m.signed_season_id),
    pending_owner_renewal = coalesce(m.pending_owner_renewal, false)
WHERE m.contracted_club IS NOT NULL;

-- Keep deal_start in sync when a new contract is signed
CREATE OR REPLACE FUNCTION public.managers_trg_deal_on_assign()
RETURNS trigger
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $function$
BEGIN
  IF NEW.contracted_club IS NOT NULL
     AND btrim(NEW.contracted_club) <> ''
     AND (
       TG_OP = 'INSERT'
       OR OLD.contracted_club IS DISTINCT FROM NEW.contracted_club
       OR OLD.signed_season_id IS DISTINCT FROM NEW.signed_season_id
     ) THEN
    NEW.deal_start_season_id := coalesce(NEW.signed_season_id, NEW.deal_start_season_id);
    NEW.pending_owner_renewal := false;
  END IF;

  IF NEW.contracted_club IS NULL OR btrim(coalesce(NEW.contracted_club, '')) = '' THEN
    NEW.pending_owner_renewal := false;
  END IF;

  RETURN NEW;
END;
$function$;

DROP TRIGGER IF EXISTS trg_managers_deal_on_assign ON public."Managers";
CREATE TRIGGER trg_managers_deal_on_assign
  BEFORE INSERT OR UPDATE OF contracted_club, signed_season_id
  ON public."Managers"
  FOR EACH ROW
  EXECUTE FUNCTION public.managers_trg_deal_on_assign();

CREATE OR REPLACE FUNCTION public.manager_rehire_block_record(
  p_club_short text,
  p_manager_id bigint,
  p_from_season_id bigint,
  p_seasons int DEFAULT 2,
  p_reason text DEFAULT 'failed_targets'
)
RETURNS void
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_club text;
  v_from bigint;
  v_through bigint;
  v_n int := greatest(coalesce(p_seasons, 2), 1);
BEGIN
  v_club := public.manager_club_short_canonical(p_club_short);
  IF v_club IS NULL OR p_manager_id IS NULL OR p_from_season_id IS NULL THEN
    RETURN;
  END IF;

  -- Ban covers the next v_n seasons after the evaluation season
  SELECT s.id INTO v_from
  FROM public.competition_seasons s
  WHERE s.id > p_from_season_id
  ORDER BY s.id
  LIMIT 1;

  SELECT s.id INTO v_through
  FROM public.competition_seasons s
  WHERE s.id > p_from_season_id
  ORDER BY s.id
  OFFSET (v_n - 1)
  LIMIT 1;

  IF v_from IS NULL THEN
    v_from := p_from_season_id + 1;
  END IF;
  IF v_through IS NULL THEN
    v_through := p_from_season_id + v_n;
  END IF;

  INSERT INTO public.manager_club_rehire_blocks (
    club_short_name, manager_id, reason,
    blocked_from_season_id, blocked_through_season_id
  )
  VALUES (v_club, p_manager_id, coalesce(p_reason, 'failed_targets'), v_from, v_through)
  ON CONFLICT (club_short_name, manager_id) DO UPDATE
    SET reason = excluded.reason,
        blocked_from_season_id = least(
          public.manager_club_rehire_blocks.blocked_from_season_id,
          excluded.blocked_from_season_id
        ),
        blocked_through_season_id = greatest(
          public.manager_club_rehire_blocks.blocked_through_season_id,
          excluded.blocked_through_season_id
        ),
        created_at = now();
END;
$function$;

CREATE OR REPLACE FUNCTION public.manager_club_rehire_blocked(
  p_club_short text,
  p_manager_id bigint,
  p_season_id bigint DEFAULT NULL
)
RETURNS boolean
LANGUAGE plpgsql
STABLE
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_club text := upper(btrim(coalesce(p_club_short, '')));
  v_season_id bigint := p_season_id;
BEGIN
  IF v_club = '' OR p_manager_id IS NULL THEN
    RETURN false;
  END IF;

  IF v_season_id IS NULL THEN
    SELECT id INTO v_season_id
    FROM public.competition_seasons
    WHERE is_current = true
    ORDER BY id DESC
    LIMIT 1;
  END IF;

  IF v_season_id IS NULL THEN
    RETURN false;
  END IF;

  RETURN EXISTS (
    SELECT 1
    FROM public.manager_club_rehire_blocks b
    WHERE upper(b.club_short_name) = v_club
      AND b.manager_id = p_manager_id
      AND v_season_id >= b.blocked_from_season_id
      AND v_season_id <= b.blocked_through_season_id
  );
END;
$function$;

-- Extend assert used by sign/draft/bids
CREATE OR REPLACE FUNCTION public.manager_assert_not_sack_blocked(
  p_club_short text,
  p_manager_id bigint
)
RETURNS void
LANGUAGE plpgsql
STABLE
SECURITY DEFINER
SET search_path = public
AS $function$
BEGIN
  IF public.manager_club_was_sacked_this_season(p_club_short, p_manager_id) THEN
    RAISE EXCEPTION
      'You cannot re-sign a manager you sacked this season — try again next season';
  END IF;

  IF public.manager_club_rehire_blocked(p_club_short, p_manager_id, NULL) THEN
    RAISE EXCEPTION
      'You cannot re-sign this manager for two seasons after a failed two-season contract';
  END IF;
END;
$function$;

CREATE OR REPLACE FUNCTION public.manager_process_season_end()
RETURNS jsonb
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_season public.competition_seasons;
  v_mgr public."Managers"%rowtype;
  v_division text;
  v_pos smallint;
  v_target public.manager_rating_targets;
  v_met boolean;
  v_deal bigint;
  v_hits int;
  v_misses int;
  v_fail_club text;
  v_results jsonb := '[]'::jsonb;
  v_row jsonb;
BEGIN
  SELECT * INTO v_season
  FROM public.competition_seasons
  WHERE is_current = true
  ORDER BY id DESC
  LIMIT 1;

  IF NOT FOUND THEN
    RETURN jsonb_build_object('ok', false, 'reason', 'no_season', 'results', '[]'::jsonb);
  END IF;

  FOR v_mgr IN
    SELECT * FROM public."Managers"
    WHERE contracted_club IS NOT NULL
      AND btrim(contracted_club) <> ''
      AND (
        contract_seasons_remaining > 0
        OR pending_owner_renewal IS TRUE
      )
  LOOP
    -- Already awaiting owner decision — leave alone
    IF coalesce(v_mgr.pending_owner_renewal, false)
       AND coalesce(v_mgr.contract_seasons_remaining, 0) = 0 THEN
      v_results := v_results || jsonb_build_array(jsonb_build_object(
        'manager_id', v_mgr.id,
        'club', v_mgr.contracted_club,
        'action', 'awaiting_renewal'
      ));
      CONTINUE;
    END IF;

    SELECT cs.division, cs.season_position
    INTO v_division, v_pos
    FROM public.manager_club_season_position(v_season.id, v_mgr.contracted_club) cs;

    v_target := public.manager_target_for(v_mgr.rating, coalesce(v_division, 'championship_a'));
    v_met := public.manager_target_met(v_target, v_pos, v_division);
    v_deal := coalesce(v_mgr.deal_start_season_id, v_mgr.signed_season_id, v_season.id);

    INSERT INTO public.manager_deal_season_results (
      manager_id, club_short_name, deal_start_season_id, season_id,
      division, final_position, target_kind, target_value, target_label, target_met
    )
    VALUES (
      v_mgr.id, v_mgr.contracted_club, v_deal, v_season.id,
      v_division, v_pos,
      v_target.target_kind, v_target.target_value, v_target.label, v_met
    )
    ON CONFLICT (manager_id, club_short_name, deal_start_season_id, season_id)
    DO UPDATE SET
      division = excluded.division,
      final_position = excluded.final_position,
      target_kind = excluded.target_kind,
      target_value = excluded.target_value,
      target_label = excluded.target_label,
      target_met = excluded.target_met,
      recorded_at = now();

    IF coalesce(v_mgr.contract_seasons_remaining, 0) > 1 THEN
      -- Mid-deal: tick down only
      UPDATE public."Managers"
      SET contract_seasons_remaining = contract_seasons_remaining - 1,
          deal_start_season_id = v_deal,
          pending_owner_renewal = false,
          updated_at = now()
      WHERE id = v_mgr.id;

      v_row := jsonb_build_object(
        'manager_id', v_mgr.id,
        'club', v_mgr.contracted_club,
        'action', 'season_tick',
        'position', v_pos,
        'target_met', v_met,
        'seasons_remaining', v_mgr.contract_seasons_remaining - 1
      );
      ELSE
      -- End of deal
      SELECT
        count(*) FILTER (WHERE target_met IS TRUE)::int,
        count(*) FILTER (WHERE target_met IS FALSE)::int
      INTO v_hits, v_misses
      FROM public.manager_deal_season_results r
      WHERE r.manager_id = v_mgr.id
        AND r.club_short_name = v_mgr.contracted_club
        AND r.deal_start_season_id = v_deal;

      IF v_hits > 0 THEN
        UPDATE public."Managers"
        SET contract_seasons_remaining = 0,
            pending_owner_renewal = true,
            deal_start_season_id = v_deal,
            updated_at = now()
        WHERE id = v_mgr.id;

        v_row := jsonb_build_object(
          'manager_id', v_mgr.id,
          'club', v_mgr.contracted_club,
          'action', 'renewal_available',
          'position', v_pos,
          'target_met', v_met,
          'hits', v_hits,
          'misses', v_misses
        );
      ELSE
        -- Failed every evaluated season in the deal
        v_fail_club := v_mgr.contracted_club;
        PERFORM public.manager_release_from_club(
          v_mgr.id,
          v_fail_club,
          v_mgr.market_value::numeric,
          'transfer_sale'
        );
        PERFORM public.manager_rehire_block_record(
          v_fail_club, v_mgr.id, v_season.id, 2, 'failed_targets'
        );

        v_row := jsonb_build_object(
          'manager_id', v_mgr.id,
          'club', v_fail_club,
          'action', 'released_failed_deal',
          'position', v_pos,
          'target_met', v_met,
          'hits', v_hits,
          'misses', v_misses,
          'payout', v_mgr.market_value
        );
      END IF;
    END IF;

    v_results := v_results || jsonb_build_array(v_row);
  END LOOP;

  RETURN jsonb_build_object('ok', true, 'season_id', v_season.id, 'results', v_results);
END;
$function$;

GRANT EXECUTE ON FUNCTION public.manager_process_season_end() TO authenticated;

CREATE OR REPLACE FUNCTION public.manager_owner_renew()
RETURNS jsonb
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_club text := public.my_club_shortname();
  v_mgr public."Managers"%rowtype;
  v_season_id bigint;
BEGIN
  IF v_club IS NULL OR btrim(v_club) = '' THEN
    RAISE EXCEPTION 'No club';
  END IF;

  SELECT * INTO v_mgr
  FROM public."Managers"
  WHERE contracted_club = v_club
    AND pending_owner_renewal IS TRUE
  LIMIT 1;

  IF NOT FOUND THEN
    RAISE EXCEPTION 'No manager renewal pending for your club';
  END IF;

  SELECT id INTO v_season_id
  FROM public.competition_seasons
  WHERE is_current = true
  ORDER BY id DESC
  LIMIT 1;

  UPDATE public."Managers"
  SET contract_seasons_remaining = 2,
      pending_owner_renewal = false,
      signed_season_id = v_season_id,
      deal_start_season_id = v_season_id,
      weekly_wage = public.manager_weekly_wage_for(market_value),
      updated_at = now()
  WHERE id = v_mgr.id;

  RETURN jsonb_build_object(
    'ok', true,
    'action', 'renewed',
    'manager_id', v_mgr.id,
    'club', v_club,
    'contract_seasons_remaining', 2
  );
END;
$function$;


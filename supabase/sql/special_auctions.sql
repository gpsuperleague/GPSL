-- =============================================================================
-- Special auctions: lowest unique bid + snap auction
-- Run once in Supabase SQL Editor.
-- =============================================================================

CREATE TABLE IF NOT EXISTS public.special_auctions (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  auction_type text NOT NULL CHECK (auction_type IN ('lowest_unique', 'snap')),
  title text NOT NULL,
  status text NOT NULL DEFAULT 'draft' CHECK (
    status IN ('draft', 'active', 'revealed', 'settled', 'cancelled')
  ),
  start_time timestamptz NOT NULL,
  end_time timestamptz NOT NULL,
  prize_type text NOT NULL CHECK (prize_type IN ('player', 'cash', 'discount')),
  prize_player_id text,
  prize_cash_amount numeric,
  prize_discount_label text,
  player_mode text CHECK (player_mode IN ('known', 'mystery')),
  mystery_clue text,
  known_player_id text,
  snap_loss_fee numeric NOT NULL DEFAULT 250000,
  snap_win_fee numeric NOT NULL DEFAULT 500000,
  winning_club_id text,
  winning_amount numeric,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now()
);

CREATE TABLE IF NOT EXISTS public.special_auction_bids (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  auction_id bigint NOT NULL REFERENCES public.special_auctions (id) ON DELETE CASCADE,
  club_id text NOT NULL,
  owner_id uuid NOT NULL,
  bid_amount numeric NOT NULL CHECK (bid_amount > 0),
  bid_time timestamptz NOT NULL DEFAULT now(),
  is_winner boolean NOT NULL DEFAULT false,
  fee_charged numeric,
  UNIQUE (auction_id, club_id)
);

CREATE INDEX IF NOT EXISTS special_auction_bids_auction_id_idx
  ON public.special_auction_bids (auction_id);

CREATE UNIQUE INDEX IF NOT EXISTS special_auctions_one_active_idx
  ON public.special_auctions ((true))
  WHERE status = 'active';


CREATE OR REPLACE FUNCTION public.is_gpsl_admin()
RETURNS boolean
LANGUAGE sql
STABLE
SECURITY DEFINER
SET search_path = public
AS $$
  SELECT coalesce(auth.jwt() ->> 'email', '') = 'rotavator66@outlook.com';
$$;

CREATE OR REPLACE FUNCTION public.my_club_shortname()
RETURNS text
LANGUAGE sql
STABLE
SECURITY DEFINER
SET search_path = public
AS $$
  SELECT "ShortName"
  FROM public."Clubs"
  WHERE owner_id = auth.uid()
  LIMIT 1;
$$;

CREATE OR REPLACE FUNCTION public.round_bid_to_million(p_amount numeric)
RETURNS numeric
LANGUAGE sql
IMMUTABLE
AS $$
  SELECT round(p_amount / 1000000.0) * 1000000;
$$;

CREATE OR REPLACE FUNCTION public.special_auction_can_view_bid(
  p_auction_id bigint,
  p_club_id text
)
RETURNS boolean
LANGUAGE sql
STABLE
SECURITY DEFINER
SET search_path = public
AS $$
  SELECT EXISTS (
    SELECT 1
    FROM public.special_auctions a
    WHERE a.id = p_auction_id
      AND (
        public.is_gpsl_admin()
        OR a.status IN ('revealed', 'settled')
        OR (a.auction_type = 'snap' AND a.status = 'active')
        OR (
          p_club_id IS NOT NULL
          AND p_club_id = public.my_club_shortname()
        )
      )
  );
$$;


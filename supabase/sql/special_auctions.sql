  FOR INSERT TO authenticated
  WITH CHECK (
    club_id = public.my_club_shortname()
    AND owner_id = auth.uid()
    AND EXISTS (
      SELECT 1 FROM public.special_auctions a
      WHERE a.id = auction_id
        AND a.status = 'active'
        AND now() >= a.start_time
        AND now() < a.end_time
    )
  );


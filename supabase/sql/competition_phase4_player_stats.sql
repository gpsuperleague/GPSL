CREATE POLICY competition_match_player_stats_admin ON public.competition_match_player_stats
  FOR ALL
  TO authenticated
  USING (public.is_gpsl_admin())
  WITH CHECK (public.is_gpsl_admin());

GRANT SELECT ON public.competition_match_player_stats TO authenticated;


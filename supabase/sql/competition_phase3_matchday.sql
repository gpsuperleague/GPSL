ALTER TABLE public.competition_result_submissions ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.competition_inbox ENABLE ROW LEVEL SECURITY;

DROP POLICY IF EXISTS competition_result_submissions_select ON public.competition_result_submissions;
CREATE POLICY competition_result_submissions_select ON public.competition_result_submissions
  FOR SELECT
  TO authenticated
  USING (true);

DROP POLICY IF EXISTS competition_result_submissions_admin ON public.competition_result_submissions;
CREATE POLICY competition_result_submissions_admin ON public.competition_result_submissions
  FOR ALL
  TO authenticated
  USING (public.is_gpsl_admin())
  WITH CHECK (public.is_gpsl_admin());

DROP POLICY IF EXISTS competition_inbox_select ON public.competition_inbox;
CREATE POLICY competition_inbox_select ON public.competition_inbox
  FOR SELECT
  TO authenticated
  USING (recipient_club_short_name = public.my_club_shortname());

DROP POLICY IF EXISTS competition_inbox_update ON public.competition_inbox;
CREATE POLICY competition_inbox_update ON public.competition_inbox
  FOR UPDATE
  TO authenticated
  USING (recipient_club_short_name = public.my_club_shortname())
  WITH CHECK (recipient_club_short_name = public.my_club_shortname());

DROP POLICY IF EXISTS competition_inbox_admin ON public.competition_inbox;
CREATE POLICY competition_inbox_admin ON public.competition_inbox
  FOR ALL
  TO authenticated
  USING (public.is_gpsl_admin())
  WITH CHECK (public.is_gpsl_admin());

GRANT SELECT ON public.competition_result_submissions TO authenticated;
GRANT SELECT ON public.competition_inbox TO authenticated;

GRANT EXECUTE ON FUNCTION public.competition_submit_result(bigint, smallint, smallint) TO authenticated;
GRANT EXECUTE ON FUNCTION public.competition_confirm_result(bigint) TO authenticated;
GRANT EXECUTE ON FUNCTION public.competition_reject_result(bigint, text) TO authenticated;
GRANT EXECUTE ON FUNCTION public.competition_inbox_mark_read(bigint) TO authenticated;

-- Admin override: confirm pending or apply directly
CREATE OR REPLACE FUNCTION public.competition_admin_confirm_submission(p_submission_id bigint)
RETURNS void
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_sub public.competition_result_submissions;
BEGIN
  IF NOT public.is_gpsl_admin() THEN
    RAISE EXCEPTION 'Admin only';
  END IF;

  SELECT * INTO v_sub
  FROM public.competition_result_submissions
  WHERE id = p_submission_id AND status = 'pending';

  IF NOT FOUND THEN
    RAISE EXCEPTION 'Pending submission not found';
  END IF;

  UPDATE public.competition_fixtures
  SET home_goals = v_sub.home_goals,
      away_goals = v_sub.away_goals,
      status = 'played'
  WHERE id = v_sub.fixture_id;

  UPDATE public.competition_result_submissions
  SET status = 'confirmed',
      responded_by_club = 'ADMIN',
      responded_at = now()
  WHERE id = p_submission_id;
END;
$function$;

GRANT EXECUTE ON FUNCTION public.competition_admin_confirm_submission(bigint) TO authenticated;

ALTER TABLE public.competition_seasons ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.competition_club_seasons ENABLE ROW LEVEL SECURITY;

DROP POLICY IF EXISTS competition_seasons_select ON public.competition_seasons;
CREATE POLICY competition_seasons_select ON public.competition_seasons
  FOR SELECT
  TO authenticated
  USING (status IN ('active', 'setup'));

DROP POLICY IF EXISTS competition_seasons_admin ON public.competition_seasons;
CREATE POLICY competition_seasons_admin ON public.competition_seasons
  FOR ALL
  TO authenticated
  USING (public.is_gpsl_admin())
  WITH CHECK (public.is_gpsl_admin());

DROP POLICY IF EXISTS competition_club_seasons_select ON public.competition_club_seasons;
CREATE POLICY competition_club_seasons_select ON public.competition_club_seasons
  FOR SELECT
  TO authenticated
  USING (
    EXISTS (
      SELECT 1
      FROM public.competition_seasons s
      WHERE s.id = season_id
        AND s.status IN ('active', 'setup')
    )
  );

DROP POLICY IF EXISTS competition_club_seasons_admin ON public.competition_club_seasons;
CREATE POLICY competition_club_seasons_admin ON public.competition_club_seasons
  FOR ALL
  TO authenticated
  USING (public.is_gpsl_admin())
  WITH CHECK (public.is_gpsl_admin());

GRANT SELECT ON public.competition_seasons TO authenticated;
GRANT SELECT ON public.competition_club_seasons TO authenticated;


CREATE TABLE IF NOT EXISTS public.competition_seasons (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  label text NOT NULL,
  status text NOT NULL DEFAULT 'setup' CHECK (status IN ('setup', 'active', 'complete')),
  is_current boolean NOT NULL DEFAULT false,
  championship_drawn_at timestamptz,
  started_at timestamptz,
  ended_at timestamptz,
  created_at timestamptz NOT NULL DEFAULT now(),
  CONSTRAINT competition_seasons_label_key UNIQUE (label)
);

CREATE UNIQUE INDEX IF NOT EXISTS competition_seasons_one_current_idx
  ON public.competition_seasons (is_current)
  WHERE is_current = true;

CREATE TABLE IF NOT EXISTS public.competition_club_seasons (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  season_id bigint NOT NULL REFERENCES public.competition_seasons (id) ON DELETE CASCADE,
  club_short_name text NOT NULL REFERENCES public."Clubs" ("ShortName"),
  division text NOT NULL DEFAULT 'unassigned' CHECK (
    division IN (
      'unassigned',
      'superleague',
      'championship_pool',
      'championship_a',
      'championship_b'
    )
  ),
  league_position smallint CHECK (
    league_position IS NULL OR (league_position >= 1 AND league_position <= 20)
  ),
  created_at timestamptz NOT NULL DEFAULT now(),
  CONSTRAINT competition_club_seasons_unique UNIQUE (season_id, club_short_name)
);

CREATE INDEX IF NOT EXISTS competition_club_seasons_season_div_idx
  ON public.competition_club_seasons (season_id, division);


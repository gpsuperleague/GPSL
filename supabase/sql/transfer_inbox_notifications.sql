-- Stable id on Transfer_History (needed for inbox links / dedupe)
DO $id$
BEGIN
  IF NOT EXISTS (
    SELECT 1
    FROM information_schema.columns
    WHERE table_schema = 'public'
      AND table_name = 'Transfer_History'
      AND column_name = 'id'
  ) THEN
    ALTER TABLE public."Transfer_History"
      ADD COLUMN id bigint GENERATED BY DEFAULT AS IDENTITY;
  END IF;
END;
$id$;

ALTER TABLE public.competition_inbox
  ADD COLUMN IF NOT EXISTS transfer_history_id bigint,
  ADD COLUMN IF NOT EXISTS transfer_listing_id bigint;

ALTER TABLE public.competition_inbox
  DROP CONSTRAINT IF EXISTS competition_inbox_message_type_check;

ALTER TABLE public.competition_inbox
  ADD CONSTRAINT competition_inbox_message_type_check
  CHECK (
    message_type IN (
      'result_to_confirm',
      'result_rejected',
      'result_confirmed',
      'transfer_signed',
      'transfer_sold'
    )
  );

CREATE INDEX IF NOT EXISTS competition_inbox_transfer_history_idx
  ON public.competition_inbox (transfer_history_id)
  WHERE transfer_history_id IS NOT NULL;


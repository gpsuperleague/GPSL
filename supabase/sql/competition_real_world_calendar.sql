CREATE OR REPLACE FUNCTION public.competition_gpsl_month_sort(p_month text)
RETURNS smallint
LANGUAGE sql
IMMUTABLE
AS $$
  SELECT CASE p_month
    WHEN 'august' THEN 1
    WHEN 'september' THEN 2
    WHEN 'october' THEN 3
    WHEN 'november' THEN 4
    WHEN 'december' THEN 5
    WHEN 'january' THEN 6
    WHEN 'february' THEN 7
    WHEN 'march' THEN 8
    WHEN 'april' THEN 9
    WHEN 'may' THEN 10
    ELSE NULL
  END;
$$;

CREATE OR REPLACE FUNCTION public.competition_gpsl_month_label(p_month text)
RETURNS text
LANGUAGE sql
IMMUTABLE
AS $$
  SELECT initcap(p_month);
$$;

/** Active GPSL month for season at p_at (default now()), or NULL if none / calendar off. */
CREATE OR REPLACE FUNCTION public.competition_active_gpsl_month(
  p_season_id bigint,
  p_at timestamptz DEFAULT now()
)
RETURNS text
LANGUAGE plpgsql
STABLE
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_month text;
BEGIN
  IF NOT EXISTS (
    SELECT 1 FROM public.competition_season_calendar_config c
    WHERE c.season_id = p_season_id
  ) THEN
    RETURN NULL;
  END IF;

  SELECT m.gpsl_month INTO v_month
  FROM public.competition_season_calendar m
  WHERE m.season_id = p_season_id
    AND p_at >= m.unlock_at
    AND p_at < m.lock_at
  ORDER BY m.sort_order
  LIMIT 1;

  RETURN v_month;
END;
$function$;

CREATE OR REPLACE FUNCTION public.competition_assert_fixture_month_unlocked(p_fixture_id bigint)
RETURNS void
LANGUAGE plpgsql
STABLE
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_fixture public.competition_fixtures;
  v_active text;
  v_unlock timestamptz;
  v_lock timestamptz;
BEGIN
  IF public.is_gpsl_admin() THEN
    RETURN;
  END IF;

  SELECT f.* INTO v_fixture
  FROM public.competition_fixtures f
  WHERE f.id = p_fixture_id;

  IF NOT FOUND THEN
    RAISE EXCEPTION 'Fixture not found';
  END IF;

  IF NOT EXISTS (
    SELECT 1 FROM public.competition_season_calendar_config c
    WHERE c.season_id = v_fixture.season_id
  ) THEN
    RETURN;
  END IF;

  v_active := public.competition_active_gpsl_month(v_fixture.season_id, now());

  IF v_active IS NOT NULL AND v_active = v_fixture.gpsl_month THEN
    RETURN;
  END IF;

  SELECT unlock_at, lock_at
  INTO v_unlock, v_lock
  FROM public.competition_season_calendar m
  WHERE m.season_id = v_fixture.season_id
    AND m.gpsl_month = v_fixture.gpsl_month;

  IF NOT FOUND THEN
    RAISE EXCEPTION 'No calendar window for GPSL %', public.competition_gpsl_month_label(v_fixture.gpsl_month);
  END IF;

  IF now() < v_unlock THEN
    RAISE EXCEPTION '% matches unlock at % UK (Fri 19:00 week)',
      public.competition_gpsl_month_label(v_fixture.gpsl_month),
      to_char(v_unlock AT TIME ZONE 'Europe/London', 'Dy DD Mon YYYY HH24:MI');
  END IF;

  RAISE EXCEPTION '% matches locked since % UK',
    public.competition_gpsl_month_label(v_fixture.gpsl_month),
    to_char(v_lock AT TIME ZONE 'Europe/London', 'Dy DD Mon YYYY HH24:MI');
END;
$function$;

-- Block new submissions outside the active GPSL month (confirm/reject still allowed).
CREATE OR REPLACE FUNCTION public.competition_result_submission_calendar_guard()
RETURNS trigger
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $function$
BEGIN
  PERFORM public.competition_assert_fixture_month_unlocked(NEW.fixture_id);
  RETURN NEW;
END;
$function$;

DROP TRIGGER IF EXISTS competition_result_submission_calendar_guard
  ON public.competition_result_submissions;

CREATE TRIGGER competition_result_submission_calendar_guard
  BEFORE INSERT ON public.competition_result_submissions
  FOR EACH ROW
  EXECUTE FUNCTION public.competition_result_submission_calendar_guard();

-- ---------------------------------------------------------------------------
-- Admin: set anchor (first real-world Fri 19:00 UK = GPSL August unlock)
-- ---------------------------------------------------------------------------

CREATE OR REPLACE FUNCTION public.competition_admin_set_season_calendar(
  p_season_id bigint,
  p_anchor_local timestamp without time zone
)
RETURNS jsonb
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_season public.competition_seasons;
  v_anchor timestamptz;
  v_months text[] := ARRAY[
    'august', 'september', 'october', 'november', 'december',
    'january', 'february', 'march', 'april', 'may'
  ];
  v_month text;
  v_i int;
  v_unlock timestamptz;
  v_lock timestamptz;
BEGIN
  IF NOT public.is_gpsl_admin() THEN
    RAISE EXCEPTION 'Admin only';
  END IF;

  SELECT * INTO v_season
  FROM public.competition_seasons
  WHERE id = p_season_id;

  IF NOT FOUND THEN
    RAISE EXCEPTION 'Season not found';
  END IF;

  IF p_anchor_local IS NULL THEN
    RAISE EXCEPTION 'Anchor date/time required';
  END IF;

  v_anchor := (p_anchor_local AT TIME ZONE 'Europe/London');

  IF extract(dow FROM (v_anchor AT TIME ZONE 'Europe/London'))::int <> 5 THEN
    RAISE EXCEPTION 'Anchor must be a Friday in UK time (got %)',
      to_char(v_anchor AT TIME ZONE 'Europe/London', 'Dy DD Mon YYYY');
  END IF;

  IF extract(hour FROM (v_anchor AT TIME ZONE 'Europe/London'))::int <> 19 THEN
    RAISE EXCEPTION 'Anchor must be 19:00 UK time (7pm)';
  END IF;

  DELETE FROM public.competition_season_calendar WHERE season_id = p_season_id;
  DELETE FROM public.competition_season_calendar_config WHERE season_id = p_season_id;

  INSERT INTO public.competition_season_calendar_config (season_id, anchor_unlock_at)
  VALUES (p_season_id, v_anchor);

  FOR v_i IN 1..array_length(v_months, 1) LOOP
    v_month := v_months[v_i];
    v_unlock := v_anchor + ((v_i - 1) * interval '7 days');
    v_lock := v_unlock + interval '7 days';

    INSERT INTO public.competition_season_calendar (
      season_id, gpsl_month, sort_order, unlock_at, lock_at
    )
    VALUES (p_season_id, v_month, v_i::smallint, v_unlock, v_lock);
  END LOOP;

  RETURN jsonb_build_object(
    'season_id', p_season_id,
    'anchor_uk',
    to_char(v_anchor AT TIME ZONE 'Europe/London', 'YYYY-MM-DD HH24:MI'),
    'months', 10,
    'season_ends_uk',
    to_char((v_anchor + interval '70 days') AT TIME ZONE 'Europe/London', 'YYYY-MM-DD HH24:MI')
  );
END;
$function$;

-- Shift all future month boundaries (insert break / pause).
CREATE OR REPLACE FUNCTION public.competition_admin_insert_calendar_break(
  p_season_id bigint,
  p_weeks integer DEFAULT 1
)
RETURNS int
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $function$
DECLARE
  v_weeks int;
  v_shift interval;
  v_count int;
BEGIN
  IF NOT public.is_gpsl_admin() THEN
    RAISE EXCEPTION 'Admin only';
  END IF;

  v_weeks := greatest(coalesce(p_weeks, 1), 1);
  v_shift := (v_weeks * interval '7 days');

  UPDATE public.competition_season_calendar m
  SET
    unlock_at = m.unlock_at + v_shift,
    lock_at = m.lock_at + v_shift
  WHERE m.season_id = p_season_id
    AND m.lock_at > now();

  GET DIAGNOSTICS v_count = ROW_COUNT;
  RETURN v_count;
END;
$function$;

CREATE OR REPLACE FUNCTION public.competition_admin_clear_season_calendar(p_season_id bigint)
RETURNS void
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $function$
BEGIN
  IF NOT public.is_gpsl_admin() THEN
    RAISE EXCEPTION 'Admin only';
  END IF;

  DELETE FROM public.competition_season_calendar WHERE season_id = p_season_id;
  DELETE FROM public.competition_season_calendar_config WHERE season_id = p_season_id;
END;
$function$;


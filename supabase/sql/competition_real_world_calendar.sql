-- =============================================================================
-- GPSL real-world calendar: 1 UK week (Fri 19:00 → Fri 19:00) = 1 GPSL month
-- Run once after competition_phase1_fixtures.sql (and matchday/cup SQL applied).
-- =============================================================================

CREATE TABLE IF NOT EXISTS public.competition_season_calendar (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  season_id bigint NOT NULL REFERENCES public.competition_seasons (id) ON DELETE CASCADE,
  gpsl_month text NOT NULL CHECK (
    gpsl_month IN (
      'august', 'september', 'october', 'november', 'december',
      'january', 'february', 'march', 'april', 'may'
    )
  ),
  sort_order smallint NOT NULL CHECK (sort_order >= 1 AND sort_order <= 10),
  unlock_at timestamptz NOT NULL,
  lock_at timestamptz NOT NULL,
  created_at timestamptz NOT NULL DEFAULT now(),
  CONSTRAINT competition_season_calendar_unique UNIQUE (season_id, gpsl_month),
  CONSTRAINT competition_season_calendar_window CHECK (lock_at > unlock_at)
);

CREATE INDEX IF NOT EXISTS competition_season_calendar_season_idx
  ON public.competition_season_calendar (season_id, sort_order);

CREATE TABLE IF NOT EXISTS public.competition_season_calendar_config (
  season_id bigint PRIMARY KEY REFERENCES public.competition_seasons (id) ON DELETE CASCADE,
  anchor_unlock_at timestamptz NOT NULL,
  timezone text NOT NULL DEFAULT 'Europe/London',
  updated_at timestamptz NOT NULL DEFAULT now()
);


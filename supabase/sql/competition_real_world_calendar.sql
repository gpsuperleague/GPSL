CREATE OR REPLACE VIEW public.competition_season_calendar_public
WITH (security_invoker = false)
AS
SELECT
  m.season_id,
  s.label AS season_label,
  m.gpsl_month,
  public.competition_gpsl_month_label(m.gpsl_month) AS gpsl_month_label,
  m.sort_order,
  m.unlock_at,
  m.lock_at,
  (now() >= m.unlock_at AND now() < m.lock_at) AS is_active,
  (now() >= m.unlock_at) AS has_started,
  (now() < m.unlock_at) AS is_future,
  (now() >= m.lock_at) AS is_locked
FROM public.competition_season_calendar m
JOIN public.competition_seasons s ON s.id = m.season_id
WHERE s.is_current = true AND s.status = 'active'
ORDER BY m.sort_order;

CREATE OR REPLACE VIEW public.competition_calendar_status_public
WITH (security_invoker = false)
AS
SELECT
  s.id AS season_id,
  s.label AS season_label,
  cfg.anchor_unlock_at,
  cfg.timezone,
  EXISTS (
    SELECT 1 FROM public.competition_season_calendar_config c
    WHERE c.season_id = s.id
  ) AS calendar_configured,
  active.gpsl_month AS active_gpsl_month,
  public.competition_gpsl_month_label(active.gpsl_month) AS active_gpsl_month_label,
  active.unlock_at AS active_unlock_at,
  active.lock_at AS active_lock_at,
  nxt.gpsl_month AS next_gpsl_month,
  nxt.unlock_at AS next_unlock_at,
  CASE
    WHEN active.gpsl_month IS NOT NULL THEN 'in_month'
    WHEN NOT EXISTS (
      SELECT 1 FROM public.competition_season_calendar_config c WHERE c.season_id = s.id
    ) THEN 'pre_season'
    WHEN cfg.anchor_unlock_at IS NOT NULL AND now() < cfg.anchor_unlock_at THEN 'pre_season'
    WHEN now() >= (
      SELECT max(c2.lock_at) FROM public.competition_season_calendar c2 WHERE c2.season_id = s.id
    ) THEN 'after_season'
    ELSE 'between_months'
  END AS calendar_phase,
  CASE
    WHEN active.gpsl_month IS NOT NULL THEN
      public.competition_gpsl_month_label(active.gpsl_month)
    WHEN now() >= (
      SELECT max(c2.lock_at) FROM public.competition_season_calendar c2 WHERE c2.season_id = s.id
    ) THEN 'Post-season'
    WHEN nxt.gpsl_month IS NOT NULL THEN
      public.competition_gpsl_month_label(nxt.gpsl_month)
    ELSE 'Pre-Season'
  END AS nav_month_label
FROM public.competition_seasons s
LEFT JOIN public.competition_season_calendar_config cfg ON cfg.season_id = s.id
LEFT JOIN LATERAL (
  SELECT m.gpsl_month, m.unlock_at, m.lock_at
  FROM public.competition_season_calendar m
  WHERE m.season_id = s.id
    AND now() >= m.unlock_at
    AND now() < m.lock_at
  ORDER BY m.sort_order
  LIMIT 1
) active ON true
LEFT JOIN LATERAL (
  SELECT m.gpsl_month, m.unlock_at
  FROM public.competition_season_calendar m
  WHERE m.season_id = s.id
    AND m.unlock_at > now()
  ORDER BY m.unlock_at
  LIMIT 1
) nxt ON true
WHERE s.is_current = true AND s.status = 'active';


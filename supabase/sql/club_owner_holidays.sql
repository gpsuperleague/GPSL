  FOR SELECT TO authenticated
  USING (club_short_name = public.my_club_shortname());

GRANT SELECT ON public.club_owner_holidays TO authenticated;
GRANT SELECT ON public.club_owner_holidays_public TO authenticated;

GRANT EXECUTE ON FUNCTION public.club_holiday_book(date, date) TO authenticated;
GRANT EXECUTE ON FUNCTION public.club_holiday_cancel(bigint) TO authenticated;
GRANT EXECUTE ON FUNCTION public.club_holiday_days_used(bigint, text) TO authenticated;
GRANT EXECUTE ON FUNCTION public.club_holiday_allows_fixture_early(bigint, text) TO authenticated;
GRANT EXECUTE ON FUNCTION public.competition_assert_fixture_month_unlocked(bigint, text) TO authenticated;

